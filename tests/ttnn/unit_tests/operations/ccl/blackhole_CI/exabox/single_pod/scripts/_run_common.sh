#!/bin/bash
# Common launch helper. Sourced by run_chain_test.sh and run_pipeline_test.sh.
#
# Caller must set:
#   TEST                  pytest test name (no .py prefix)
#   TEST_FILE             test file basename, default = test_fake_moe_traffic.py
#   EXTRA_ENV             extra env vars to inject into the bash -c, e.g. "TT_METAL_SLOW_DISPATCH_MODE=1"
#   PYTEST_TIMEOUT        per-test timeout, default 240
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
. "$SCRIPT_DIR/_hosts.sh"

TT_METAL_HOME="${TT_METAL_HOME:-/data/llong/tt-metal}"

# Pipeline config bundle. Default lives under $TT_METAL_HOME/generated/
# (gitignored) so we don't depend on /tmp. Override with
# SINGLE_POD_PIPELINE_DIR if you need a different location. Generated by
# bootstrap_pipeline_dir.sh (which wraps the per-host PCIe discovery and
# rank-binding generation); auto-bootstrapped below if missing or stale.
PIPELINE_DIR="${SINGLE_POD_PIPELINE_DIR:-${TT_METAL_HOME}/generated/single_pod_pipeline_dir}"
BOOTSTRAP="$SCRIPT_DIR/bootstrap_pipeline_dir.sh"

# Files we expect inside the bundle. If any is missing, bootstrap.
EXPECTED_RB="$PIPELINE_DIR/blitz_decode_pipeline_rank_binding_single_pod_ci.yaml"
EXPECTED_RF="$PIPELINE_DIR/blitz_decode_pipeline_rank_file_single_pod_ci"

if [ ! -d "$PIPELINE_DIR" ] || [ ! -f "$EXPECTED_RB" ] || [ ! -f "$EXPECTED_RF" ]; then
    if [ ! -x "$BOOTSTRAP" ]; then
        echo "[run] ERROR: $PIPELINE_DIR is missing/incomplete and bootstrap not found at $BOOTSTRAP" >&2
        exit 1
    fi
    echo "[run] $PIPELINE_DIR missing or incomplete — auto-bootstrapping..." >&2
    if ! SINGLE_POD_PIPELINE_DIR="$PIPELINE_DIR" "$BOOTSTRAP"; then
        echo "[run] ERROR: bootstrap failed; see RUNBOOK.md §2.4 for manual recovery." >&2
        exit 1
    fi
fi
SSH_WRAPPER="$SCRIPT_DIR/ssh_ulimit_wrapper.sh"
TEST_FILE="${TEST_FILE:-test_fake_moe_traffic.py}"
PYTEST_TIMEOUT="${PYTEST_TIMEOUT:-240}"
EXTRA_ENV="${EXTRA_ENV:-}"

if [ -z "${TEST:-}" ]; then
  echo "Usage: TEST=<test_name> [TEST_FILE=<file.py>] $0" >&2
  exit 2
fi

# PIPELINE_DIR must exist on every remote host (each rank does `cd $PIPELINE_DIR`
# before invoking pytest). /tmp can age out from under us between sessions, so
# we verify and auto-recreate the (mostly-symlink) scaffolding when missing.
ensure_pipeline_dir_on_host() {
  local h="$1"
  if ssh -o BatchMode=yes -o ConnectTimeout=5 "$h" "[ -d $PIPELINE_DIR ]" 2>/dev/null; then
    return 0
  fi
  echo "[run] $h: $PIPELINE_DIR missing — recreating symlink scaffolding..."
  ssh -o BatchMode=yes "$h" "
    mkdir -p $PIPELINE_DIR
    cd $PIPELINE_DIR && mkdir -p .benchmarks generated/{fabric,inspector,test_reports,watcher}
    for sub in build models python_env runtime tests tt_metal ttnn; do
      [ -e \$sub ] || ln -s $TT_METAL_HOME/\$sub \$sub
    done
  "
}
for h in $SINGLE_POD_HOSTS; do ensure_pipeline_dir_on_host "$h"; done

# Build the comma-separated host list with :4 slot suffix (rankfile drives ranks).
HOSTS_WITH_SLOTS="$(echo "$SINGLE_POD_HOSTS" | tr ' ' '\n' | sed 's/$/:4/' | paste -sd,)"

LOG_FILE="/tmp/single_pod_$(date +%Y%m%d_%H%M%S)_${TEST}.log"
echo "[run] PIPELINE_DIR=$PIPELINE_DIR"
echo "[run] TEST=$TEST_FILE::$TEST"
echo "[run] LOG_FILE=$LOG_FILE"
echo "[run] running... (per-rank output multiplexed into $LOG_FILE; this block holds for ~30-60s)"

export PATH=/opt/openmpi-v5.0.7-ulfm/bin:$PATH
ulimit -n 65536
cd "$PIPELINE_DIR"

# Pass the rankfile via the hyphen-free copy that bootstrap_pipeline_dir.sh
# placed at /var/tmp/single_pod_rankfile (override with SINGLE_POD_RANKFILE_PATH).
# Why not use the bundle's rankfile directly: OpenMPI 5.0.7's
# --map-by rankfile:file=PATH parser treats '-' as a qualifier separator,
# so any absolute path containing a hyphen (e.g. anything under
# .../tt-metal/) gets rejected as "unrecognized qualifier". tt-run also
# dereferences symlinks via Path.resolve() before passing to mpirun, so a
# symlink workaround doesn't help — we need a real file at a hyphen-free
# absolute path.
RANKFILE_FOR_MPI="${SINGLE_POD_RANKFILE_PATH:-/var/tmp/single_pod_rankfile}"
if [ ! -f "$RANKFILE_FOR_MPI" ]; then
    echo "[run] ERROR: mpirun-friendly rankfile not found at $RANKFILE_FOR_MPI" >&2
    echo "[run]   bootstrap should have copied it from $PIPELINE_DIR/blitz_decode_pipeline_rank_file_single_pod_ci" >&2
    exit 1
fi

tt-run --skip-executable-check \
  --rank-binding "$PIPELINE_DIR/blitz_decode_pipeline_rank_binding_single_pod_ci.yaml" \
  --mpi-args "--map-by rankfile:file=$RANKFILE_FOR_MPI --bind-to hwt:overload-allowed --host $HOSTS_WITH_SLOTS --tag-output --mca btl_tcp_if_exclude docker0,lo --prtemca plm_rsh_agent $SSH_WRAPPER --prtemca plm_ssh_no_tree_spawn 1" \
  bash -c "ulimit -n 65536; cd $PIPELINE_DIR && source $TT_METAL_HOME/python_env/bin/activate && \
    MESH_DEVICE=QUAD_BH TT_METAL_HOME=$TT_METAL_HOME $EXTRA_ENV \
    pytest -svv --timeout=$PYTEST_TIMEOUT \
    $TT_METAL_HOME/tests/ttnn/unit_tests/operations/ccl/blackhole_CI/exabox/single_pod/$TEST_FILE::$TEST" \
  > "$LOG_FILE" 2>&1
RC=$?

echo "[run] exit=$RC"

# Per-rank summary: count PASSED / FAILED / SKIPPED / ERROR across all 16 ranks,
# and print per-rank wall-clock from each rank's pytest "1 X in …s" line.
NUM_RANKS=$(echo "$SINGLE_POD_HOSTS" | wc -w)
NUM_RANKS=$((NUM_RANKS * 4))   # 4 procs/host
STRIPPED=$(sed 's/\x1b\[[0-9;]*m//g' "$LOG_FILE")
extract_ranks() {  # arg1 = grep pattern; emit unique rank ids
  echo "$STRIPPED" | grep -aE "$1" | sed -E 's/^\[1,([0-9]+)\].*/\1/' | sort -un
}
PASSED_RANKS=$(extract_ranks '^\[1,[0-9]+\]<stdout>: PASSED ' || true)
FAILED_RANKS=$(extract_ranks '^\[1,[0-9]+\]<stdout>: FAILED ' || true)
SKIPPED_RANKS=$(extract_ranks '^\[1,[0-9]+\]<stdout>: SKIPPED ' || true)
ERROR_RANKS=$(extract_ranks '^\[1,[0-9]+\]<stdout>: ERROR ' || true)
# Use `wc -w` (returns 0 on empty input) instead of `grep -c` (returns
# exit 1 on zero matches, which trips `set -e` and aborts the summary).
N_PASSED=$(echo $PASSED_RANKS | wc -w)
N_FAILED=$(echo $FAILED_RANKS | wc -w)
N_SKIPPED=$(echo $SKIPPED_RANKS | wc -w)
N_ERROR=$(echo $ERROR_RANKS | wc -w)

echo "[run] per-rank wall-clock (from pytest '1 X in …s' lines):"
{
  echo "$STRIPPED" \
    | grep -aE '^\[1,[0-9]+\]<stdout>: =+ 1 (passed|failed|skipped|error) in [0-9.]+s' \
    | sed -E 's/^\[1,([0-9]+)\]<stdout>: =+ 1 (passed|failed|skipped|error) in ([0-9.]+s).*/  rank \1: \2 (\3)/' \
    | sort -t: -k1.8n
} || true

# On non-zero exit, dump the last 40 lines so you can see the actual error.
if [ $RC -ne 0 ]; then
  echo "[run] (non-zero exit — tail of log:)"
  echo "$STRIPPED" | tail -40
fi

# Final summary banner (last thing on screen so you don't have to scroll).
# Choose a one-word verdict + colored banner if running in a TTY.
if [ $N_FAILED -gt 0 ] || [ $N_ERROR -gt 0 ] || [ $RC -ne 0 ]; then
  VERDICT="FAILED"; COLOR='\033[1;31m'  # bold red
elif [ $N_PASSED -eq $NUM_RANKS ] && [ $N_SKIPPED -eq 0 ]; then
  VERDICT="PASSED"; COLOR='\033[1;32m'  # bold green
elif [ $((N_PASSED + N_SKIPPED)) -eq $NUM_RANKS ]; then
  VERDICT="PASSED (some skipped)"; COLOR='\033[1;33m'  # bold yellow
else
  VERDICT="INCOMPLETE"; COLOR='\033[1;33m'  # bold yellow
fi
RESET='\033[0m'
[ -t 1 ] || { COLOR=''; RESET=''; }   # no color when not on a TTY

echo
printf "${COLOR}═══════════════════════════════════════════════════════════════${RESET}\n"
printf "${COLOR}[run] %s — PASSED=%d/%d  FAILED=%d  SKIPPED=%d  ERROR=%d${RESET}\n" \
       "$VERDICT" "$N_PASSED" "$NUM_RANKS" "$N_FAILED" "$N_SKIPPED" "$N_ERROR"
[ -n "$FAILED_RANKS" ]  && echo "[run]   failed ranks:  $(echo $FAILED_RANKS  | tr '\n' ' ')"
[ -n "$SKIPPED_RANKS" ] && echo "[run]   skipped ranks: $(echo $SKIPPED_RANKS | tr '\n' ' ')"
[ -n "$ERROR_RANKS" ]   && echo "[run]   error ranks:   $(echo $ERROR_RANKS   | tr '\n' ' ')"
printf "${COLOR}═══════════════════════════════════════════════════════════════${RESET}\n"
exit $RC

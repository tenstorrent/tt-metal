#ifndef _DRAM_PATTERN_FILL_H
#define _DRAM_PATTERN_FILL_H

#include <stdint.h>

#include "patterns/patterns.hpp"

struct DramPatternFillParams {
    uint32_t pattern_id;
    uint32_t seed;
    uint32_t pass_index;
    uint32_t repeat_index;
    uint32_t base_word_index;
    uint32_t word_count;
};

static inline bool dram_pattern_generated_can_be_reused(uint32_t pattern_id) {
    // These patterns produce the same generated chunk contents for every
    // chunk within a pass/repeat configuration, so ping/pong can be filled
    // once and reused. Do not include address/counter/random patterns here.
    switch (pattern_id) {
        case DRAM_PATTERN_CHECKERBOARD:
        case DRAM_PATTERN_MARCHING_ONE_BITS:
        case DRAM_PATTERN_MARCHING_ZERO_BITS:
        case DRAM_PATTERN_TOGGLE_BITS:
        case DRAM_PATTERN_BYTEWISE_SSN: return true;
        default: return false;
    }
}

static inline uint32_t dram_pattern_reuse_period_words(uint32_t pattern_id) {
    // Return the word-period for patterns whose generated L1 chunk can be
    // reused when the next chunk starts at the same phase. A period of 1 means
    // the chunk is independent of absolute word offset. A period of 0 means
    // do not reuse generated chunks.
    switch (pattern_id) {
        case DRAM_PATTERN_CHECKERBOARD:
        case DRAM_PATTERN_MARCHING_ONE_BITS:
        case DRAM_PATTERN_MARCHING_ZERO_BITS: return 1u;

        // dram_pattern_toggle_bits() depends on bit 1 of word_index:
        // 0,0,1,1 repeated, optionally inverted by pass.
        case DRAM_PATTERN_TOGGLE_BITS: return 4u;

        // dram_pattern_bytewise_ssn() depends on (word_index / 2) & 0xFF and
        // word parity, so the full generated sequence repeats every 512 words.
        case DRAM_PATTERN_BYTEWISE_SSN: return 512u;

        default: return 0u;
    }
}

static inline void dram_fill_pattern_buffer_range(
    uint32_t* out_words, const DramPatternFillParams& p, uint32_t local_word_offset, uint32_t local_word_count) {
    const bool use_checkerboard_fastpath = (p.pattern_id == DRAM_PATTERN_CHECKERBOARD);

    const bool use_counter_fastpath = (p.pattern_id == DRAM_PATTERN_COUNTER);

    const bool use_address_fastpath = (p.pattern_id == DRAM_PATTERN_ADDRESS);

    const bool use_marching_one_bits_fastpath = (p.pattern_id == DRAM_PATTERN_MARCHING_ONE_BITS);

    const bool use_marching_zero_bits_fastpath = (p.pattern_id == DRAM_PATTERN_MARCHING_ZERO_BITS);

    if (local_word_count == 0u) {
        return;
    }

    if (use_checkerboard_fastpath) {
        // Match dram_fill_pattern_buffer() exactly: checkerboard parity is
        // relative to the beginning of this generated chunk, not necessarily
        // to the absolute DRAM word address.
        const uint32_t first = (p.pass_index & 1u) ? 0xAAAAAAAAu : 0x55555555u;
        const uint32_t second = ~first;
        for (uint32_t i = 0; i < local_word_count; ++i) {
            out_words[i] = (((local_word_offset + i) & 1u) == 0u) ? first : second;
        }
        return;
    }

    if (use_counter_fastpath) {
        dram_pattern_counter_fill_buffer(out_words, local_word_count, p.seed, p.base_word_index + local_word_offset);
        return;
    }

    if (use_address_fastpath) {
        dram_pattern_address_fill_buffer(
            out_words, local_word_count, p.repeat_index, p.base_word_index + local_word_offset);
        return;
    }

    if (use_marching_one_bits_fastpath) {
        const uint32_t value = dram_pattern_marching_one_bits(p.pass_index);
        dram_pattern_constant_fill_buffer(out_words, local_word_count, value);
        return;
    }

    if (use_marching_zero_bits_fastpath) {
        const uint32_t value = dram_pattern_marching_zero_bits(p.pass_index);
        dram_pattern_constant_fill_buffer(out_words, local_word_count, value);
        return;
    }

    if (p.pattern_id == DRAM_PATTERN_RANDOM) {
        // Preserve the old MATH-only chunk stream. The RNG restarts once per
        // chunk from seed^pass; PACK simply skips the words generated by MATH.
        uint32_t rng_state = p.seed ^ p.pass_index;
        if (rng_state == 0u) {
            rng_state = 1u;
        }
        for (uint32_t i = 0; i < local_word_offset; ++i) {
            rng_state = dram_pattern_random_step(rng_state);
        }
        for (uint32_t i = 0; i < local_word_count; ++i) {
            rng_state = dram_pattern_random_step(rng_state);
            out_words[i] = rng_state;
        }
        return;
    }

    if (p.pattern_id == DRAM_PATTERN_RANDOM_XOSHIRO128PP) {
        // Same idea as DRAM_PATTERN_RANDOM: skip into the exact MATH-only
        // per-chunk stream, then emit this worker's slice.
        DramXoshiro128ppState xoshiro_state = dram_pattern_random_xoshiro128pp_init(p.seed ^ p.pass_index);
        for (uint32_t i = 0; i < local_word_offset; ++i) {
            (void)dram_pattern_random_xoshiro128pp_next(xoshiro_state);
        }
        for (uint32_t i = 0; i < local_word_count; ++i) {
            out_words[i] = dram_pattern_random_xoshiro128pp_next(xoshiro_state);
        }
        return;
    }

    for (uint32_t i = 0; i < local_word_count; ++i) {
        out_words[i] = dram_pattern_generate(
            p.pattern_id, p.seed, p.pass_index, p.base_word_index + local_word_offset + i, p.repeat_index);
    }
}

static inline void dram_fill_pattern_buffer(uint32_t* out_words, const DramPatternFillParams& p) {
    dram_fill_pattern_buffer_range(out_words, p, 0u, p.word_count);
}

#endif /* _DRAM_PATTERN_FILL_H */

def _load_cluster_topology(config_path):
    """
    Load parse_cluster_config from its repo location and parse *config_path*.

    Returns a ClusterTopology instance.
    """
    if not _CLUSTER_PARSER_PATH.exists():
        print(
            f"error: cannot find parse_cluster_config.py at {_CLUSTER_PARSER_PATH}",
            file=sys.stderr,
        )
        sys.exit(1)
    spec = importlib.util.spec_from_file_location("parse_cluster_config", _CLUSTER_PARSER_PATH)
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod.parse_file(str(config_path))


def _build_tray_to_mesh(binding_data, tray_yaml_path):
    """
    Return {tray_id: local_mesh_id} for one host, or {} if the mapping is absent.

    Loads *tray_yaml_path* (tray_to_pcie_device_mapping.yaml), then matches
    each tray's PCIe device set against the TT_VISIBLE_DEVICES in the rank
    bindings to identify which mesh_id owns that tray.
    """
    tray_yaml_path = Path(tray_yaml_path)
    if not tray_yaml_path.exists():
        return {}

    with open(tray_yaml_path) as f:
        tray_map = yaml.safe_load(f)

    # mesh_id -> frozenset of PCIe device IDs from rank bindings
    mesh_to_devices = {}
    for binding in binding_data["rank_bindings"]:
        visible = binding.get("env_overrides", {}).get("TT_VISIBLE_DEVICES", "")
        if visible:
            devs = frozenset(int(d) for d in str(visible).split(",") if d.strip())
            if devs:
                mesh_to_devices[binding["mesh_id"]] = devs

    tray_to_mesh = {}
    for tray_id_raw, pcie_devices in tray_map.get("device_mapping", {}).items():
        tray_id = int(tray_id_raw)
        tray_devs = frozenset(pcie_devices)
        for mesh_id, mesh_devs in mesh_to_devices.items():
            if tray_devs <= mesh_devs:
                tray_to_mesh[tray_id] = mesh_id
                break

    return tray_to_mesh


def _build_inter_host_connections(topology, per_host, tray_to_mesh_per_host, meshes_per_host, instances):
    """
    Derive inter-host connection tuples from *topology* (a ClusterTopology).

    For each physical cable between two different hosts in *per_host*, the
    source tray_id is translated to a local mesh_id via *tray_to_mesh_per_host*,
    then offset to the global mesh_id space.  Cables that connect the same
    global (mesh_a, mesh_b) pair are counted together so a single connection
    block with channels { count: N } is emitted per pair.

    Returns a list of (node_a, node_b, channels_inner, extras) tuples ready
    to append to all_connections.
    """
    hostname_to_idx = {hostname: i for i, (hostname, _) in enumerate(per_host)}

    # Use the descriptor name from the source instances for the inter-host
    # connections.  All instances share the same source textproto so the first
    # entry's descriptor name applies universally.
    desc_name = instances[0][0] if instances else "M0"

    pair_counts = defaultdict(int)
    warned_trays = set()

    for conn in topology.connections:
        host_a = conn.port_a.host
        host_b = conn.port_b.host

        # Skip intra-host cables and cables to hosts outside our list.
        if host_a == host_b:
            continue
        if host_a not in hostname_to_idx or host_b not in hostname_to_idx:
            continue

        idx_a = hostname_to_idx[host_a]
        idx_b = hostname_to_idx[host_b]

        tray_map_a = tray_to_mesh_per_host.get(host_a, {})
        tray_map_b = tray_to_mesh_per_host.get(host_b, {})

        local_mesh_a = tray_map_a.get(conn.port_a.tray_id)
        local_mesh_b = tray_map_b.get(conn.port_b.tray_id)

        if local_mesh_a is None:
            key = (host_a, conn.port_a.tray_id)
            if key not in warned_trays:
                print(
                    f"warning: no mesh_id mapping for {host_a} tray {conn.port_a.tray_id}; "
                    "skipping inter-host connection (is tray_to_pcie_device_mapping.yaml present?)",
                    file=sys.stderr,
                )
                warned_trays.add(key)
            continue

        if local_mesh_b is None:
            key = (host_b, conn.port_b.tray_id)
            if key not in warned_trays:
                print(
                    f"warning: no mesh_id mapping for {host_b} tray {conn.port_b.tray_id}; "
                    "skipping inter-host connection (is tray_to_pcie_device_mapping.yaml present?)",
                    file=sys.stderr,
                )
                warned_trays.add(key)
            continue

        global_mesh_a = local_mesh_a + idx_a * meshes_per_host
        global_mesh_b = local_mesh_b + idx_b * meshes_per_host

        # Normalise so smaller id is always first (undirected pairs).
        if global_mesh_a > global_mesh_b:
            global_mesh_a, global_mesh_b = global_mesh_b, global_mesh_a

        pair_counts[(global_mesh_a, global_mesh_b)] += 1

    return [
        (
            (desc_name, mesh_a),
            (desc_name, mesh_b),
            f"count: {count}",
            {},
        )
        for (mesh_a, mesh_b), count in sorted(pair_counts.items())
    ]


# ---------------------------------------------------------------------------
# Ring remapping helpers
# ---------------------------------------------------------------------------


def _find_ring_ordering(all_instances, all_connections):
    """
    Find a Hamiltonian cycle in the combined mesh graph and return the node
    visit order as a list, starting from the smallest mesh_id.

    Uses backtracking DFS.  The graph is expected to be small (O(10s) of
    nodes) and ring-like, so this terminates quickly in practice.

    Raises ValueError if no Hamiltonian cycle exists.
    """
    nodes = sorted(set(mesh_id for _, mesh_id in all_instances))
    n = len(nodes)
    node_set = set(nodes)

    # Build adjacency (multiple connection blocks between the same pair count
    # as one edge for traversal purposes).
    adj = defaultdict(set)
    for (_, id_a), (_, id_b), _, _ in all_connections:
        adj[id_a].add(id_b)
        adj[id_b].add(id_a)

    start = nodes[0]

    def dfs(path, visited):
        if len(path) == n:
            return start in adj[path[-1]]
        current = path[-1]
        for neighbor in sorted(adj[current]):
            if neighbor in node_set and neighbor not in visited:
                path.append(neighbor)
                visited.add(neighbor)
                if dfs(path, visited):
                    return True
                path.pop()
                visited.remove(neighbor)
        return False

    path = [start]
    if dfs(path, {start}):
        return path

    raise ValueError(
        f"No Hamiltonian cycle found in the combined mesh graph "
        f"(nodes: {nodes}). Verify that --cluster-config inter-host "
        f"connections complete a ring topology."
    )


def _remap_to_ring(all_instances, all_connections, combined_bindings):
    """
    Remap mesh IDs so that the combined graph's Hamiltonian cycle aligns with
    the natural integer sequence 0 → 1 → 2 → … → n-1 → 0.

    The node that currently holds mesh_id 0 keeps id 0; subsequent nodes along
    the cycle receive ids 1, 2, … in traversal order.

    Returns (new_instances, new_connections, new_combined_bindings).
    """
    ring_order = _find_ring_ordering(all_instances, all_connections)
    remap = {old_id: new_id for new_id, old_id in enumerate(ring_order)}
    print(f"ring remapping (old -> new): { {k: v for k, v in sorted(remap.items())} }")

    new_instances = sorted(
        [(desc, remap[mid]) for desc, mid in all_instances],
        key=lambda x: x[1],
    )
    new_connections = [
        ((desc_a, remap[id_a]), (desc_b, remap[id_b]), ch, extras)
        for (desc_a, id_a), (desc_b, id_b), ch, extras in all_connections
    ]
    # Update both mesh_id and rank so that MPI rank N occupies ring position N.
    new_bindings = sorted(
        [{**b, "rank": remap[b["mesh_id"]], "mesh_id": remap[b["mesh_id"]]} for b in combined_bindings],
        key=lambda b: b["rank"],
    )
    return new_instances, new_connections, new_bindings



"""Compare two Tracy CSV reports (main vs branch) for the matmul/SDPA perf sweep.

Reads ``ops_perf_results_*.csv`` files generated by
``python -m tracy -r -v <script>.py`` and extracts only the
``DEVICE KERNEL DURATION [ns]`` column (per ``docs/claude/profiling.md`` —
wall-clock columns include host dispatch overhead and mask kernel-level deltas).

Outputs a per-kernel / per-op-code table:

    OP CODE | GLOBAL CALL COUNT | main ns | branch ns | delta ns | delta %

Plus optional per-RISC columns (BRISC / NCRISC / TRISC0-2). If a given op
code appears more than once on either side (e.g. when running N measure
iterations), rows are aggregated by the mean device-kernel-duration.

Usage (NO dependencies beyond pandas):

    python compare_tracy.py main_reuse_fuse_bias.csv branch_reuse_fuse_bias.csv
    python compare_tracy.py main.csv branch.csv --op-filter Matmul
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

import pandas as pd


DEVICE_KERNEL_DURATION = "DEVICE KERNEL DURATION [ns]"

# Optional per-RISC columns. If present, we emit them too — useful for
# separating reader (BRISC) regressions from compute (TRISC) improvements.
PER_RISC_COLUMNS = [
    "DEVICE BRISC KERNEL DURATION [ns]",
    "DEVICE NCRISC KERNEL DURATION [ns]",
    "DEVICE TRISC0 KERNEL DURATION [ns]",
    "DEVICE TRISC1 KERNEL DURATION [ns]",
    "DEVICE TRISC2 KERNEL DURATION [ns]",
]


def _parse_csv(path: Path) -> pd.DataFrame:
    """Read a Tracy ops perf CSV, coerce the device-duration columns to numeric.

    Unknown / non-numeric cells (e.g. ``"-"`` placeholders Tracy uses for
    host-only rows) are dropped. Rows with no ``DEVICE KERNEL DURATION``
    value are discarded — they represent host-only events that don't matter
    for this comparison.
    """
    if not path.exists():
        raise FileNotFoundError(f"CSV not found: {path}")
    df = pd.read_csv(path)
    if "OP CODE" not in df.columns:
        raise ValueError(f"{path} does not look like an ops_perf_results CSV (no 'OP CODE' column).")
    if DEVICE_KERNEL_DURATION not in df.columns:
        raise ValueError(f"{path} is missing '{DEVICE_KERNEL_DURATION}'.")

    # Coerce numeric columns, drop non-device rows.
    numeric_cols = [DEVICE_KERNEL_DURATION] + [c for c in PER_RISC_COLUMNS if c in df.columns]
    for col in numeric_cols:
        df[col] = pd.to_numeric(df[col], errors="coerce")
    df = df.dropna(subset=[DEVICE_KERNEL_DURATION])
    return df


def _aggregate(df: pd.DataFrame) -> pd.DataFrame:
    """Group by OP CODE, average each numeric column, count rows."""
    numeric_cols = [DEVICE_KERNEL_DURATION] + [c for c in PER_RISC_COLUMNS if c in df.columns]
    agg = df.groupby("OP CODE", as_index=False)[numeric_cols].mean()
    counts = df.groupby("OP CODE", as_index=False).size().rename(columns={"size": "count"})
    return agg.merge(counts, on="OP CODE")


def _build_comparison(main_df: pd.DataFrame, branch_df: pd.DataFrame) -> pd.DataFrame:
    """Outer-join on OP CODE, then compute deltas and delta %."""
    main_agg = _aggregate(main_df).add_suffix("_main")
    main_agg = main_agg.rename(columns={"OP CODE_main": "OP CODE"})
    branch_agg = _aggregate(branch_df).add_suffix("_branch")
    branch_agg = branch_agg.rename(columns={"OP CODE_branch": "OP CODE"})
    merged = main_agg.merge(branch_agg, on="OP CODE", how="outer").sort_values("OP CODE")

    main_col = f"{DEVICE_KERNEL_DURATION}_main"
    branch_col = f"{DEVICE_KERNEL_DURATION}_branch"
    merged["delta_ns"] = merged[branch_col] - merged[main_col]
    merged["delta_%"] = (merged["delta_ns"] / merged[main_col]) * 100.0

    # Build the final view (short column names) — device kernel duration +
    # per-RISC deltas if available.
    cols = ["OP CODE", "count_main", "count_branch", main_col, branch_col, "delta_ns", "delta_%"]
    for risc in PER_RISC_COLUMNS:
        m = f"{risc}_main"
        b = f"{risc}_branch"
        if m in merged.columns and b in merged.columns:
            merged[f"delta_{risc}"] = merged[b] - merged[m]
            cols.extend([m, b, f"delta_{risc}"])
    return merged[cols]


def _print_table(df: pd.DataFrame) -> None:
    # Render with nice width and float precision.
    with pd.option_context(
        "display.max_rows",
        None,
        "display.max_columns",
        None,
        "display.width",
        200,
        "display.float_format",
        "{:.1f}".format,
    ):
        print(df.to_string(index=False))


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__.split("\n\n")[0])
    parser.add_argument("main_csv", type=Path, help="ops_perf_results CSV collected on main")
    parser.add_argument("branch_csv", type=Path, help="ops_perf_results CSV collected on the branch")
    parser.add_argument(
        "--op-filter",
        type=str,
        default=None,
        help="Only show rows whose OP CODE contains this substring (case-insensitive).",
    )
    parser.add_argument(
        "--drop-missing",
        action="store_true",
        help="Drop rows where either side is missing (NaN). Default: keep for visibility.",
    )
    args = parser.parse_args()

    main_df = _parse_csv(args.main_csv)
    branch_df = _parse_csv(args.branch_csv)
    cmp_df = _build_comparison(main_df, branch_df)
    if args.op_filter:
        needle = args.op_filter.lower()
        cmp_df = cmp_df[cmp_df["OP CODE"].str.lower().str.contains(needle, na=False)]
    if args.drop_missing:
        cmp_df = cmp_df.dropna(subset=[f"{DEVICE_KERNEL_DURATION}_main", f"{DEVICE_KERNEL_DURATION}_branch"])
    if cmp_df.empty:
        print("(no rows after filtering)")
        return 1
    print(f"# main : {args.main_csv}")
    print(f"# branch: {args.branch_csv}")
    _print_table(cmp_df)
    return 0


if __name__ == "__main__":
    sys.exit(main())

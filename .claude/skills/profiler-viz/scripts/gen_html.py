def main():
    ap=argparse.ArgumentParser(); ap.add_argument("csv")
    ap.add_argument("--out",default="perf_ops_analysis.html"); ap.add_argument("--wall",type=float,default=67.5)
    ap.add_argument("--title",default="GPT-OSS-120B Global Layer - Per-Op Performance Analysis")
    ap.add_argument("--h1",default="Per-Op Performance Analysis - Global Decoder Layer")
    ap.add_argument("--subtitle",default="GPT-OSS-120B global layer, TP=8, slow dispatch.")
    a=ap.parse_args()
    rows=[]
    for r in csv.DictReader(open(a.csv)):
        rows.append({"op":r["op"],"phase":r["phase"],"time_us":float(r["time_us"]),"pct_wall":float(r["pct_wall"]),
                     "busy_cores":int(r["busy_cores"]),"n_chips":int(r["n_chips"])})
    out=(TMPL.replace("__DATA__",json.dumps(rows)).replace("__WALL__",repr(a.wall))
             .replace("__TITLE__",a.title).replace("__H1__",a.h1).replace("__SUBTITLE__",a.subtitle))
    open(a.out,"w").write(out)
    print("wrote",a.out,"(",len(rows),"ops ) wall=",a.wall)

if __name__=="__main__": main()

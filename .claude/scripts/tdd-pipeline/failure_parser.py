"""Failure Parser — Classify tt-test.sh failures into structured JSON.

Consumes exit codes and output from tt-test.sh, plus triage logs from
hang detection, and produces a structured classification that the
orchestrator can feed to the kernel-writer agent on retry.

Usage:
    from failure_parser import classify_failure
    result = classify_failure(exit_code=2, pytest_output="...", triage_log_path="/tmp/tt-test-triage-dev0.log")
    # result = {"classification": "hang_cb_deadlock", "summary": "...", ...}

Depends on: .claude/scripts/summarize-triage.py (reuses parsing patterns)
Called by: tdd_orchestrator.py test subcommand
"""

import os
import re
from typing import Optional


# Exit codes from tt-test.sh
EXIT_PASS = 0
EXIT_FAIL = 1
EXIT_HANG = 2
EXIT_SETUP_ERROR = 3

# Classification types
CLASS_NUMERICAL_MISMATCH = "numerical_mismatch"
CLASS_COMPILATION_ERROR = "compilation_error"
CLASS_SHAPE_MISMATCH = "shape_mismatch"
CLASS_RUNTIME_ERROR = "runtime_error"
CLASS_WATCHER_ASSERT = "watcher_assert"
CLASS_HANG_CB_DEADLOCK = "hang_cb_deadlock"
CLASS_HANG_NOC_ERROR = "hang_noc_error"
CLASS_HANG_UNKNOWN = "hang_unknown"
CLASS_UNKNOWN = "unknown"

# Suggested actions per classification
SUGGESTED_ACTIONS = {
    CLASS_NUMERICAL_MISMATCH: (
        "Check helper parameters against design. Verify CB data format and tile counts. "
        "Compare tolerance values — intermediate stages may need looser tolerances."
    ),
    CLASS_COMPILATION_ERROR: (
        "Check includes and template parameters. Verify kernel API signatures. "
        "Ensure compute_kernel_hw_startup() is called before helpers."
    ),
    CLASS_SHAPE_MISMATCH: (
        "Verify output shape calculation in ProgramDescriptor. Check work distribution "
        "logic and per-core tile counts."
    ),
    CLASS_RUNTIME_ERROR: (
        "Check runtime args indexing. Verify tensor addresses and buffer sizes. "
        "Look for off-by-one errors in tile counting."
    ),
    CLASS_WATCHER_ASSERT: (
        "Read the assert message and line number. Check the specific kernel file "
        "at that line. Common: unaligned NoC addresses, out-of-bounds CB access."
    ),
    CLASS_HANG_CB_DEADLOCK: (
        "Check CB sync: total pushes must equal total pops. Verify the stuck CB "
        "is fed by its producer. Do NOT add CB ops around helpers."
    ),
    CLASS_HANG_NOC_ERROR: (
        "Check NoC read/write addresses for alignment (32-byte). Verify buffer "
        "addresses are valid DRAM addresses from get_arg_val."
    ),
    CLASS_HANG_UNKNOWN: (
        "Check triage log at /tmp/tt-test-triage-dev0.log for more details. "
        "Verify all three kernels (reader/compute/writer) are making progress."
    ),
    CLASS_UNKNOWN: "Inspect the full pytest output for unexpected errors.",
}


def classify_failure(
    exit_code: int,
    pytest_output: str,
    triage_log_path: str = "/tmp/tt-test-triage-dev0.log",
) -> dict:
    """Classify a test failure into a structured summary.

    Args:
        exit_code: tt-test.sh exit code (1=fail, 2=hang).
        pytest_output: Combined stdout+stderr from pytest run.
        triage_log_path: Path to triage log (generated by hang detection).

    Returns:
        Dict with keys: classification, summary, details, suggested_action.
        See README.md "Failure Classifications" for all classification types.

    Example:
        >>> classify_failure(1, "AssertionError: Max diff: 0.5")
        {"classification": "numerical_mismatch", "summary": "Max diff: 0.5", ...}
    """
    if exit_code == EXIT_PASS:
        return {
            "classification": "pass",
            "summary": "All tests passed",
            "details": {},
            "suggested_action": "None — proceed to advance.",
        }

    if exit_code == EXIT_HANG:
        return _classify_hang(triage_log_path, pytest_output)

    if exit_code == EXIT_FAIL:
        return _classify_test_failure(pytest_output)

    if exit_code == EXIT_SETUP_ERROR:
        return {
            "classification": "setup_error",
            "summary": "tt-test.sh setup error (missing args or environment issue)",
            "details": {"exit_code": exit_code},
            "suggested_action": "Check tt-test.sh invocation arguments and environment.",
        }

    return {
        "classification": CLASS_UNKNOWN,
        "summary": f"Unexpected exit code: {exit_code}",
        "details": {"exit_code": exit_code},
        "suggested_action": SUGGESTED_ACTIONS[CLASS_UNKNOWN],
    }


def _classify_hang(triage_log_path: str, pytest_output: str) -> dict:
    """Parse triage log for: stuck RISC-V, stuck function, stuck CB, core count.

    Args:
        triage_log_path: Path to triage log file.
        pytest_output: Combined stdout+stderr from pytest.

    Returns:
        Dict with classification: hang_cb_deadlock | hang_noc_error | hang_unknown.
    """
    triage_content = ""
    if os.path.isfile(triage_log_path):
        with open(triage_log_path) as f:
            triage_content = f.read()

    # Parse triage summary from pytest output (tt-test.sh prints it to stderr)
    summary_text = pytest_output
    if triage_content:
        summary_text += "\n" + triage_content

    # Look for CB deadlock pattern: cb_wait_front in callstack
    cb_wait_match = re.search(r"cb_wait_front", summary_text)
    stuck_riscv = _extract_stuck_riscv(summary_text)
    stuck_function = _extract_stuck_function(summary_text)
    stuck_cb = _extract_stuck_cb(summary_text)
    core_count = _extract_core_count(summary_text)

    details = {
        "stuck_riscv": stuck_riscv,
        "stuck_function": stuck_function,
        "stuck_cb": stuck_cb,
        "core_count": core_count,
        "triage_path": triage_log_path,
    }

    if cb_wait_match:
        cb_name = stuck_cb or "unknown CB"
        cores_str = f" on {core_count} cores" if core_count else ""
        riscv_str = stuck_riscv or "unknown RISC-V"
        summary = f"{riscv_str} stuck at cb_wait_front({cb_name}){cores_str}"
        return {
            "classification": CLASS_HANG_CB_DEADLOCK,
            "summary": summary,
            "details": details,
            "suggested_action": SUGGESTED_ACTIONS[CLASS_HANG_CB_DEADLOCK],
        }

    # Look for NoC error pattern
    noc_match = re.search(r"noc.*error|NOC.*FAIL|noc_locations.*FAIL", summary_text, re.IGNORECASE)
    if noc_match:
        return {
            "classification": CLASS_HANG_NOC_ERROR,
            "summary": f"Hang with NoC error: {noc_match.group(0)}",
            "details": details,
            "suggested_action": SUGGESTED_ACTIONS[CLASS_HANG_NOC_ERROR],
        }

    # Unknown hang
    return {
        "classification": CLASS_HANG_UNKNOWN,
        "summary": f"Hang detected (stuck: {stuck_function or 'unknown'})",
        "details": details,
        "suggested_action": SUGGESTED_ACTIONS[CLASS_HANG_UNKNOWN],
    }


def _classify_test_failure(pytest_output: str) -> dict:
    """Pattern-match pytest output for failure type.

    Args:
        pytest_output: Combined stdout+stderr from pytest.

    Returns:
        Dict with classification and structured details.
    """
    # Check patterns in priority order

    # Watcher assert (highest priority — hardware-level)
    watcher_match = re.search(r"tripped assert on line (\d+)", pytest_output)
    if watcher_match:
        line_num = watcher_match.group(1)
        # Try to extract the file
        file_match = re.search(r"tripped assert.*?(\S+\.(?:cpp|h|hpp))", pytest_output)
        file_name = file_match.group(1) if file_match else "unknown"
        return {
            "classification": CLASS_WATCHER_ASSERT,
            "summary": f"Watcher assert at line {line_num} in {file_name}",
            "details": {"assert_line": int(line_num), "file": file_name},
            "suggested_action": SUGGESTED_ACTIONS[CLASS_WATCHER_ASSERT],
        }

    # Compilation error
    compile_patterns = [
        r"CompileError",
        r"undefined reference to",
        r"error:\s+",
        r"fatal error:",
        r"No such file or directory.*\.h",
        r"kernel compilation failed",
    ]
    for pattern in compile_patterns:
        compile_match = re.search(pattern, pytest_output)
        if compile_match:
            # Extract a useful snippet around the error
            snippet = _extract_error_snippet(pytest_output, compile_match.start())
            return {
                "classification": CLASS_COMPILATION_ERROR,
                "summary": f"Compilation error: {snippet[:200]}",
                "details": {"error_snippet": snippet},
                "suggested_action": SUGGESTED_ACTIONS[CLASS_COMPILATION_ERROR],
            }

    # Shape mismatch
    shape_match = re.search(r"Shape:.*?(\[[\d, ]+\]).*?expected.*?(\[[\d, ]+\])", pytest_output)
    if not shape_match:
        shape_match = re.search(r"shape mismatch", pytest_output, re.IGNORECASE)
    if shape_match:
        summary = shape_match.group(0)[:200] if shape_match else "Shape mismatch"
        return {
            "classification": CLASS_SHAPE_MISMATCH,
            "summary": summary,
            "details": {},
            "suggested_action": SUGGESTED_ACTIONS[CLASS_SHAPE_MISMATCH],
        }

    # Numerical mismatch
    num_patterns = [
        (r"Max diff:\s*([\d.e+-]+)", "max_diff"),
        (r"not close.*?rtol=([\d.e+-]+).*?atol=([\d.e+-]+)", "tolerance"),
        (r"AssertionError.*Max diff", None),
        (r"assert_close", None),
        (r"allclose", None),
    ]
    for pattern, detail_key in num_patterns:
        num_match = re.search(pattern, pytest_output)
        if num_match:
            details = {}
            if detail_key == "max_diff":
                details["max_diff"] = num_match.group(1)
                summary = f"Numerical mismatch — max diff: {num_match.group(1)}"
            elif detail_key == "tolerance":
                details["rtol"] = num_match.group(1)
                details["atol"] = num_match.group(2)
                summary = f"Numerical mismatch — rtol={num_match.group(1)}, atol={num_match.group(2)}"
            else:
                summary = f"Numerical mismatch: {num_match.group(0)[:150]}"
            return {
                "classification": CLASS_NUMERICAL_MISMATCH,
                "summary": summary,
                "details": details,
                "suggested_action": SUGGESTED_ACTIONS[CLASS_NUMERICAL_MISMATCH],
            }

    # Generic runtime error
    runtime_match = re.search(
        r"(RuntimeError|TypeError|ValueError|KeyError|AttributeError):\s*(.+)",
        pytest_output,
    )
    if runtime_match:
        error_type = runtime_match.group(1)
        error_msg = runtime_match.group(2)[:200]
        return {
            "classification": CLASS_RUNTIME_ERROR,
            "summary": f"{error_type}: {error_msg}",
            "details": {"error_type": error_type, "message": error_msg},
            "suggested_action": SUGGESTED_ACTIONS[CLASS_RUNTIME_ERROR],
        }

    # Unknown failure
    # Try to extract the FAILED line from pytest
    failed_match = re.search(r"FAILED\s+(.+?)(?:\s+-\s+(.+))?$", pytest_output, re.MULTILINE)
    summary = "Unknown test failure"
    if failed_match:
        summary = f"Test failed: {failed_match.group(0)[:200]}"

    return {
        "classification": CLASS_UNKNOWN,
        "summary": summary,
        "details": {},
        "suggested_action": SUGGESTED_ACTIONS[CLASS_UNKNOWN],
    }


def _extract_stuck_riscv(text: str) -> Optional[str]:
    """Extract which RISC-V core is stuck from triage output."""
    # Look in triage summary format: "[1] BRISC (8 cores)"
    match = re.search(r"\[[\d]+\]\s+(BRISC|NCRISC|TRISC\d?)\s+\(", text)
    if match:
        return match.group(1)
    # Look in raw triage table
    match = re.search(r"(BRISC|NCRISC|TRISC\d?).*?cb_wait_front", text)
    if match:
        return match.group(1)
    return None


def _extract_stuck_function(text: str) -> Optional[str]:
    """Extract the function where execution is stuck."""
    # From triage summary: "cb_wait_front()    path:line"
    match = re.search(r"(\w+)\(\)\s+\S+:\d+", text)
    if match:
        return match.group(1) + "()"
    return None


def _extract_stuck_cb(text: str) -> Optional[str]:
    """Extract which CB is causing the deadlock."""
    # Look for CB ID in cb_wait_front context
    match = re.search(r"cb_wait_front\s*\(\s*(\w+)", text)
    if match:
        return match.group(1)
    # Look for "stuck_cb" or similar in summary
    match = re.search(r"cb_(\w+)|CB\s+(\d+)", text)
    if match:
        return match.group(1) or f"cb_{match.group(2)}"
    return None


def _extract_core_count(text: str) -> Optional[int]:
    """Extract the number of stuck cores."""
    match = re.search(r"(\d+)\s+cores?\)", text)
    if match:
        return int(match.group(1))
    return None


def _extract_error_snippet(text: str, pos: int, window: int = 300) -> str:
    """Extract a snippet around an error position.

    Args:
        text: Full output text.
        pos: Character position of the error match.
        window: Number of characters to include around the match.

    Returns:
        A cleaned-up snippet of the error context.
    """
    start = max(0, pos - window // 2)
    end = min(len(text), pos + window // 2)
    snippet = text[start:end].strip()
    # Clean up ANSI escape codes
    snippet = re.sub(r"\x1b\[[0-9;]*m", "", snippet)
    return snippet

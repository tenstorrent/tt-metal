#!/bin/bash
# Export Claude TTNN Agents from .claude/ to ttnn/experimental/claude_ttnn_agents/
#
# Use this when you need to recreate the experimental directory (e.g., for sharing
# with users who don't track .claude/ in git, or for backward compatibility).
#
# Source of truth: .claude/
# This script creates: ttnn/experimental/claude_ttnn_agents/

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"
CLAUDE_DIR="$REPO_ROOT/.claude"
TARGET_DIR="$REPO_ROOT/ttnn/experimental/claude_ttnn_agents"

echo "Exporting Claude TTNN Agents..."
echo "  Source: $CLAUDE_DIR"
echo "  Target: $TARGET_DIR"
echo ""

if [ -d "$TARGET_DIR" ]; then
    read -p "Target directory already exists. Overwrite? (y/n): " -n 1 -r
    echo
    if [[ ! $REPLY =~ ^[Yy]$ ]]; then
        echo "Aborted."
        exit 0
    fi
    rm -rf "$TARGET_DIR"
fi

mkdir -p "$TARGET_DIR"

# Copy agent definitions
if [ -d "$CLAUDE_DIR/agents" ]; then
    cp -r "$CLAUDE_DIR/agents" "$TARGET_DIR/"
    echo "  Exported agents/"
fi

# Copy references
if [ -d "$CLAUDE_DIR/references" ]; then
    cp -r "$CLAUDE_DIR/references" "$TARGET_DIR/"
    echo "  Exported references/"
fi

# Copy scripts (excluding __pycache__)
if [ -d "$CLAUDE_DIR/scripts" ]; then
    mkdir -p "$TARGET_DIR/scripts"
    rsync -a --exclude='__pycache__/' --exclude='export_agents.sh' "$CLAUDE_DIR/scripts/" "$TARGET_DIR/scripts/"
    echo "  Exported scripts/"
fi

# Copy skills
if [ -d "$CLAUDE_DIR/skills" ]; then
    cp -r "$CLAUDE_DIR/skills" "$TARGET_DIR/"
    echo "  Exported skills/"
fi

# Copy CLAUDE.md
if [ -f "$CLAUDE_DIR/CLAUDE.md" ]; then
    cp "$CLAUDE_DIR/CLAUDE.md" "$TARGET_DIR/"
    echo "  Exported CLAUDE.md"
fi

# Copy QUICK_START.md
if [ -f "$CLAUDE_DIR/QUICK_START.md" ]; then
    cp "$CLAUDE_DIR/QUICK_START.md" "$TARGET_DIR/"
    echo "  Exported QUICK_START.md"
fi

# Generate activate_agents.sh that copies back from experimental to .claude
cat > "$TARGET_DIR/activate_agents.sh" << 'ACTIVATE_EOF'
#!/bin/bash
# Activate Claude TTNN Agents
# Copies agents from this directory to .claude/ for Claude Code to use.

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/../../.." && pwd)"

echo "Activating Claude TTNN Agents..."
echo "Repository root: $REPO_ROOT"

mkdir -p "$REPO_ROOT/.claude/agents"
mkdir -p "$REPO_ROOT/.claude/scripts"
mkdir -p "$REPO_ROOT/.claude/references"
mkdir -p "$REPO_ROOT/.claude/skills"
mkdir -p "$REPO_ROOT/.claude/logs"

# Copy agent definitions
for agent in "$SCRIPT_DIR/agents/"*.md; do
    [ -f "$agent" ] && cp "$agent" "$REPO_ROOT/.claude/agents/"
done
echo "  Installed agents/"

# Copy scripts
if [ -d "$SCRIPT_DIR/scripts" ]; then
    cp -r "$SCRIPT_DIR/scripts/"* "$REPO_ROOT/.claude/scripts/"
    find "$REPO_ROOT/.claude/scripts" -name "*.sh" -exec chmod +x {} \;
    find "$REPO_ROOT/.claude/scripts" -name "*.py" -exec chmod +x {} \;
    echo "  Installed scripts/"
fi

# Copy references
if [ -d "$SCRIPT_DIR/references" ]; then
    cp -r "$SCRIPT_DIR/references/"* "$REPO_ROOT/.claude/references/"
    echo "  Installed references/"
fi

# Copy skills
if [ -d "$SCRIPT_DIR/skills" ]; then
    cp -r "$SCRIPT_DIR/skills/"* "$REPO_ROOT/.claude/skills/"
    echo "  Installed skills/"
fi

# Copy CLAUDE.md
if [ -f "$SCRIPT_DIR/CLAUDE.md" ]; then
    if [ -f "$REPO_ROOT/.claude/CLAUDE.md" ]; then
        cp "$REPO_ROOT/.claude/CLAUDE.md" "$REPO_ROOT/.claude/CLAUDE.md.old"
        echo "  Backed up existing .claude/CLAUDE.md"
    fi
    cp "$SCRIPT_DIR/CLAUDE.md" "$REPO_ROOT/.claude/"
    echo "  Installed CLAUDE.md"
fi

echo ""
echo "Claude TTNN Agents activated! Restart Claude Code to use them."
ACTIVATE_EOF
chmod +x "$TARGET_DIR/activate_agents.sh"
echo "  Generated activate_agents.sh"

# Generate README
cat > "$TARGET_DIR/README.md" << 'README_EOF'
# Claude TTNN Agents (Exported)

This directory was generated by `.claude/scripts/export_agents.sh`.

**Source of truth**: `.claude/` (tracked in git)

## Usage

If you don't have `.claude/` tracked in your branch, run:

```bash
./ttnn/experimental/claude_ttnn_agents/activate_agents.sh
```

To regenerate this directory from `.claude/`:

```bash
.claude/scripts/export_agents.sh
```
README_EOF
echo "  Generated README.md"

echo ""
echo "=============================================="
echo "Export complete!"
echo "=============================================="
echo ""
echo "Exported to: $TARGET_DIR"
echo ""

    out.append("         (passing components have their stubs auto-removed; failing ones print the")
    out.append("         failure tail inline.)")
    out.append("")
    out.append(f"  When done with this component, ask for the next task again with the same command.")
    out.append("")
    out.append(sep)
    return "\n".join(out)



def _component_op_count(demo_dir: Path, component_safe: str) -> int:
    """Op count for a graduated component, read from its op-synth manifest
    if present, else 0. Used by `demo_wiring.select_maximal_antichain` as
    a tiebreaker among same-path candidates and surfaced in convergence
    diagnostics to flag which graduated components are the heaviest.

    Schema (per op_emitter.py): `counts` is a dict of
    {op-REUSE, op-ADAPT, op-NEW} -> int. `palette` is a list of ops.
    Sum the counts; fall back to len(palette) if counts is missing."""
    manifest = demo_dir / "_stubs" / f"{component_safe}.opplan.json"
    if not manifest.is_file():
        return 0
    try:
        data = json.loads(manifest.read_text())
    except Exception:
        return 0
    counts = data.get("counts") or {}
    if isinstance(counts, dict) and counts:
        return sum(int(v) for v in counts.values() if isinstance(v, (int, float)))
    palette = data.get("palette") or []
    return len(palette) if isinstance(palette, list) else 0


def emit_runnable_demo(
    *,
    model_id: str,
    repo_root: Path = REPO_ROOT,
    overwrite: bool = True,
) -> Tuple[Optional[Path], str]:
    """Generate a deterministic `demo.py` that wires EVERY graduated
    component into the HF reference model end-to-end.

    Reads `bringup_status.json` + the on-disk graduated stubs and the
    capture manifests, then writes a demo that:
      - Loads the HF reference model.
      - For each graduated component on the maximal antichain (parents
        subsume children), replaces the corresponding HF submodule with
        the TT port built via `build(device, torch_module)`.
      - Exercises each replaced submodule on its captured args/kwargs
        (the same fixtures the per-component PCC test validated against).
      - Asserts non-None output for every wired component and prints
        shapes / a summary line.

    Components not in the wired list stay on CPU as HF reference — that
    is the intended mixed-execution end-to-end pipeline.

    Returns `(demo_path_or_None, status_message)`. Status is one of:
      - `"emitted"`     — demo written successfully.
      - `"no-primary"`  — no graduated component is wirable (no NEW
                          component has both a graduated stub and a
                          recorded submodule_path on disk).
      - `"no-demo-dir"` — model not scaffolded yet."""
    from .demo_wiring import build_wiring_specs, format_wiring_literal

    if repo_root == REPO_ROOT:
        from .discovery import BRINGUP_ROOT as _BRINGUP_ROOT

        repo_root = _BRINGUP_ROOT()
    demo_dir = find_demo_dir(model_id, repo_root=repo_root)
    if demo_dir is None:
        return None, "no-demo-dir"
    status_path = demo_dir / "bringup_status.json"
    if not status_path.is_file():
        return None, "no-demo-dir"
    data = json.loads(status_path.read_text())
    components = data.get("components", []) or []

    specs = build_wiring_specs(
        demo_dir=demo_dir,
        components=components,
        repo_root=repo_root,
        model_id=model_id,
    )
    if not specs:
        return None, "no-primary"

    # Align to tt-metal repo standard: demos live at <demo_dir>/demo/demo.py
    # (matches qwen3_vl/demo/demo.py, bert/demo/demo.py, etc.).
    demo_subdir = demo_dir / "demo"
    demo_subdir.mkdir(parents=True, exist_ok=True)
    pytest_path = str(safe_relative_to_root(demo_subdir / "demo.py")) + "::test_demo"

    body = _MIXED_EXEC_DEMO_TEMPLATE.format(
        model_id=model_id,
        wired_components_literal=format_wiring_literal(specs),
        pytest_path=pytest_path,
    )
    destination = demo_subdir / "demo.py"
    if destination.exists() and not overwrite:
        return destination, "exists"

    if destination.exists():
        bak = destination.with_suffix(".py.before_auto_demo.bak")
        if not bak.exists():
            try:
                shutil.copy2(destination, bak)
            except Exception:
                pass
    destination.write_text(body)
    return destination, "emitted"



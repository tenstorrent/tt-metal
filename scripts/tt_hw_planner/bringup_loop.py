def _component_op_count(demo_dir: Path, component_safe: str) -> int:
    """Op count for a graduated component, read from its op-synth manifest
    if present, else 0. Used by `_pick_primary_component` to choose the
    component with the most ops as the demo's entry point — that's
    typically the model's main forward path (e.g. `encoder_stack` for
    sam2, the full transformer for an LLM).

    Schema (per op_emitter.py): `counts` is a dict of
    {op-REUSE, op-ADAPT, op-NEW} -> int. `palette` is a list of ops.
    Sum the counts; fall back to len(palette) if counts is missing."""
    manifest = demo_dir / "_stubs" / f"{component_safe}.opplan.json"
    if not manifest.is_file():
        return 0
    try:
        data = json.loads(manifest.read_text())
    except Exception:
        return 0
    counts = data.get("counts") or {}
    if isinstance(counts, dict) and counts:
        return sum(int(v) for v in counts.values() if isinstance(v, (int, float)))
    palette = data.get("palette") or []
    return len(palette) if isinstance(palette, list) else 0


def _pick_primary_component(
    demo_dir: Path,
    components: List[Dict[str, Any]],
) -> Optional[Dict[str, Any]]:
    """Pick the primary entry-point component for the demo. Heuristic:
      1. Must be NEW (graduated, not REUSE/ADAPT — those have no
         `_captured/<name>/`).
      2. Must have all four capture artifacts (`args.pt`, `kwargs.pt`,
         `manifest.json`, `output.pt`) on disk.
      3. Among the eligible set, pick the one with the highest op count
         (manifest-derived). Ties broken alphabetically by component name.
    Returns the chosen component dict or None if no eligible component
    exists."""
    eligible: List[Tuple[int, str, Dict[str, Any]]] = []
    for comp in components:
        if comp.get("status") != "NEW":
            continue
        name = comp.get("name", "")
        safe = _safe_id(name)
        cap_dir = demo_dir / "_captured" / safe
        required = (
            cap_dir / "args.pt",
            cap_dir / "kwargs.pt",
            cap_dir / "manifest.json",
        )
        if not all(p.is_file() for p in required):
            continue
        ops = _component_op_count(demo_dir, safe)
        eligible.append((ops, name, comp))
    if not eligible:
        return None
    eligible.sort(key=lambda t: (-t[0], t[1]))
    return eligible[0][2]


def emit_runnable_demo(
    *,
    model_id: str,
    repo_root: Path = REPO_ROOT,
    overwrite: bool = True,
) -> Tuple[Optional[Path], str]:
    """Generate a deterministic `demo.py` for an already-scaffolded model.

    Reads the bringup_status + captured inputs and writes a demo that:
      - Loads the HF reference and resolves the captured submodule path.
      - Loads `_captured/<primary>/{{args,kwargs}}.pt`.
      - Builds the TTNN port via `_stubs/<primary>.build(device, torch_module)`.
      - Invokes the port with captured-input kwargs (with signature
        introspection so TT ports that accept a kwargs subset still work).
      - Asserts the output is non-None and prints its shape.

    Returns `(demo_path_or_None, status_message)`. Status is one of:
      - `"emitted"`     — demo written successfully.
      - `"no-primary"`  — no eligible NEW component with captured inputs.
      - `"no-demo-dir"` — model not scaffolded yet."""
    if repo_root == REPO_ROOT:
        from .discovery import BRINGUP_ROOT as _BRINGUP_ROOT

        repo_root = _BRINGUP_ROOT()
    demo_dir = find_demo_dir(model_id, repo_root=repo_root)
    if demo_dir is None:
        return None, "no-demo-dir"
    status_path = demo_dir / "bringup_status.json"
    if not status_path.is_file():
        return None, "no-demo-dir"
    data = json.loads(status_path.read_text())
    components = data.get("components", []) or []
    primary = _pick_primary_component(demo_dir, components)
    if primary is None:
        return None, "no-primary"

    primary_name = primary["name"]
    primary_safe = _safe_id(primary_name)
    manifest = json.loads((demo_dir / "_captured" / primary_safe / "manifest.json").read_text())
    captured_submodule = manifest.get("submodule_path") or ""
    candidate_paths: List[str] = []
    if captured_submodule:
        candidate_paths.append(captured_submodule)

    candidate_paths.extend(p for p in COMPONENT_SUBMODULE_HINTS.get(primary_name, []) if p and p != captured_submodule)
    if not candidate_paths:
        candidate_paths = ["<UNKNOWN>"]

    pytest_path = str(safe_relative_to_root(demo_dir / "demo.py")) + "::test_demo"

    body = _DEMO_TEMPLATE.format(
        model_id=model_id,
        primary_name=primary_name,
        primary_safe=primary_safe,
        stub_import_path=_stub_import_path(demo_dir, primary_safe, repo_root),
        candidate_paths=candidate_paths,
        pytest_path=pytest_path,
    )
    destination = demo_dir / "demo.py"
    if destination.exists() and not overwrite:
        return destination, "exists"

    if destination.exists():
        bak = destination.with_suffix(".py.before_auto_demo.bak")
        if not bak.exists():
            try:
                shutil.copy2(destination, bak)
            except Exception:
                pass
    destination.write_text(body)
    return destination, "emitted"



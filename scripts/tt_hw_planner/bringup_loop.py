_AUTOFILL_DTYPE_FIX_MARKER = "Bug Y fix (2026-05-23 live-run sam2-hiera-tiny)"


def _autofill_stub_missing_dtype_fix(stub_path: Any) -> bool:
    """Return True iff `stub_path` is an autofill-generated stub that
    predates the Bug Y dtype-coercion fix. Used by the preserve branch
    to force-regenerate stubs whose `_coerce_to_torch` still returns
    bf16 ttnn tensors directly to the HF reference module (which
    crashes with "mat1 and mat2 must have the same dtype")."""
    try:
        text = stub_path.read_text()
    except OSError:
        return False

    if "Generated by `tt_hw_planner bringup --autofill`" not in text or "_get_torch_submodule" not in text:
        return False

    return _AUTOFILL_DTYPE_FIX_MARKER not in text


def _resolve_torch_submodule_for_autofill(
    model: Any,
    component_name: str,
    *,
    discovered_submodule_path: Optional[str] = None,
) -> Tuple[Any, List[str]]:
    """Try every candidate path in `COMPONENT_SUBMODULE_HINTS[component_name]`
    against a loaded HF model. Returns (resolved_submodule_or_None, candidates).

    2026-05-23 audit defect 3 fix: when the component was discovered
    via module-tree decomposition (`bringup_plan._extract_components_
    from_module_tree`), it carries a `submodule_path` that's the
    ACTUAL `named_modules()` path -- no guessing required. Pass it as
    `discovered_submodule_path` and we resolve it directly, bypassing
    the hand-maintained `COMPONENT_SUBMODULE_HINTS` dict (which is
    legacy and only covers the original 6 SAM2-style component
    names)."""

    from .module_tree import resolve_dotted as _resolve

    if discovered_submodule_path:
        try:
            sub = _resolve(model, discovered_submodule_path)
            if sub is not None:
                return sub, [discovered_submodule_path]
        except (AttributeError, IndexError, KeyError, TypeError):
            pass

    candidates = COMPONENT_SUBMODULE_HINTS.get(component_name, [])
    if not candidates:
        return None, ([discovered_submodule_path] if discovered_submodule_path else [])
    for path in candidates:
        try:
            sub = _resolve(model, path)
            if sub is not None:
                return sub, candidates
        except (AttributeError, IndexError, KeyError, TypeError):
            continue
    return None, candidates


def _emit_op_synth_stub(
    *,
    demo_dir: Path,
    component: Dict[str, Any],
    model: Any,
    model_id: str,
) -> Optional[Tuple[str, Dict[str, Any]]]:
    """If the op-classifier can resolve the HF submodule for this component,
    emit a partial native TTNN stub (with weights pre-loaded and
    deterministic op helpers pre-bound) and return (action_label, manifest).
    Returns None if resolution failed and the caller should fall back to
    the torch wrapper."""

    try:
        from .op_classifier import classify_ops_in_component
        from .op_emitter import emit_partial_stub
    except Exception:
        return None

    name = component["name"]
    safe = _safe_id(name)

    discovered_path = component.get("submodule_path")
    sub, candidates = _resolve_torch_submodule_for_autofill(model, name, discovered_submodule_path=discovered_path)
    if sub is None:
        return None

    ops = classify_ops_in_component(sub)
    if not ops:
        return None

    source, manifest = emit_partial_stub(
        component_name=name,
        model_id=model_id,
        hf_reference=component.get("hf_reference") or "",
        submodule_candidates=candidates or ["<UNKNOWN>"],
        ops=ops,
    )

    stub_path = demo_dir / "_stubs" / f"{safe}.py"
    stub_path.write_text(source)
    (demo_dir / "_stubs" / f"{safe}.opplan.json").write_text(json.dumps(manifest, indent=2))

    counts = manifest.get("counts", {})
    label = (
        f"op-synth:reuse={counts.get('op-REUSE', 0)},"
        f"adapt={counts.get('op-ADAPT', 0)},new={counts.get('op-NEW', 0)}"
    )
    return label, manifest


def autofill_stubs(
    *,
    model_id: str,
    repo_root: Path = REPO_ROOT,
    overwrite: bool = False,
    op_synth: bool = False,
) -> List[Tuple[str, str]]:
    """Phase-1 unblock for every NEW component.

    Default behaviour: write a torch-fallback wrapper that delegates the
    component's forward to the HF reference on host CPU.

    With `op_synth=True`: walk the live HF submodule, classify each leaf
    op against the ttnn primitive set, and emit a partial native TTNN
    stub where weights and op-REUSE/op-ADAPT helpers are pre-bound. The
    `__call__` body still falls back to torch so the smoke test passes
    immediately; only the orchestration is left for the LLM. Components
    whose HF submodule cannot be resolved fall back transparently to the
    torch wrapper, so the autofill phase still always produces a runnable
    stub for every NEW component.
    """
    if repo_root == REPO_ROOT:
        from .discovery import BRINGUP_ROOT as _BRINGUP_ROOT

        repo_root = _BRINGUP_ROOT()
    demo_dir = find_demo_dir(model_id, repo_root=repo_root)
    if demo_dir is None:
        raise FileNotFoundError(
            f"No scaffolded demo folder found for {model_id!r}. Run "
            f"`python -m scripts.tt_hw_planner scaffold {model_id} --apply` first."
        )
    status_path = demo_dir / "bringup_status.json"
    data = json.loads(status_path.read_text())

    hf_model: Any = None
    if op_synth:
        try:
            import transformers

            hf_model = transformers.AutoModel.from_pretrained(model_id, trust_remote_code=True)
            hf_model.eval()
        except Exception as exc:
            print(
                f"  [op-synth] could not load HF model {model_id!r} "
                f"({type(exc).__name__}: {exc}); falling back to torch-wrapper "
                f"autofill for every NEW component.",
                flush=True,
            )
            hf_model = None

    actions: List[Tuple[str, str]] = []
    for comp in data.get("components", []):
        if comp.get("status") != "NEW":
            continue
        safe = _safe_id(comp["name"])
        stub_path = demo_dir / "_stubs" / f"{safe}.py"
        body = _render_autofill_stub(
            component_name=comp["name"],
            model_id=model_id,
            hf_reference=comp.get("hf_reference") or "",
            discovered_submodule_path=comp.get("submodule_path"),
        )

        _skip_preserve_branches = False
        if stub_path.exists() and not overwrite:
            existing = stub_path.read_text()

            _manifest_path = demo_dir / "_stubs" / f"{safe}.opplan.json"
            _needs_op_synth_regen = (
                op_synth
                and hf_model is not None
                and "_get_torch_submodule" in existing
                and "_LLM_GAPS" not in existing
                and not _manifest_path.is_file()
            )

            _needs_dtype_fix_regen = hf_model is not None and _autofill_stub_missing_dtype_fix(stub_path)
            if _needs_op_synth_regen:
                actions.append((comp["name"], "regen-as-op-synth:torch-wrapper-upgrade"))
                _skip_preserve_branches = True

            elif _needs_dtype_fix_regen:
                actions.append((comp["name"], "regen:autofill-missing-dtype-fix"))
                _skip_preserve_branches = True

            elif "_get_torch_submodule" in existing or "_LLM_GAPS" in existing:
                actions.append((comp["name"], "preserved:already-autofilled"))

                _safe = _safe_id(comp["name"])
                _test_path = demo_dir / "tests" / "pcc" / f"test_{_safe}.py"
                _existing_is_smoke = (
                    _test_path.is_file() and "Phase-1 SMOKE test" in _test_path.read_text(errors="ignore")[:600]
                )
                _stub_native = _stub_has_graduated_from_autofill(stub_path)
                if _stub_native or (_test_path.is_file() and not _existing_is_smoke):
                    if _stub_native and (not _test_path.is_file() or _existing_is_smoke):
                        _emit_pcc_template(
                            demo_dir=demo_dir,
                            component_name=comp["name"],
                            model_id=model_id,
                            hf_reference=comp.get("hf_reference") or "",
                            new_shape=comp.get("new_shape") or {},
                            repo_root=repo_root,
                            overwrite=True,
                            discovered_submodule_path=comp.get("submodule_path"),
                        )
                else:
                    _emit_autofill_smoke_test(
                        demo_dir=demo_dir,
                        component_name=comp["name"],
                        model_id=model_id,
                        repo_root=repo_root,
                    )
                continue

            if (
                not _skip_preserve_branches
                and "raise NotImplementedError" not in existing
                and "BRING-UP GAP" not in existing
            ):
                test_path = demo_dir / "tests" / "pcc" / f"test_{safe}.py"
                needs_regen = True
                if test_path.is_file():
                    head_text = test_path.read_text(errors="ignore")[:600]
                    needs_regen = "Phase-1 SMOKE test" in head_text
                if needs_regen:
                    _emit_pcc_template(
                        demo_dir=demo_dir,
                        component_name=comp["name"],
                        model_id=model_id,
                        hf_reference=comp.get("hf_reference") or "",
                        new_shape=comp.get("new_shape") or {},
                        repo_root=repo_root,
                        overwrite=True,
                        discovered_submodule_path=comp.get("submodule_path"),
                    )
                label = "preserved:user-edited+pcc-regen" if needs_regen else "preserved:user-edited"
                actions.append((comp["name"], label))
                continue

        wrote = False
        if op_synth and hf_model is not None:
            result = _emit_op_synth_stub(demo_dir=demo_dir, component=comp, model=hf_model, model_id=model_id)
            if result is not None:
                label, _manifest = result
                actions.append((comp["name"], label))
                wrote = True
        if not wrote:
            stub_path.write_text(body)
            actions.append((comp["name"], "written" if not op_synth else "op-synth:fallback-to-torch"))

            stale_manifest = demo_dir / "_stubs" / f"{safe}.opplan.json"
            if stale_manifest.is_file():
                try:
                    stale_manifest.unlink()
                except Exception:
                    pass
        _emit_autofill_smoke_test(
            demo_dir=demo_dir,
            component_name=comp["name"],
            model_id=model_id,
            repo_root=repo_root,
        )
    return actions



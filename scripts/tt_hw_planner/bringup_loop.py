def _build_torch_reference():
    # AutoModelForCausalLM-first fallback: HF causal-LM models
    # (Llama / Qwen / Mistral / DeepSeek / Phi / Gemma / ...) follow
    # the `XxxForCausalLM(model: XxxModel)` structure. `AutoModel.from_pretrained`
    # returns the INNER `XxxModel` (no LM head); captured submodule paths
    # typically reference the OUTER `XxxForCausalLM` (e.g. `model.layers.0`).
    # The path mismatch causes `_resolve` to land on a bare `nn.Module`
    # whose `_forward_unimplemented` raises TypeError on every kwarg.
    # Mirror the multi-class fallback pattern already established in
    # `agentic/probe.py:159`, `output_validation.py:830-965`, and
    # `activation_diff.py:1163-1171` so the PCC test harness resolves
    # the same class the capture step used.
    model = None
    _last_err = None
    for _cls_name in ("AutoModelForCausalLM", "AutoModel"):
        try:
            _cls = getattr(transformers, _cls_name)
        except AttributeError:
            continue
        try:
            model = _cls.from_pretrained(HF_MODEL_ID, trust_remote_code=True, torch_dtype="bfloat16", low_cpu_mem_usage=True)
            break
        except Exception as _e:
            _last_err = _e
            continue
    if model is None:
        try:
            import importlib.util as _ilu
            import os as _os
            _rl = _os.path.join(_os.path.dirname(_os.path.abspath(__file__)), "_reference_loader.py")
            if _os.path.isfile(_rl):
                _spec = _ilu.spec_from_file_location("_reference_loader", _rl)
                _rlmod = _ilu.module_from_spec(_spec)
                _spec.loader.exec_module(_rlmod)
                model = _rlmod.load_reference_model(HF_MODEL_ID)
        except Exception as _rle:
            _last_err = _rle
    if model is None:
        raise RuntimeError(
            f"Could not load {{HF_MODEL_ID}} via AutoModelForCausalLM or "
            f"AutoModel; last error: {{type(_last_err).__name__}}: {{_last_err}}"
        )
    model.eval()
    torch_module = None
    resolved_path = None
    # BUG-2 FIX: if capture-inputs recorded a submodule_path in the manifest,
    # try THAT path FIRST. Capture and test must resolve to the same submodule
    # or captured args won't fit the test-resolved torch_module's signature.
    _captured_path = _captured_submodule_path(COMPONENT_NAME)
    if _captured_path:
        try:
            torch_module = _resolve(model, _captured_path)
            resolved_path = _captured_path
            print(f"[bringup] resolved torch submodule via captured manifest path `{{_captured_path}}`")
        except (AttributeError, IndexError, KeyError, TypeError):
            torch_module = None
            resolved_path = None
    if torch_module is None:
        for path in _CANDIDATE_SUBMODULE_PATHS:
            try:
                torch_module = _resolve(model, path)
                resolved_path = path
                break
            except (AttributeError, IndexError, KeyError, TypeError):
                continue
    if torch_module is None:
        pytest.skip(
            "none of the canonical submodule paths resolved for "
            f"`{component_name}` of {{HF_MODEL_ID}}; edit `_CANDIDATE_SUBMODULE_PATHS`."
        )
    if not (_captured_path and resolved_path == _captured_path):
        print(f"[bringup] resolved torch submodule via `{{resolved_path}}`")

    sig = inspect.signature(torch_module.forward)
    kwargs = {{}}
    primary = None
    for name, param in sig.parameters.items():
        if name == "self":
            continue
        if param.kind in (param.VAR_POSITIONAL, param.VAR_KEYWORD):
            continue
        is_required = param.default is inspect.Parameter.empty
        is_well_known = name in _WELL_KNOWN_INPUTS
        if not is_required and not is_well_known:
            continue
        value = _make_arg_for(name, model=model, torch_module=torch_module)
        if isinstance(value, _Omit):
            continue
        kwargs[name] = value
        if primary is None and isinstance(value, torch.Tensor):
            primary = (name, value)
    if primary is None:
        primary = ("(synthetic)", torch.randn(1, 64, 64))
    return torch_module, kwargs, primary


def _build_ttnn_port(device, torch_module):
    mod = importlib.import_module(STUB_IMPORT_PATH)
    if hasattr(mod, "build"):
        return mod.build(device, torch_module)
    return getattr(mod, COMPONENT_SAFE)


def _normalize_out(out):
    if isinstance(out, tuple) and out:
        out = out[0]
    if hasattr(out, "last_hidden_state"):
        out = out.last_hidden_state
    if isinstance(out, (list, tuple)) and out:
        out = out[0]
    return out


class _StageTimeout(Exception):
    pass


def _stage_timeout_handler(signum, frame):
    raise _StageTimeout(
        "stage exceeded TT_PLANNER_TEST_STAGE_S budget; the previous step "
        "(see last [bringup] stage=... print) did not return in time"
    )


def _set_stage_timeout(seconds: int):
    if seconds <= 0:
        return
    try:
        import signal as _signal
        _signal.signal(_signal.SIGALRM, _stage_timeout_handler)
        _signal.alarm(seconds)
    except (ValueError, AttributeError, ImportError):
        pass


def _clear_stage_timeout():
    try:
        import signal as _signal
        _signal.alarm(0)
    except (ValueError, AttributeError, ImportError):
        pass


def _is_mesh_device(device):
    """Mesh-agnostic helper: True iff `device` is a multi-device mesh."""
    try:
        if isinstance(device, ttnn.MeshDevice):
            return True
    except AttributeError:
        pass
    return hasattr(device, "get_device_ids") or hasattr(device, "get_devices")


def _ttnn_from_torch_mesh_safe(tensor, device, *,
                                dtype=ttnn.bfloat16, layout=ttnn.TILE_LAYOUT):
    """`ttnn.from_torch` that works on both single-device and mesh devices.

    On a mesh, the tensor is replicated to every device so per-device compute
    produces identical shards (readable from device 0)."""
    t = tensor.to(torch.bfloat16) if dtype == ttnn.bfloat16 else tensor
    if _is_mesh_device(device):
        try:
            return ttnn.from_torch(
                t, dtype=dtype, layout=layout, device=device,
                mesh_mapper=ttnn.ReplicateTensorToMesh(device),
            )
        except (AttributeError, TypeError):
            pass
    return ttnn.from_torch(t, dtype=dtype, layout=layout, device=device)


def _ttnn_to_torch_mesh_safe(ttnn_tensor, device):
    """`ttnn.to_torch` that works on both single-device and mesh devices.

    Uses the same `mesh_composer=ConcatMeshToTensor(...)` pattern that the
    existing tt-metal demos use, which is the only known-stable way to read
    back from a MeshDevice (bare `to_torch` and `get_device_tensors[0]` can
    busy-loop in the C extension with the GIL held).

    A device synchronize is issued first so any pending kernels finish
    before the readback (otherwise the readback can deadlock on a kernel
    that never completes)."""
    # CPU-fallback `__call__` paths legitimately return `torch.Tensor`
    # (e.g. when a stub was rolled back to `.bak` after the auto-iterate
    # cap-out, or when a `_apply_*` helper invoked the torch reference
    # because the ttnn path raised). Without this guard,
    # `ttnn.to_torch(...)` would try `tensor.to_torch(mesh_composer=...)`
    # on a torch.Tensor and raise:
    #   AttributeError: 'Tensor' object has no attribute 'to_torch'
    # — turning a clean numerical comparison into an opaque crash.
    # Observed in the 2026-05-22 sam2-hiera-small final pytest after
    # decoder_head was restored from `.bak`.
    if isinstance(ttnn_tensor, torch.Tensor):
        return ttnn_tensor
    try:
        if hasattr(ttnn, "synchronize_device"):
            ttnn.synchronize_device(device)
        elif hasattr(device, "synchronize"):
            device.synchronize()
    except Exception:
        pass

    if _is_mesh_device(device):
        for mk_composer in (
            lambda: ttnn.concat_mesh_to_tensor_composer(device, 0),
            lambda: ttnn.ConcatMeshToTensor(device, dim=0),
        ):
            try:
                composer = mk_composer()
            except (AttributeError, TypeError):
                continue
            try:
                t = ttnn.to_torch(ttnn_tensor, mesh_composer=composer)
                if t is None:
                    continue
                if t.ndim >= 1 and t.shape[0] > 1:
                    n_devices = 1
                    try:
                        ids = device.get_device_ids() if hasattr(device, "get_device_ids") else []
                        n_devices = len(ids) or 1
                    except Exception:
                        pass
                    if n_devices > 1 and t.shape[0] % n_devices == 0:
                        t = t[: t.shape[0] // n_devices]
                return t
            except Exception:
                continue
    return ttnn.to_torch(ttnn_tensor)



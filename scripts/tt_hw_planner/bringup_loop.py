def _tokenize_path(dotted: str):
    """Tokenize an HF submodule path like `encoder.layers[0].mlp` into a
    list of (str|int) tokens. Integers become list indices; strings become
    attribute names. Empty tokens are skipped."""
    toks = []
    for tok in dotted.replace("[", ".").replace("]", "").split("."):
        if not tok:
            continue
        toks.append(int(tok) if tok.isdigit() else tok)
    return toks


def _resolve(obj, dotted: str):
    cur = obj
    for tok in _tokenize_path(dotted):
        if isinstance(tok, int):
            cur = cur[tok]
        else:
            cur = getattr(cur, tok)
    # 2026-06-03 fix: ModuleList/Sequential containers have no forward()
    # — calling them raises NotImplementedError. Auto-pick element [0]
    # so the demo wiring gets a callable module.
    try:
        if isinstance(cur, (torch.nn.ModuleList, torch.nn.Sequential)) and len(cur) > 0:
            cur = cur[0]
    except Exception:
        pass
    return cur


class _TTModuleShim(torch.nn.Module):
    """Thin `nn.Module` wrapper around a TT port.

    PyTorch's `nn.Module.__setattr__` rejects non-Module values when
    the attribute name is registered as a child module on the parent
    (e.g. `hf_model.mask_decoder` was originally a `Sam2VideoMaskDecoder`
    submodule). TT ports written by the auto-loop don't inherit from
    `nn.Module`, so direct `setattr(hf_model, "mask_decoder", tt_port)`
    raises TypeError.

    The shim is an nn.Module so PyTorch accepts the assignment, and
    forwards `__call__`/`forward` to the TT port. The TT port is stored
    via `object.__setattr__` to bypass nn.Module's tensor/module-only
    registration check (the TT port isn't a tensor and may or may not
    be a Module — we store it as a plain Python attribute either way).
    """

    def __init__(self, tt_port):
        super().__init__()
        object.__setattr__(self, "_tt_port", tt_port)

    def forward(self, *args, **kwargs):
        return self._tt_port(*args, **kwargs)


def _set_submodule(model, dotted: str, new_module):
    """Replace `model.<dotted>` with `new_module`. Handles indexed
    paths like `encoder.layers[0]` by walking to the parent and
    assigning via `parent[idx] = ...` for integer-valued last tokens
    and `setattr(parent, attr, ...)` otherwise.

    Non-Module values (e.g. TT ports that don't inherit nn.Module) are
    transparently wrapped in `_TTModuleShim` so PyTorch's strict child-
    module registration accepts the assignment."""
    if not isinstance(new_module, torch.nn.Module):
        new_module = _TTModuleShim(new_module)
    tokens = _tokenize_path(dotted)
    if not tokens:
        raise ValueError("cannot replace the model root")
    parent = model
    for tok in tokens[:-1]:
        if isinstance(tok, int):
            parent = parent[tok]
        else:
            parent = getattr(parent, tok)
    last = tokens[-1]
    if isinstance(last, int):
        parent[last] = new_module
    else:
        setattr(parent, last, new_module)


def _build_hf_model():
    # AutoModelForCausalLM-first fallback (see _PCC_TEST_TEMPLATE
    # ._build_torch_reference for rationale).
    model = None
    _last_err = None
    for _cls_name in ("AutoModelForCausalLM", "AutoModel"):
        try:
            _cls = getattr(transformers, _cls_name)
        except AttributeError:
            continue
        try:
            model = _cls.from_pretrained(HF_MODEL_ID, trust_remote_code=True, torch_dtype="bfloat16", low_cpu_mem_usage=True)
            break
        except Exception as _e:
            _last_err = _e
            continue
    if model is None:
        raise RuntimeError(
            f"Could not load {{HF_MODEL_ID}}: {{type(_last_err).__name__}}: {{_last_err}}"
        )
    model.eval()
    return model


def _build_ttnn_port(device, stub_import_path, torch_module):
    mod = importlib.import_module(stub_import_path)
    if hasattr(mod, "build"):
        return mod.build(device, torch_module)
    raise RuntimeError(
        f"stub `{{stub_import_path}}` has no `build(device, torch_module)` — "
        f"every graduated stub must expose this universal contract."
    )


def _to_tt(x, device):
    # Match the PCC test's input convention: bfloat16 + TILE_LAYOUT. The
    # PCC test validated the LLM-written `__call__` with exactly this
    # boundary, so reusing it here guarantees the demo passes if the PCC
    # test passes. Float tensors are cast to bf16; non-float tensors
    # (attention masks, token ids) pass through with their native dtype.
    if isinstance(x, torch.Tensor):
        if x.is_floating_point():
            x = x.to(torch.bfloat16)
            return ttnn.from_torch(
                x, dtype=ttnn.bfloat16, layout=ttnn.TILE_LAYOUT, device=device
            )
        return ttnn.from_torch(x, layout=ttnn.TILE_LAYOUT, device=device)
    return x


def _normalize_out(out):
    if isinstance(out, ttnn.Tensor):
        out = ttnn.to_torch(ttnn.from_device(out))
    if isinstance(out, tuple) and out:
        out = out[0]
    if hasattr(out, "last_hidden_state"):
        out = out.last_hidden_state
    if isinstance(out, (list, tuple)) and out:
        out = out[0]
    if isinstance(out, ttnn.Tensor):
        out = ttnn.to_torch(ttnn.from_device(out))
    return out


def _load_captured(captured_root: Path, safe: str):
    cap = captured_root / safe
    args = torch.load(cap / "args.pt", map_location="cpu", weights_only=False)
    kwargs = torch.load(cap / "kwargs.pt", map_location="cpu", weights_only=False)
    return args or (), kwargs or {{}}


def _merge_args_into_kwargs(torch_module, captured_args, captured_kwargs):
    """Project captured positional args into kwargs by name using the HF
    reference's forward signature. Matches the PCC test convention so the
    demo passes whenever the PCC test passes."""
    sig = inspect.signature(torch_module.forward)
    names = [
        p.name for p in sig.parameters.values()
        if p.name != "self"
        and p.kind not in (p.VAR_POSITIONAL, p.VAR_KEYWORD)
    ]
    out = dict(captured_kwargs or {{}})
    for i, v in enumerate(captured_args or []):
        if i >= len(names):
            break
        if names[i] not in out:
            out[names[i]] = v
    return out


def _call_tt_module(tt_module, full_kwargs, device):
    """Invoke the TTNN port with captured kwargs converted to ttnn. Handles
    three common `__call__` shapes (positional+kwargs / primary+kwargs /
    kwargs-only) and strips kwargs the port doesn't accept when there's
    no **kwargs catch-all."""
    tt_sig = inspect.signature(tt_module.__call__)
    tt_params = [p for p in tt_sig.parameters.values() if p.name != "self"]
    tt_kwarg_names = {{p.name for p in tt_params if p.kind != p.VAR_POSITIONAL}}
    tt_has_var_kwargs = any(p.kind == p.VAR_KEYWORD for p in tt_params)
    tt_has_var_pos = any(p.kind == p.VAR_POSITIONAL for p in tt_params)
    converted = {{k: _to_tt(v, device) for k, v in full_kwargs.items()}}
    if tt_has_var_pos and tt_has_var_kwargs:
        primary_name = None
        for k, v in converted.items():
            if isinstance(v, ttnn.Tensor):
                primary_name = k
                break
        if primary_name is None:
            return tt_module(**converted)
        primary = converted.pop(primary_name)
        return tt_module(primary, **converted)
    if not tt_has_var_kwargs:
        converted = {{k: v for k, v in converted.items() if k in tt_kwarg_names}}
    return tt_module(**converted)



def autofill_stubs(
    *,
    model_id: str,
    repo_root: Path = REPO_ROOT,
    overwrite: bool = False,
    op_synth: bool = False,
) -> List[Tuple[str, str]]:
    """Phase-1 unblock for every NEW component.

    Default behaviour: write a torch-fallback wrapper that delegates the
    component's forward to the HF reference on host CPU.

    With `op_synth=True`: walk the live HF submodule, classify each leaf
    op against the ttnn primitive set, and emit a partial native TTNN
    stub where weights and op-REUSE/op-ADAPT helpers are pre-bound. The
    `__call__` body still falls back to torch so the smoke test passes
    immediately; only the orchestration is left for the LLM. Components
    whose HF submodule cannot be resolved fall back transparently to the
    torch wrapper, so the autofill phase still always produces a runnable
    stub for every NEW component.
    """
    if repo_root == REPO_ROOT:
        from .discovery import BRINGUP_ROOT as _BRINGUP_ROOT

        repo_root = _BRINGUP_ROOT()
    demo_dir = find_demo_dir(model_id, repo_root=repo_root)
    if demo_dir is None:
        raise FileNotFoundError(
            f"No scaffolded demo folder found for {model_id!r}. Run "
            f"`python -m scripts.tt_hw_planner scaffold {model_id} --apply` first."
        )
    status_path = demo_dir / "bringup_status.json"
    data = json.loads(status_path.read_text())

    hf_model: Any = None
    if op_synth:
        try:
            import transformers

            hf_model = transformers.AutoModel.from_pretrained(model_id, trust_remote_code=True)
            hf_model.eval()
        except Exception as exc:
            print(
                f"  [op-synth] could not load HF model {model_id!r} "
                f"({type(exc).__name__}: {exc}); falling back to torch-wrapper "
                f"autofill for every NEW component.",
                flush=True,
            )
            hf_model = None

    actions: List[Tuple[str, str]] = []
    for comp in data.get("components", []):
        if comp.get("status") != "NEW":
            continue
        safe = _safe_id(comp["name"])
        stub_path = demo_dir / "_stubs" / f"{safe}.py"
        body = _render_autofill_stub(
            component_name=comp["name"],
            model_id=model_id,
            hf_reference=comp.get("hf_reference") or "",
            discovered_submodule_path=comp.get("submodule_path"),
        )

        _skip_preserve_branches = False
        if stub_path.exists() and not overwrite:
            existing = stub_path.read_text()

            _manifest_path = demo_dir / "_stubs" / f"{safe}.opplan.json"
            _needs_op_synth_regen = (
                op_synth
                and hf_model is not None
                and "_get_torch_submodule" in existing
                and "_LLM_GAPS" not in existing
                and not _manifest_path.is_file()
            )

            _needs_dtype_fix_regen = hf_model is not None and _autofill_stub_missing_dtype_fix(stub_path)
            if _needs_op_synth_regen:
                actions.append((comp["name"], "regen-as-op-synth:torch-wrapper-upgrade"))
                _skip_preserve_branches = True

            elif _needs_dtype_fix_regen:
                actions.append((comp["name"], "regen:autofill-missing-dtype-fix"))
                _skip_preserve_branches = True

            elif "_get_torch_submodule" in existing or "_LLM_GAPS" in existing:
                actions.append((comp["name"], "preserved:already-autofilled"))

                _safe = _safe_id(comp["name"])
                _test_path = demo_dir / "tests" / "pcc" / f"test_{_safe}.py"
                _existing_is_smoke = (
                    _test_path.is_file() and "Phase-1 SMOKE test" in _test_path.read_text(errors="ignore")[:600]
                )
                _stub_native = _stub_has_graduated_from_autofill(stub_path)
                if _stub_native or (_test_path.is_file() and not _existing_is_smoke):
                    if _stub_native and (not _test_path.is_file() or _existing_is_smoke):
                        _emit_pcc_template(
                            demo_dir=demo_dir,
                            component_name=comp["name"],
                            model_id=model_id,
                            hf_reference=comp.get("hf_reference") or "",
                            new_shape=comp.get("new_shape") or {},
                            repo_root=repo_root,
                            overwrite=True,
                            discovered_submodule_path=comp.get("submodule_path"),
                        )
                else:
                    _emit_autofill_smoke_test(
                        demo_dir=demo_dir,
                        component_name=comp["name"],
                        model_id=model_id,
                        repo_root=repo_root,
                        hf_reference=comp.get("hf_reference") or "",
                        new_shape=comp.get("new_shape") or {},
                        discovered_submodule_path=comp.get("submodule_path"),
                    )
                continue

            if (
                not _skip_preserve_branches
                and "raise NotImplementedError" not in existing
                and "BRING-UP GAP" not in existing
            ):
                test_path = demo_dir / "tests" / "pcc" / f"test_{safe}.py"
                needs_regen = True
                if test_path.is_file():
                    head_text = test_path.read_text(errors="ignore")[:600]
                    needs_regen = "Phase-1 SMOKE test" in head_text
                if needs_regen:
                    _emit_pcc_template(
                        demo_dir=demo_dir,
                        component_name=comp["name"],
                        model_id=model_id,
                        hf_reference=comp.get("hf_reference") or "",
                        new_shape=comp.get("new_shape") or {},
                        repo_root=repo_root,
                        overwrite=True,
                        discovered_submodule_path=comp.get("submodule_path"),
                    )
                label = "preserved:user-edited+pcc-regen" if needs_regen else "preserved:user-edited"
                actions.append((comp["name"], label))
                continue

        wrote = False
        # Canonical-import path (Option 2, 2026-05-31): when the registry
        # mapped this component to an existing TT impl AND the component
        # was demoted REUSE -> NEW (only happens via force_adapt_all on
        # the escalation path), emit a stub that points the LLM at the
        # canonical impl with no torch-fallback escape hatch. This is the
        # closest-to-graduation starting point: the LLM cannot game pytest
        # by leaving HF AutoModel boilerplate in `__call__` — the stub
        # body raises NotImplementedError until the LLM actually wires
        # the canonical class. Gated on tt_reuse_target so SAM2 cold-start
        # NEW components (no canonical impl, no tt_reuse_target) follow
        # the existing op-synth/torch-fallback path unchanged.
        _tt_reuse_target = comp.get("tt_reuse_target")
        _wrote_canonical_import = False
        if _tt_reuse_target and isinstance(_tt_reuse_target, str) and _tt_reuse_target.strip():
            canonical_body = _render_canonical_import_stub(
                component_name=comp["name"],
                model_id=model_id,
                tt_reuse_target=_tt_reuse_target,
            )
            stub_path.write_text(canonical_body)
            actions.append((comp["name"], f"canonical-import:{_tt_reuse_target}"))
            wrote = True
            _wrote_canonical_import = True
            stale_manifest = demo_dir / "_stubs" / f"{safe}.opplan.json"
            if stale_manifest.is_file():
                try:
                    stale_manifest.unlink()
                except Exception:
                    pass
        if not wrote and op_synth and hf_model is not None:
            result = _emit_op_synth_stub(demo_dir=demo_dir, component=comp, model=hf_model, model_id=model_id)
            if result is not None:
                label, _manifest = result
                actions.append((comp["name"], label))
                wrote = True
        if not wrote:
            stub_path.write_text(body)
            actions.append((comp["name"], "written" if not op_synth else "op-synth:fallback-to-torch"))

            stale_manifest = demo_dir / "_stubs" / f"{safe}.opplan.json"
            if stale_manifest.is_file():
                try:
                    stale_manifest.unlink()
                except Exception:
                    pass
        # Test emission: SMOKE for torch-fallback stubs (the default
        # autofill path) so the build-time test passes immediately and
        # the LLM can iterate later. But for canonical-import stubs
        # (Option 2), the stub starts at NotImplementedError — there is
        # no "passes immediately" semantics — and the only meaningful
        # validation is a REAL PCC test against the captured HF tensors.
        # Emitting a SMOKE test for canonical-import stubs creates a
        # chicken-and-egg: `target_newly_graduated` requires the test
        # NOT be SMOKE, but the SMOKE-to-PCC upgrade requires graduation.
        # Cut the loop by emitting a real PCC test from the start when
        # we know the stub is canonical-import.
        if _wrote_canonical_import:
            _emit_pcc_template(
                demo_dir=demo_dir,
                component_name=comp["name"],
                model_id=model_id,
                hf_reference=comp.get("hf_reference") or "",
                new_shape=comp.get("new_shape") or {},
                repo_root=repo_root,
                overwrite=True,
                discovered_submodule_path=comp.get("submodule_path"),
            )
        else:
            _emit_autofill_smoke_test(
                demo_dir=demo_dir,
                component_name=comp["name"],
                model_id=model_id,
                repo_root=repo_root,
                hf_reference=comp.get("hf_reference") or "",
                new_shape=comp.get("new_shape") or {},
                discovered_submodule_path=comp.get("submodule_path"),
            )
    return actions


def _emit_autofill_smoke_test(
    *,
    demo_dir: Path,
    component_name: str,
    model_id: str,
    repo_root: Path,
    hf_reference: str = "",
    new_shape: Optional[dict] = None,
    discovered_submodule_path: Optional[str] = None,
) -> Path:
    """Emit a real PCC test for an autofilled component.

    SMOKE tests removed 2026-06-01 — they were dead weight: gave no
    graduation signal (filtered out via the ``smoke_tests`` set), the
    "import + doesn't crash" check they provided is already done by
    pytest collection, and the SMOKE-to-PCC upgrade machinery had a
    chicken-and-egg deadlock that blocked graduation indefinitely
    (caught during Qwen2.5-14B verification — graduation needed
    non-SMOKE test, SMOKE upgrade needed graduation).

    The function name is kept for callsite stability; it now delegates
    to ``_emit_pcc_template`` so every component gets a real PCC test
    from iter 0. Graduation safety is preserved by the brain's
    ``_stub_uses_torch_wrapper`` AST check: even if a torch-fallback
    stub's PCC test trivially passes (torch ≡ torch), the brain's
    ``target_is_ast_native`` gate still refuses to graduate the
    component until the stub body uses ``ttnn.*`` ops directly.
    """
    # Auto-upgrade: if an existing test file at this path is a stale
    # SMOKE test (left from a pre-strip run, or restored by overlay
    # replay), force-overwrite it with a real PCC test. Without this,
    # `_emit_pcc_template`'s preserve-existing branch returns the SMOKE
    # template untouched and the brain still treats the component as
    # a SMOKE candidate — re-opening the deadlock we're trying to close.
    safe = _safe_id(component_name)
    test_path_check = demo_dir / "tests" / "pcc" / f"test_{safe}.py"
    _force = False
    if test_path_check.is_file():
        try:
            _head = test_path_check.read_text(errors="ignore")[:600]
            if "Phase-1 SMOKE test" in _head:
                _force = True
        except Exception:
            pass
    test_path, _generated, _already = _emit_pcc_template(
        demo_dir=demo_dir,
        component_name=component_name,
        model_id=model_id,
        hf_reference=hf_reference,
        new_shape=new_shape or {},
        repo_root=repo_root,
        overwrite=_force,
        discovered_submodule_path=discovered_submodule_path,
    )
    return test_path



def _resolve_torch_submodule_for_autofill(
    model: Any,
    component_name: str,
    *,
    discovered_submodule_path: Optional[str] = None,
) -> Tuple[Any, List[str]]:
    """Try every candidate path in `COMPONENT_SUBMODULE_HINTS[component_name]`
    against a loaded HF model. Returns (resolved_submodule_or_None, candidates).

    2026-05-23 audit defect 3 fix: when the component was discovered
    via module-tree decomposition (`bringup_plan._extract_components_
    from_module_tree`), it carries a `submodule_path` that's the
    ACTUAL `named_modules()` path -- no guessing required. Pass it as
    `discovered_submodule_path` and we resolve it directly, bypassing
    the hand-maintained `COMPONENT_SUBMODULE_HINTS` dict (which is
    legacy and only covers the original 6 SAM2-style component
    names)."""

    from .module_tree import resolve_dotted as _resolve

    if discovered_submodule_path:
        try:
            sub = _resolve(model, discovered_submodule_path)
            if sub is not None:
                return sub, [discovered_submodule_path]
        except (AttributeError, IndexError, KeyError, TypeError):
            pass

    candidates = COMPONENT_SUBMODULE_HINTS.get(component_name, [])
    if not candidates:
        return None, ([discovered_submodule_path] if discovered_submodule_path else [])
    for path in candidates:
        try:
            sub = _resolve(model, path)
            if sub is not None:
                return sub, candidates
        except (AttributeError, IndexError, KeyError, TypeError):
            continue
    return None, candidates



# would otherwise correctly mark MIXED. 24576 (24 KiB) matches the
# established convention used by every existing vision demo in this repo
# (see e.g. `models/demos/vision/segmentation/sam2_hiera_small/demo/
# demo_for_image_classification.py:98`). It is harmless on models that
# don't use conv2d/max_pool2d — those L1_SMALL banks just stay empty.

def _tokenize_path(dotted: str):
    """Tokenize an HF submodule path like `encoder.layers[0].mlp` into a
    list of (str|int) tokens. Integers become list indices; strings become
    attribute names. Empty tokens are skipped."""
    toks = []
    for tok in dotted.replace("[", ".").replace("]", "").split("."):
        if not tok:
            continue
        toks.append(int(tok) if tok.isdigit() else tok)
    return toks


def _resolve(obj, dotted: str):
    cur = obj
    for tok in _tokenize_path(dotted):
        if isinstance(tok, int):
            cur = cur[tok]
        else:
            cur = getattr(cur, tok)
    return cur


class _TTModuleShim(torch.nn.Module):
    """Thin `nn.Module` wrapper around a TT port.

    PyTorch's `nn.Module.__setattr__` rejects non-Module values when
    the attribute name is registered as a child module on the parent
    (e.g. `hf_model.mask_decoder` was originally a `Sam2VideoMaskDecoder`
    submodule). TT ports written by the auto-loop don't inherit from
    `nn.Module`, so direct `setattr(hf_model, "mask_decoder", tt_port)`
    raises TypeError.

    The shim is an nn.Module so PyTorch accepts the assignment, and
    forwards `__call__`/`forward` to the TT port. The TT port is stored
    via `object.__setattr__` to bypass nn.Module's tensor/module-only
    registration check (the TT port isn't a tensor and may or may not
    be a Module — we store it as a plain Python attribute either way).
    """

    def __init__(self, tt_port):
        super().__init__()
        object.__setattr__(self, "_tt_port", tt_port)

    def forward(self, *args, **kwargs):
        return self._tt_port(*args, **kwargs)


def _set_submodule(model, dotted: str, new_module):
    """Replace `model.<dotted>` with `new_module`. Handles indexed
    paths like `encoder.layers[0]` by walking to the parent and
    assigning via `parent[idx] = ...` for integer-valued last tokens
    and `setattr(parent, attr, ...)` otherwise.

    Non-Module values (e.g. TT ports that don't inherit nn.Module) are
    transparently wrapped in `_TTModuleShim` so PyTorch's strict child-
    module registration accepts the assignment."""
    if not isinstance(new_module, torch.nn.Module):
        new_module = _TTModuleShim(new_module)
    tokens = _tokenize_path(dotted)
    if not tokens:
        raise ValueError("cannot replace the model root")
    parent = model
    for tok in tokens[:-1]:
        if isinstance(tok, int):
            parent = parent[tok]
        else:
            parent = getattr(parent, tok)
    last = tokens[-1]
    if isinstance(last, int):
        parent[last] = new_module
    else:
        setattr(parent, last, new_module)



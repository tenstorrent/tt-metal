def _to_tt(x, device):
    # Match the PCC test's input convention: bfloat16 + TILE_LAYOUT. The
    # PCC test validated the LLM-written `__call__` with exactly this
    # boundary, so reusing it here guarantees the demo passes if the PCC
    # test passes. Float tensors are cast to bf16; non-float tensors
    # (attention masks, token ids) pass through with their native dtype.
    if isinstance(x, torch.Tensor):
        if x.is_floating_point():
            x = x.to(torch.bfloat16)
            return ttnn.from_torch(
                x, dtype=ttnn.bfloat16, layout=ttnn.TILE_LAYOUT, device=device
            )
        return ttnn.from_torch(x, layout=ttnn.TILE_LAYOUT, device=device)
    return x


def _normalize_out(out):
    if isinstance(out, ttnn.Tensor):
        out = ttnn.to_torch(ttnn.from_device(out))
    if isinstance(out, tuple) and out:
        out = out[0]
    if hasattr(out, "last_hidden_state"):
        out = out.last_hidden_state
    if isinstance(out, (list, tuple)) and out:
        out = out[0]
    if isinstance(out, ttnn.Tensor):
        out = ttnn.to_torch(ttnn.from_device(out))
    return out


def _load_captured(captured_root: Path, safe: str):
    cap = captured_root / safe
    args = torch.load(cap / "args.pt", map_location="cpu", weights_only=False)
    kwargs = torch.load(cap / "kwargs.pt", map_location="cpu", weights_only=False)
    return args or (), kwargs or {{}}


def _merge_args_into_kwargs(torch_module, captured_args, captured_kwargs):
    """Project captured positional args into kwargs by name using the HF
    reference's forward signature. Matches the PCC test convention so the
    demo passes whenever the PCC test passes."""
    sig = inspect.signature(torch_module.forward)
    names = [
        p.name for p in sig.parameters.values()
        if p.name != "self"
        and p.kind not in (p.VAR_POSITIONAL, p.VAR_KEYWORD)
    ]
    out = dict(captured_kwargs or {{}})
    for i, v in enumerate(captured_args or []):
        if i >= len(names):
            break
        if names[i] not in out:
            out[names[i]] = v
    return out


def _call_tt_module(tt_module, full_kwargs, device):
    """Invoke the TTNN port with captured kwargs converted to ttnn. Handles
    three common `__call__` shapes (positional+kwargs / primary+kwargs /
    kwargs-only) and strips kwargs the port doesn't accept when there's
    no **kwargs catch-all."""
    tt_sig = inspect.signature(tt_module.__call__)
    tt_params = [p for p in tt_sig.parameters.values() if p.name != "self"]
    tt_kwarg_names = {{p.name for p in tt_params if p.kind != p.VAR_POSITIONAL}}
    tt_has_var_kwargs = any(p.kind == p.VAR_KEYWORD for p in tt_params)
    tt_has_var_pos = any(p.kind == p.VAR_POSITIONAL for p in tt_params)
    converted = {{k: _to_tt(v, device) for k, v in full_kwargs.items()}}
    if tt_has_var_pos and tt_has_var_kwargs:
        primary_name = None
        for k, v in converted.items():
            if isinstance(v, ttnn.Tensor):
                primary_name = k
                break
        if primary_name is None:
            return tt_module(**converted)
        primary = converted.pop(primary_name)
        return tt_module(primary, **converted)
    if not tt_has_var_kwargs:
        converted = {{k: v for k, v in converted.items() if k in tt_kwarg_names}}
    return tt_module(**converted)



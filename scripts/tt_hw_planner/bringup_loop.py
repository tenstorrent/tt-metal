_AUTOFILL_TORCH_TEMPLATE = '''# SPDX-FileCopyrightText: © 2026 Tenstorrent USA, Inc.
#
# SPDX-License-Identifier: Apache-2.0

"""HF reference fallback for `{component_name}` of {model_id}.

Generated by `tt_hw_planner bringup --autofill`. This runs the upstream
HF PyTorch module on host CPU. It exists so the demo pipeline runs
end-to-end on TT hardware (device opened, dispatch path exercised) while
you incrementally replace each fallback with a real TTNN port in Phase 2.

Replace the body of `{component_safe}` with TTNN ops when you're ready
to move this component off host. The HF submodule access paths the
fallback tries are listed below — use them as the spec for your port.

HF reference: {hf_reference}
"""

from __future__ import annotations

import transformers


HF_MODEL_ID = "{model_id}"

_CANDIDATE_SUBMODULE_PATHS = {submodule_candidates!r}

_cache: dict = {{}}


def _resolve(obj, dotted: str):
    cur = obj
    for tok in dotted.replace("[", ".").replace("]", "").split("."):
        if tok == "":
            continue
        if tok.isdigit():
            cur = cur[int(tok)]
        else:
            cur = getattr(cur, tok)
    return cur


def _get_torch_submodule():
    if "module" in _cache:
        return _cache["module"]
    model = transformers.AutoModel.from_pretrained(HF_MODEL_ID, trust_remote_code=True)
    model.eval()
    resolved = None
    for path in _CANDIDATE_SUBMODULE_PATHS:
        try:
            resolved = _resolve(model, path)
            break
        except (AttributeError, IndexError, KeyError, TypeError):
            continue
    if resolved is None:
        raise RuntimeError(
            "AUTOFILL: could not resolve `{component_name}` for "
            f"{{HF_MODEL_ID}} from any of {{_CANDIDATE_SUBMODULE_PATHS}}. "
            "Edit _CANDIDATE_SUBMODULE_PATHS in this file."
        )
    _cache["module"] = resolved
    return resolved


def _coerce_to_torch(x):
    """Recursively convert any `ttnn.Tensor` to `torch.Tensor` so the
    HF PyTorch module can consume our inputs.

    The Phase-2 PCC test passes a `ttnn.Tensor` as the primary input
    (and the original torch kwargs for the rest). The torch reference
    cannot consume ttnn tensors — it crashes on calls like
    `pixel_values.to(self.projection.weight.dtype)`. This boundary
    coercion is the cheap fix that lets every Phase-1 fallback work
    against the same Phase-2 test as a real native port would."""
    try:
        import ttnn as _ttnn
        if isinstance(x, _ttnn.Tensor):
            import torch as _torch
            t = _ttnn.to_torch(x)
            # Bug Y fix (2026-05-23 live-run sam2-hiera-tiny):
            # HF reference modules carry fp32 weights by default. When
            # the test feeds a bf16 device tensor (the standard
            # Blackhole dtype), the post-conversion torch tensor
            # inherits bf16 and the HF forward raises
            #   RuntimeError: mat1 and mat2 must have the same dtype,
            #   but got BFloat16 and Float
            # Promote float-typed tensors to fp32 unconditionally so
            # the autofill safety net runs against any device dtype.
            # Non-float (e.g. integer attention masks / token ids)
            # tensors are passed through unchanged.
            if t.is_floating_point() and t.dtype != _torch.float32:
                t = t.to(_torch.float32)
            return t
    except Exception:
        pass
    if isinstance(x, tuple):
        return tuple(_coerce_to_torch(e) for e in x)
    if isinstance(x, list):
        return [_coerce_to_torch(e) for e in x]
    if isinstance(x, dict):
        return {{k: _coerce_to_torch(v) for k, v in x.items()}}
    return x


def {component_safe}(*args, **kwargs):
    """Phase-1 fallback: invokes the HF PyTorch submodule directly."""
    args = tuple(_coerce_to_torch(a) for a in args)
    kwargs = {{k: _coerce_to_torch(v) for k, v in kwargs.items()}}
    return _get_torch_submodule()(*args, **kwargs)
'''



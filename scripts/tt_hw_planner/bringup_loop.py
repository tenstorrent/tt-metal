def _stub_import_path(demo_dir: Path, component_safe: str, repo_root: Path) -> str:
    from .discovery import BRINGUP_ROOT, REPO_ROOT as _CANONICAL_REPO_ROOT

    full = demo_dir / "_stubs" / f"{component_safe}.py"
    candidates = []
    if Path(repo_root).resolve() == _CANONICAL_REPO_ROOT.resolve():
        candidates.append(BRINGUP_ROOT())
    candidates.append(Path(repo_root))
    for root in candidates:
        try:
            rel = full.resolve().relative_to(Path(root).resolve())
            return ".".join(rel.with_suffix("").parts)
        except ValueError:
            continue
    rel = safe_relative_to_root(full)
    return ".".join(rel.with_suffix("").parts)


def _stub_has_graduated_from_autofill(stub_path: Path) -> bool:
    """True iff the stub HAS BEEN VERIFIED-GRADUATED (PCC passed against
    native ttnn code) at some point.

    Detection requires BOTH:

      1. Positive graduation evidence: a ``.py.last_good_native`` snapshot
         sibling exists. That file is written ONLY by
         ``_snapshot_native_stub`` in the auto-iterate loop, upon a PCC
         test passing. Its existence means "at some point this component
         had a working native ttnn forward verified by PCC."

      2. Current stub does NOT delegate to torch fallback. AST-aware
         detection so we don't false-positive on the LLM leaving
         ``_get_torch_submodule`` as a dead helper while writing native
         ttnn elsewhere. Catches the case where a graduated stub was
         later edited back to fallback (rare but possible).

    Why BOTH: previously this function returned True for any stub
    without torch-fallback markers — which mis-classified op-synth
    scaffolded stubs (no fallback markers, but the body is just
    pre-bound ``_apply_*`` helpers that were never PCC-validated) as
    graduated. The snapshot-exists check is positive evidence; the
    fallback-absence check guards against later regressions.
    """
    if not stub_path.is_file():
        return False
    # Positive evidence: PCC graduation snapshot from a prior successful
    # iteration. No snapshot → never graduated, no matter what the
    # current stub body looks like (could be placeholder, scaffold,
    # in-progress LLM draft, etc.).
    snapshot = stub_path.with_suffix(".py.last_good_native")
    if not snapshot.is_file():
        return False
    try:
        text = stub_path.read_text(errors="ignore")
    except Exception:
        return False
    try:
        import ast

        tree = ast.parse(text)
    except SyntaxError:
        head = text[:2000]
        if "_get_torch_submodule" in head:
            return False
        if "self._torch_module" in head or "_coerce_to_torch" in head:
            return False
        return True

    forbidden_calls = {"_get_torch_submodule", "_coerce_to_torch"}
    forward_method_names = {"__call__", "forward"}
    helper_function_names = {"_get_torch_submodule", "_resolve", "_coerce_to_torch"}

    def _name_of_call(node) -> str:
        try:
            if isinstance(node, ast.Call):
                f = node.func
                if isinstance(f, ast.Name):
                    return f.id
                if isinstance(f, ast.Attribute):
                    return f.attr
        except Exception:
            pass
        return ""

    def _calls_self_torch_module(n) -> bool:
        forbidden_torch_methods = {
            "forward",
            "__call__",
            "_call_impl",
            "_forward_impl",
        }
        try:
            if isinstance(n, ast.Call) and isinstance(n.func, ast.Attribute):
                f = n.func
                if isinstance(f.value, ast.Name) and f.value.id == "self" and f.attr == "_torch_module":
                    return True
            if isinstance(n, ast.Call) and isinstance(n.func, ast.Attribute):
                inner = n.func.value
                if (
                    isinstance(inner, ast.Attribute)
                    and inner.attr == "_torch_module"
                    and isinstance(inner.value, ast.Name)
                    and inner.value.id == "self"
                    and n.func.attr in forbidden_torch_methods
                ):
                    return True
        except Exception:
            pass
        return False

    def _calls_fallback(fn) -> bool:
        for n in ast.walk(fn):
            if isinstance(n, ast.With):
                for item in n.items:
                    if isinstance(item.context_expr, ast.Call):
                        if _name_of_call(item.context_expr) in {"torch.no_grad", "no_grad"}:
                            return True
            if isinstance(n, ast.Call) and _name_of_call(n) in forbidden_calls:
                return True
            if _calls_self_torch_module(n):
                return True
        return False

    for node in ast.walk(tree):
        if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
            if node.name in helper_function_names:
                continue
            if node.name in forward_method_names and _calls_fallback(node):
                return False
            if _calls_fallback(node) and node.name not in helper_function_names:
                return False
    return True


def _emit_pcc_template(
    *,
    demo_dir: Path,
    component_name: str,
    model_id: str,
    hf_reference: str,
    new_shape: dict,
    repo_root: Path,
    overwrite: bool = False,
    discovered_submodule_path: Optional[str] = None,
) -> Tuple[Path, bool, bool]:
    pcc_dir = demo_dir / "tests" / "pcc"
    pcc_dir.mkdir(parents=True, exist_ok=True)
    safe = _safe_id(component_name)
    fname = f"test_{safe}.py"
    test_path = pcc_dir / fname

    try:
        from .overlay_manager import is_no_emit_test

        if is_no_emit_test(model_id, component_name):
            return test_path, False, test_path.exists()
    except Exception:
        pass

    if test_path.exists() and not overwrite:
        return test_path, False, True

    submodule_candidates = list(COMPONENT_SUBMODULE_HINTS.get(component_name, []))
    if not discovered_submodule_path:
        try:
            import json as _json

            manifest_p = demo_dir / "_captured" / safe / "manifest.json"
            if manifest_p.is_file():
                m = _json.loads(manifest_p.read_text())
                discovered_submodule_path = (m or {}).get("submodule_path") or None
        except Exception:
            pass
    if discovered_submodule_path and discovered_submodule_path not in submodule_candidates:
        submodule_candidates.insert(0, discovered_submodule_path)
    if not submodule_candidates:
        submodule_candidates = ["<UNKNOWN>"]
    body = _PCC_TEST_TEMPLATE.format(
        component_name=component_name,
        component_safe=safe,
        model_id=model_id,
        hf_reference=hf_reference or "(no HF reference resolved)",
        new_shape=new_shape or {},
        submodule_candidates=submodule_candidates,
        sample_input_expr=_shape_to_torch_randn(new_shape or {}),
        stub_import_path=_stub_import_path(demo_dir, safe, repo_root),
    )
    test_path.write_text(body)
    return test_path, True, False


def _run_pytest_target(test_path: Path, *, exclude_k: str) -> Tuple[str, str]:
    if not test_path.exists():
        return ("missing", "test file not on disk")
    cmd = [
        "pytest",
        "-svv",
        str(test_path),
        "-k",
        exclude_k,
    ]
    import os as _os
    import signal as _signal

    timeout_s = int(_os.environ.get("TT_PLANNER_PYTEST_TIMEOUT_S", "600"))
    from .discovery import BRINGUP_ROOT

    proc = subprocess.Popen(
        cmd,
        cwd=str(BRINGUP_ROOT()),
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        text=True,
        start_new_session=True,
    )
    try:
        stdout, stderr = proc.communicate(timeout=timeout_s)
        timed_out = False
    except subprocess.TimeoutExpired:
        timed_out = True
        try:
            _os.killpg(_os.getpgid(proc.pid), _signal.SIGTERM)
        except ProcessLookupError:
            pass
        try:
            stdout, stderr = proc.communicate(timeout=10)
        except subprocess.TimeoutExpired:
            try:
                _os.killpg(_os.getpgid(proc.pid), _signal.SIGKILL)
            except ProcessLookupError:
                pass
            try:
                stdout, stderr = proc.communicate(timeout=5)
            except subprocess.TimeoutExpired:
                stdout, stderr = "", ""
    tail = "\n".join((stdout or "").splitlines()[-12:])
    err = "\n".join((stderr or "").splitlines()[-6:])
    summary = (tail + ("\n" + err if err else "")).strip()
    if timed_out:
        summary = (
            f"TIMEOUT after {timeout_s}s (likely a ttnn device-side deadlock; "
            f"pytest-timeout signals cannot interrupt C++ device calls)\n" + summary
        ).strip()
        return ("failed", summary)
    status = "passed" if proc.returncode == 0 else "failed"
    return (status, summary)


def run_bringup_loop(
    *,
    model_id: str,
    repo_root: Path = REPO_ROOT,
    emit_tests: bool = True,
    run_tests: bool = False,
    remove_passing_stubs: bool = True,
    overwrite_tests: bool = False,
    exclude_k: str = DEFAULT_TEMPLATE_PYTEST_EXCLUDE_K,
) -> BringupLoopResult:
    if repo_root == REPO_ROOT:
        from .discovery import BRINGUP_ROOT as _BRINGUP_ROOT

        repo_root = _BRINGUP_ROOT()
    demo_dir = find_demo_dir(model_id, repo_root=repo_root)
    if demo_dir is None:
        raise FileNotFoundError(
            f"No scaffolded demo folder found for {model_id!r}. Run "
            f"`python -m scripts.tt_hw_planner scaffold {model_id} --apply` first."
        )

    status_path = demo_dir / "bringup_status.json"
    data = json.loads(status_path.read_text())
    components = data.get("components", [])
    counts_before = data.get("counts", {})

    actions: List[StubAction] = []
    notes: List[str] = []

    for comp in components:
        if comp.get("status") != "NEW":
            continue
        name = comp["name"]
        stub_rel = demo_dir / "_stubs" / f"{_safe_id(name)}.py"
        hf_ref = comp.get("hf_reference") or ""
        new_shape = comp.get("new_shape") or {}

        action = StubAction(
            component=name,
            stub_path=str(safe_relative_to_root(stub_rel)),
            test_path="",
            hf_reference=hf_ref,
        )

        if emit_tests:
            stub_graduated = _stub_has_graduated_from_autofill(stub_rel)
            existing_test = demo_dir / "tests" / "pcc" / f"test_{_safe_id(name)}.py"
            existing_is_smoke = existing_test.exists() and "Phase-1 SMOKE test" in existing_test.read_text()[:600]
            force = overwrite_tests or (stub_graduated and existing_is_smoke)
            test_path, generated, already = _emit_pcc_template(
                demo_dir=demo_dir,
                component_name=name,
                model_id=model_id,
                hf_reference=hf_ref,
                new_shape=new_shape,
                repo_root=repo_root,
                overwrite=force,
                discovered_submodule_path=comp.get("submodule_path"),
            )
            action.test_path = str(safe_relative_to_root(test_path))
            action.test_generated = generated
            action.test_already_existed = already and not force
            if stub_graduated and existing_is_smoke:
                action.notes.append(
                    "stub has graduated from autofill -> regenerated test from "
                    "Phase-1 smoke variant to real PCC variant"
                )
            elif already and not overwrite_tests:
                action.notes.append("test already existed; left untouched (use --overwrite-tests to refresh)")

        if run_tests:
            if not action.test_path:
                action.test_status = "missing"
                action.notes.append("no test to run (emit_tests=False)")
            else:
                status, summary = _run_pytest_target(repo_root / action.test_path, exclude_k=exclude_k)
                action.test_status = status
                if status == "passed" and remove_passing_stubs and stub_rel.exists():
                    stub_rel.unlink()
                    action.stub_removed = True
                if status == "failed":
                    action.notes.append("pytest failed — see pytest output for the failing assertion")
                    action.notes.append(summary.splitlines()[-1] if summary else "(no output)")

        actions.append(action)

    counts_after = _refresh_plan(model_id=model_id, repo_root=repo_root)
    if counts_after is None:
        counts_after = counts_before
        notes.append(
            "bringup_status.json was not refreshed (scaffold subprocess returned non-zero); "
            "counts shown are pre-loop."
        )

    return BringupLoopResult(
        model_id=model_id,
        demo_dir=str(safe_relative_to_root(demo_dir)),
        actions=actions,
        counts_before=counts_before,
        counts_after=counts_after,
        notes=notes,
    )


def _refresh_plan(*, model_id: str, repo_root: Path) -> Optional[dict]:
    cmd = [
        "python",
        "-m",
        "scripts.tt_hw_planner",
        "scaffold",
        model_id,
        "--apply",
    ]
    proc = subprocess.run(cmd, cwd=str(repo_root), capture_output=True, text=True, check=False)
    if proc.returncode != 0:
        return None
    demo_dir = find_demo_dir(model_id, repo_root=repo_root)
    if demo_dir is None:
        return None
    status_path = demo_dir / "bringup_status.json"
    if not status_path.exists():
        return None
    try:
        return json.loads(status_path.read_text()).get("counts", {})
    except Exception:
        return None


def render_text(result: BringupLoopResult) -> str:
    sep = "=" * 78
    out: List[str] = [sep]
    out.append(f"  BRING-UP LOOP for {result.model_id}")
    out.append(sep)
    out.append("")
    out.append(f"  Demo folder: {result.demo_dir}")
    cb = result.counts_before
    ca = result.counts_after
    out.append(f"  Counts before: {cb.get('REUSE', 0)} REUSE / {cb.get('NEW', 0)} NEW")
    out.append(f"  Counts after:  {ca.get('REUSE', 0)} REUSE / {ca.get('NEW', 0)} NEW")
    out.append("")

    if not result.actions:
        out.append("  No NEW stubs found — model has nothing left to port. Ready to assemble.")
    else:
        out.append("  Per-stub actions:")
        out.append("")
        for a in result.actions:
            tag = []
            if a.test_generated:
                tag.append("test+")
            elif a.test_already_existed:
                tag.append("test=")
            if a.test_status != "not-run":
                tag.append(a.test_status)
            if a.stub_removed:
                tag.append("stub-removed")
            tag_str = ", ".join(tag) if tag else "discovered"
            out.append(f"    [{tag_str:>20s}]  {a.component}")
            out.append(f"        stub:  {a.stub_path}")
            if a.test_path:
                out.append(f"        test:  {a.test_path}")
            if a.hf_reference:
                out.append(f"        ref :  {a.hf_reference}")
            for n in a.notes:
                out.append(f"        note:  {n}")
            out.append("")

    if result.notes:
        out.append("  Loop notes:")
        for n in result.notes:
            out.append(f"    -  {n}")
        out.append("")

    out.append("  What to do next:")
    if result.actions and any(a.test_status != "passed" for a in result.actions):
        out.append("    1. Open each generated test file under tests/pcc/ and fill in the 3 marked blocks.")
        out.append("    2. Open each `_stubs/<component>.py` and replace `NotImplementedError` with a TTNN port.")
        out.append("    3. Re-run with `--run-tests` to validate; stubs whose PCC passes will auto-remove.")
    else:
        out.append("    - Nothing left to port. Run `prepare --execute` to confirm end-to-end.")
    out.append("")
    out.append(sep)
    return "\n".join(out)



def _shape_to_torch_randn(new_shape: Dict[str, object]) -> str:
    hidden_size = new_shape.get("hidden_size")
    image_size = new_shape.get("image_size")
    num_channels = new_shape.get("num_channels") or 3
    if image_size and num_channels and isinstance(image_size, (int, float)):
        return f"torch.randn(1, {int(num_channels)}, {int(image_size)}, {int(image_size)})"
    if hidden_size and isinstance(hidden_size, (int, float)):
        return f"torch.randn(1, 64, {int(hidden_size)})"
    return "torch.randn(1, 64, 64)"



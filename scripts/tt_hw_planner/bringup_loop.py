def _resolve(obj, dotted):
    cur = obj
    for tok in dotted.replace("[", ".").replace("]", "").split("."):
        if tok == "":
            continue
        if tok.isdigit():
            cur = cur[int(tok)]
        else:
            cur = getattr(cur, tok)
    return cur


def _detect_image_size(model):
    cfg = getattr(model, "config", None)
    for path in ("vision_config.image_size", "image_size", "vision_config.input_size"):
        cur = cfg
        ok = True
        for tok in path.split("."):
            cur = getattr(cur, tok, None)
            if cur is None:
                ok = False
                break
        if ok and isinstance(cur, int):
            return cur
    return 224


def _detect_hidden_shape(torch_module, model=None):
    """Pick a sensible `hidden_states` input shape for this attention/MLP module.

    HIERA-style multi-scale attention has `.qkv` and expects (B, H, W, C).
    Standard attention has `.q_proj` / `.query` and expects (B, N, C).

    For standalone norm modules (RMSNorm / LayerNorm) the projection
    attributes are absent; fall back to the module's own `.weight.shape`
    (the norm's per-element scale, sized for the channel dim it normalizes),
    and finally to ``model.config.hidden_size`` -- NOT a hardcoded 768.
    The hardcoded 768 default was the root cause of every Qwen3-style
    model SKIPping its PCC test on shape mismatch.
    """
    if hasattr(torch_module, "qkv") and hasattr(torch_module.qkv, "in_features"):
        c = torch_module.qkv.in_features
        return (1, 16, 16, c), "nhwc"
    for name in ("q_proj", "query", "proj_in", "fc1", "dense"):
        proj = getattr(torch_module, name, None)
        if proj is not None and hasattr(proj, "in_features"):
            return (1, 64, proj.in_features), "nlc"
    w = getattr(torch_module, "weight", None)
    if w is not None and hasattr(w, "shape") and len(w.shape) >= 1:
        c = int(w.shape[-1])
        if c > 1:
            return (1, 64, c), "nlc"
    cfg = getattr(model, "config", None) if model is not None else None
    h = getattr(cfg, "hidden_size", None)
    if isinstance(h, int) and h > 0:
        return (1, 64, h), "nlc"
    return (1, 64, 768), "nlc"



def _refresh_plan(*, model_id: str, repo_root: Path) -> Optional[dict]:
    # 2026-06-04 Fix 1 — use sys.executable (the parent's Python with
    # PyTorch) instead of bare "python". Bare `python` resolves via
    # PATH and may pick a different venv that lacks PyTorch — in which
    # case scaffold's `AutoModel.from_pretrained` fails, the module-
    # tree walk silently returns [], scaffold writes a 0-component
    # plan, and bringup_status.json gets nuked under the orchestrator's
    # feet. Caught on the 2026-06-03 seamless-m4t promote run.
    cmd = [
        sys.executable,
        "-m",
        "scripts.tt_hw_planner",
        "scaffold",
        model_id,
        "--apply",
    ]
    # Snapshot pre-state for corruption detection. find_demo_dir reads
    # bringup_status.json on disk; capture the component count BEFORE
    # we let scaffold subprocess touch it.
    demo_dir_pre = find_demo_dir(model_id, repo_root=repo_root)
    pre_counts: Optional[Dict[str, int]] = None
    pre_children: List[Dict[str, Any]] = []
    if demo_dir_pre is not None:
        try:
            pre_data = json.loads((demo_dir_pre / "bringup_status.json").read_text())
            pre_counts = pre_data.get("counts") or {}
            pre_children = [
                c
                for c in (pre_data.get("components") or [])
                if isinstance(c, dict) and c.get("_added_by_decomposition_of")
            ]
        except Exception:
            pre_counts = None

    proc = subprocess.run(cmd, cwd=str(repo_root), capture_output=True, text=True, check=False)
    if proc.returncode != 0:
        # Surface subprocess stderr (otherwise it's invisible to the
        # caller). Don't return — the caller's `if counts_after is None`
        # path still applies, but the user gets the actual error reason.
        if proc.stderr:
            print(
                f"  [_refresh_plan] scaffold subprocess failed "
                f"(rc={proc.returncode}); last 800 chars of stderr:\n"
                f"{proc.stderr[-800:]}",
                file=sys.stderr,
            )
        return None
    demo_dir = find_demo_dir(model_id, repo_root=repo_root)
    if demo_dir is None:
        return None
    status_path = demo_dir / "bringup_status.json"
    if not status_path.exists():
        return None
    try:
        post_data = json.loads(status_path.read_text())
        post_counts = post_data.get("counts", {}) or {}

        if pre_children:
            post_components = post_data.get("components") or []
            present = {c.get("name") for c in post_components if isinstance(c, dict)}
            restored = [c for c in pre_children if c.get("name") not in present]
            if restored:
                post_components.extend(restored)
                post_data["components"] = post_components
                if isinstance(post_data.get("counts"), dict):
                    post_data["counts"]["NEW"] = int(post_data["counts"].get("NEW", 0) or 0) + len(restored)
                    post_counts = post_data["counts"]
                status_path.write_text(json.dumps(post_data, indent=2))
                print(
                    f"  [_refresh_plan] preserved {len(restored)} decomposition "
                    f"child component(s) across scaffold refresh",
                    file=sys.stderr,
                )

        # 2026-06-04 Fix 4 — corruption guard. If pre had N>0 components
        # and post has 0, scaffold subprocess silently nuked the plan
        # (e.g., torch-less Python that can't walk the model). Loud-
        # error so the iter loop's apply step doesn't operate on a
        # corrupted manifest. The fix for the WHY is using sys.executable
        # above; this gate catches any future similar regression.
        if isinstance(pre_counts, dict):
            pre_n = sum(int(pre_counts.get(k, 0) or 0) for k in ("REUSE", "ADAPT", "NEW"))
            post_n = sum(int(post_counts.get(k, 0) or 0) for k in ("REUSE", "ADAPT", "NEW"))
            if pre_n > 0 and post_n == 0:
                print(
                    f"  [_refresh_plan] CORRUPTION GUARD: scaffold subprocess "
                    f"silently dropped all components (pre={pre_n}, post=0). "
                    f"Subprocess stderr (last 800 chars):\n"
                    f"{(proc.stderr or '')[-800:]}",
                    file=sys.stderr,
                )
        return post_counts
    except Exception:
        return None


def render_text(result: BringupLoopResult) -> str:
    sep = "=" * 78
    out: List[str] = [sep]
    out.append(f"  BRING-UP LOOP for {result.model_id}")
    out.append(sep)
    out.append("")
    out.append(f"  Demo folder: {result.demo_dir}")
    cb = result.counts_before
    ca = result.counts_after
    out.append(f"  Counts before: {cb.get('REUSE', 0)} REUSE / {cb.get('ADAPT', 0)} ADAPT / {cb.get('NEW', 0)} NEW")
    out.append(f"  Counts after:  {ca.get('REUSE', 0)} REUSE / {ca.get('ADAPT', 0)} ADAPT / {ca.get('NEW', 0)} NEW")
    out.append("")

    if not result.actions:
        out.append("  No NEW stubs found — model has nothing left to port. Ready to assemble.")
    else:
        out.append("  Per-stub actions:")
        out.append("")
        for a in result.actions:
            tag = []
            if a.test_generated:
                tag.append("test+")
            elif a.test_already_existed:
                tag.append("test=")
            if a.test_status != "not-run":
                tag.append(a.test_status)
            if a.stub_removed:
                tag.append("stub-removed")
            tag_str = ", ".join(tag) if tag else "discovered"
            out.append(f"    [{tag_str:>20s}]  {a.component}")
            out.append(f"        stub:  {a.stub_path}")
            if a.test_path:
                out.append(f"        test:  {a.test_path}")
            if a.hf_reference:
                out.append(f"        ref :  {a.hf_reference}")
            for n in a.notes:
                out.append(f"        note:  {n}")
            out.append("")

    if result.notes:
        out.append("  Loop notes:")
        for n in result.notes:
            out.append(f"    -  {n}")
        out.append("")

    out.append("  What to do next:")
    if result.actions and any(a.test_status != "passed" for a in result.actions):
        out.append("    1. Open each generated test file under tests/pcc/ and fill in the 3 marked blocks.")
        out.append("    2. Open each `_stubs/<component>.py` and replace `NotImplementedError` with a TTNN port.")
        out.append("    3. Re-run with `--run-tests` to validate; stubs whose PCC passes will auto-remove.")
    else:
        out.append("    - Nothing left to port. Run `prepare --execute` to confirm end-to-end.")
    out.append("")
    out.append(sep)
    return "\n".join(out)



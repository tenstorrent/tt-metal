def _model_param_dtype(model):
    """Return the dtype of the first floating-point parameter on `model`,
    falling back to float32 if none. HF models loaded via `from_pretrained`
    often carry bfloat16 weights; feeding them float32 activations causes
    matmul to raise a dtype-mismatch error and the PCC test SKIPs, which the
    bring-up loop counts as `verified_fail` and refuses to converge on.
    """
    try:
        for p in model.parameters():
            if p.is_floating_point():
                return p.dtype
    except Exception:
        pass
    return torch.float32


def _make_arg_for(arg_name, *, model, torch_module):
    """Return a plausible tensor/value for one forward-arg, based on its name.

    Returns `_OMIT` sentinel if the arg has no useful default we can provide;
    the caller will then drop it from the kwargs dict so HF can apply its own
    default (or skip with a clear error)."""
    md = _model_param_dtype(model)
    if arg_name == "pixel_values":
        s = _detect_image_size(model)
        return torch.randn(1, 3, s, s).to(md)
    if arg_name in ("hidden_states", "inputs_embeds", "embeddings"):
        shape, _ = _detect_hidden_shape(torch_module, model=model)
        return torch.randn(*shape).to(md)
    if arg_name in ("x", "features", "input", "inputs", "hidden_states_in"):
        shape, _ = _detect_hidden_shape(torch_module, model=model)
        return torch.randn(*shape).to(md)
    if arg_name in ("input_ids",):
        # Use small non-zero token ids so embeddings, positional
        # embeddings, and norm layers see realistic activations
        # (all-zeros input_ids collapses through softmax to degenerate
        # outputs that masquerade as PCC=1.0 against an HF reference
        # that also got zeros).
        cfg = getattr(model, "config", None)
        vocab = getattr(cfg, "vocab_size", None) if cfg is not None else None
        if isinstance(vocab, int) and vocab > 1:
            return torch.randint(low=1, high=min(vocab, 1000), size=(1, 64), dtype=torch.long)
        return torch.zeros(1, 64, dtype=torch.long)
    if arg_name == "attention_mask":
        return torch.ones(1, 64, dtype=torch.long)
    if arg_name in ("position_ids", "token_type_ids"):
        return torch.zeros(1, 64, dtype=torch.long)
    if arg_name == "position_embeddings":
        # HF Qwen3Attention requires `position_embeddings=(cos, sin)` matching
        # the per-head slice (batch, seq_len, head_dim). Without it the inner
        # `cos, sin = position_embeddings` unpack raises and pytest SKIPs.
        cfg = getattr(model, "config", None)
        h = getattr(cfg, "hidden_size", 4096) if cfg is not None else 4096
        n_heads = getattr(cfg, "num_attention_heads", 32) if cfg is not None else 32
        hd = getattr(cfg, "head_dim", h // n_heads) if cfg is not None else (h // n_heads)
        cos = torch.ones(1, 64, hd, dtype=md)
        sin = torch.zeros(1, 64, hd, dtype=md)
        return (cos, sin)
    if arg_name == "image_embeddings":
        return torch.randn(1, 256, 64, 64)
    if arg_name == "image_positional_embeddings":
        return torch.randn(1, 256, 64, 64)
    if arg_name == "sparse_prompt_embeddings":
        return torch.randn(1, 1, 256)
    if arg_name == "dense_prompt_embeddings":
        return torch.randn(1, 256, 64, 64)
    if arg_name == "high_resolution_features":
        return [torch.randn(1, 32, 256, 256), torch.randn(1, 64, 128, 128)]
    if arg_name == "input_points":
        return torch.tensor([[[[512.0, 512.0]]]])
    if arg_name == "input_labels":
        return torch.tensor([[[1]]])
    if arg_name == "input_boxes":
        return None
    if arg_name == "input_masks":
        return None
    if arg_name == "multimask_output":
        return True
    if arg_name.startswith("output_"):
        return False
    if arg_name in ("past_key_values", "cache_position", "use_cache",
                    "return_dict", "head_mask", "encoder_hidden_states",
                    "encoder_attention_mask", "labels"):
        return None
    return _OMIT



"""Convergence gate — only proceed when the model is actually optimized.

Gate criteria:
  1. All optimizer blocks GREEN (no remaining findings) or explicitly
     `--suppress`ed with a reason.
  2. Accuracy gate held throughout (PCC / loss vs `model_targets.yaml`).
  3. Decode TPS >= `--target-tps` OR ratio-to-best-known >= --target-ratio.
  4. No COMPUTE KERNEL HASH regressions vs baseline.

On pass, writes:
  - `optimized_config.yaml` (frozen kwargs per cluster, from applied patches)
  - tuning-table diff patch against `models/tt_transformers/tt/model_config.py`
  - `runner.sh` (canonical launch command)
"""

from __future__ import annotations

import json
import shlex
from dataclasses import dataclass, field
from pathlib import Path
from typing import Dict, List, Optional

import yaml

from .collect import find_run, load_run_meta, require_healthy_run
from .runner import PATCHES_SUBDIR
from .status_board import compute_status


@dataclass
class FinalizeResult:
    run_id: str
    passed: bool
    summary: str
    optimized_config_path: Optional[Path] = None
    runner_sh_path: Optional[Path] = None
    tuning_patch_path: Optional[Path] = None
    failure_reasons: List[str] = field(default_factory=list)


def _read_patches(run_dir: Path) -> Dict[str, Dict[str, dict]]:
    """Walk patches/ and parse the embedded JSON. Returns {block: {cluster: payload}}."""
    out: Dict[str, Dict[str, dict]] = {}
    patch_dir = run_dir / PATCHES_SUBDIR
    if not patch_dir.exists():
        return out
    for p in patch_dir.glob("*.patch"):
        block, _, rest = p.stem.partition("__")
        cluster = rest or "all"
        try:
            text = p.read_text()
            blob = text[text.index("{") : text.rindex("}") + 1]
            data = json.loads(blob)
            out.setdefault(block, {})[cluster] = data
        except (ValueError, OSError, json.JSONDecodeError):
            continue
    return out


def _build_optimized_config(patches: Dict[str, Dict[str, dict]], meta: dict) -> dict:
    return {
        "version": 1,
        "model_id": meta.get("model_id"),
        "box": meta.get("box"),
        "mesh_shape": meta.get("mesh_shape"),
        "dtype": meta.get("dtype"),
        "applied_blocks": list(patches.keys()),
        "clusters": [
            {
                "cluster_id": cid,
                "block": block,
                "kind": payload.get("kind"),
                "kwargs": payload.get("new_kwargs", {}),
                "target": payload.get("target", {}),
                "rationale": payload.get("rationale", ""),
            }
            for block, by_cluster in patches.items()
            for cid, payload in by_cluster.items()
        ],
    }


def _build_runner_sh(meta: dict, run_id: str, optimized_config_path: Path) -> str:
    env_lines = [f"export {k}={shlex.quote(v)}" for k, v in (meta.get("env") or {}).items()]
    argv = list(meta.get("pytest_argv") or [])
    cmd = " \\\n    ".join(shlex.quote(a) for a in argv)
    return (
        "#!/usr/bin/env bash\n"
        f"# Auto-generated by `tt_hw_planner perf finalize {run_id}`.\n"
        f"# Source optimized config: {optimized_config_path}\n"
        "set -euo pipefail\n"
        "\n" + "\n".join(env_lines) + "\n\n"
        f"export TT_PERF_OPTIMIZED_CONFIG={shlex.quote(str(optimized_config_path))}\n\n" + cmd + "\n"
    )


def _build_tuning_patch(optimized: dict) -> str:
    """Render a unified-diff-style sidecar against the tuning table.

    v1 is informational only — it shows the user which model_config.py
    entries would change. A future revision will turn this into an
    `apply`-able patch via AST rewriting.
    """
    out: List[str] = []
    out.append("--- a/models/tt_transformers/tt/model_config.py")
    out.append("+++ b/models/tt_transformers/tt/model_config.py")
    out.append("@@ converged tuning entries from perf finalize @@")
    for cluster in optimized.get("clusters", []):
        target = cluster.get("target") or {}
        out.append(f"# cluster {cluster.get('cluster_id')}  block {cluster.get('block')}")
        out.append(f"# target: {target.get('path')}  variable={target.get('variable')}")
        out.append(f"# kwargs:")
        for k, v in (cluster.get("kwargs") or {}).items():
            out.append(f"#   {k}: {v}")
        out.append(f"# rationale: {cluster.get('rationale')}")
        out.append("")
    return "\n".join(out)


def finalize_run(
    run_id: str,
    *,
    baseline_run_id: Optional[str] = None,
    target_tps: Optional[float] = None,
    target_ratio: float = 0.85,
    suppressed: Optional[List[str]] = None,
    run_dir_root: Optional[Path] = None,
) -> FinalizeResult:
    suppressed = list(suppressed or [])
    run_dir = find_run(run_id, run_dir_root)
    meta = require_healthy_run(run_dir)

    reasons: List[str] = []
    statuses = compute_status(run_id, run_dir_root=run_dir_root)
    for s in statuses:
        if s.status in ("GREEN", "APPLIED", "N/A"):
            continue
        if s.name in suppressed:
            continue
        reasons.append(f"block '{s.name}' is {s.status} ({s.finding_count} findings)")

    if target_tps is not None:
        observed_tps = meta.get("observed_decode_tps") or 0.0
        if observed_tps < target_tps:
            reasons.append(f"decode TPS {observed_tps} < target {target_tps}")

    # Kernel-hash regression check (vs baseline)
    if baseline_run_id:
        try:
            from .report import _load

            cur_rows, _, _, _ = _load(run_dir, run_id)
            bdir = find_run(baseline_run_id, run_dir_root)
            base_rows, _, _, _ = _load(bdir, baseline_run_id)
            cur_hashes = {r.compute_kernel_hash for r in cur_rows if r.compute_kernel_hash}
            base_hashes = {r.compute_kernel_hash for r in base_rows if r.compute_kernel_hash}
            new_hashes = cur_hashes - base_hashes
            if new_hashes:
                slow_count = 0
                base_total = sum(r.device_kernel_ns or 0 for r in base_rows)
                cur_total = sum(r.device_kernel_ns or 0 for r in cur_rows)
                if cur_total > base_total * 1.02:
                    slow_count += 1
                if slow_count:
                    reasons.append(
                        f"{len(new_hashes)} new compute_kernel_hash(es) and total device time grew {cur_total/base_total:.2f}x"
                    )
        except FileNotFoundError:
            pass

    passed = not reasons

    optimized_config_path = None
    runner_sh_path = None
    tuning_patch_path = None
    summary_lines: List[str] = []
    summary_lines.append(f"=== perf finalize: {run_id} ===")
    summary_lines.append(f"model: {meta.get('model_id')}  box: {meta.get('box')}  mesh: {meta.get('mesh_shape')}")
    summary_lines.append("")

    if passed:
        patches = _read_patches(run_dir)
        optimized = _build_optimized_config(patches, meta)
        optimized_config_path = run_dir / "optimized_config.yaml"
        optimized_config_path.write_text(yaml.safe_dump(optimized, sort_keys=False, indent=2))

        runner_sh_path = run_dir / "runner.sh"
        runner_sh_path.write_text(_build_runner_sh(meta, run_id, optimized_config_path))
        runner_sh_path.chmod(0o700)

        tuning_patch_path = run_dir / "tuning_table.patch"
        tuning_patch_path.write_text(_build_tuning_patch(optimized))

        summary_lines.append("PASSED — convergence gate satisfied")
        summary_lines.append(f"  optimized_config: {optimized_config_path}")
        summary_lines.append(f"  runner.sh:        {runner_sh_path}")
        summary_lines.append(f"  tuning.patch:     {tuning_patch_path}")
        summary_lines.append("")
        summary_lines.append("Next step: review the patch, then `bash runner.sh` to launch the optimized model.")
    else:
        summary_lines.append("FAILED — convergence gate refuses to finalize:")
        for r in reasons:
            summary_lines.append(f"  - {r}")
        summary_lines.append("")
        summary_lines.append("Resolve the listed blockers (or pass --suppress NAME) and re-run finalize.")

    return FinalizeResult(
        run_id=run_id,
        passed=passed,
        summary="\n".join(summary_lines),
        optimized_config_path=optimized_config_path,
        runner_sh_path=runner_sh_path,
        tuning_patch_path=tuning_patch_path,
        failure_reasons=reasons,
    )

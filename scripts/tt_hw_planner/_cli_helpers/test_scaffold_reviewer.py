"""LLM batch review of freshly-emitted PCC test scaffolds (Tier-2,
proactive).

When ``_emit_pcc_template`` (Tier-1 deterministic) creates new test
files, this reviewer batches them into a single LLM call. The LLM
identifies broken tests (missing required args, mutually-exclusive
kwargs, semantic input mismatches) and patches them in place BEFORE
pytest runs. This avoids the "waste an iter to discover the test is
broken" cycle that happens with the reactive ``skip_diagnoser`` alone.

Distinct from ``skip_diagnoser``:
  * ``skip_diagnoser``: REACTIVE, fires when pytest SKIPS at runtime.
    Diagnoses one component at a time.
  * ``test_scaffold_reviewer``: PROACTIVE, fires at emission time.
    Batch-reviews N tests in one LLM call before pytest runs.

Both share the same underlying ``_invoke_agent`` call infrastructure
and treat the LLM as a code-review oracle that patches test fixtures.

Design contract
---------------
* Pure helpers (``build_review_prompt``, ``parse_review_verdict``,
  ``is_review_target``) are unit-tested with no LLM.
* The orchestrator ``review_test_scaffolds`` is best-effort: every
  failure mode (no agent_bin, subprocess error, malformed verdict
  JSON) degrades to a no-op rather than raising. The caller treats
  failure as "skip review, let pytest catch what we couldn't."
* The reviewer NEVER edits ``_stubs/*.py`` — only test files. Stub
  code is the iter loop's job, not the scaffold reviewer's.
"""

from __future__ import annotations

import json
import re
import sys
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Dict, List, Optional


# Verdict constants
VERDICT_OK = "ok"  # test looks correct, no patching needed
VERDICT_PATCHED = "patched"  # test was broken, LLM applied a patch
VERDICT_UNFIXABLE = "unfixable"  # test is broken in a way LLM can't auto-fix; flag for manual
VERDICT_UNKNOWN = "unknown"  # parse error or LLM uncertainty


@dataclass
class ReviewVerdict:
    """One verdict from the batch reviewer."""

    component: str  # component name (test filename stem minus 'test_')
    verdict: str  # one of VERDICT_*
    summary: str = ""  # human-readable explanation
    patch_applied: bool = False  # whether the LLM applied a patch
    agent_stdout: str = ""  # raw agent output (for debug)
    rc: int = 0  # subprocess returncode


# ─── Pure helpers (no LLM) ──────────────────────────────────────────


def is_review_target(test_path: Path) -> bool:
    """True iff this is a freshly-emitted PCC test we want the LLM to
    review. Filters out: tests already marked as no-emit, tests that
    don't look like auto-generated PCC tests (lacking the template
    marker), tests for stubs that no longer exist."""
    if not test_path.is_file():
        return False
    try:
        head = test_path.read_text(encoding="utf-8")[:800]
    except Exception:
        return False
    # Heuristic: auto-generated PCC tests contain "_CANDIDATE_SUBMODULE_PATHS"
    # (the template's submodule resolution gate). Hand-written tests don't.
    return "_CANDIDATE_SUBMODULE_PATHS" in head


def _extract_component_name(test_path: Path) -> str:
    """test_<component>.py → component"""
    stem = test_path.stem
    return stem[5:] if stem.startswith("test_") else stem


def build_review_prompt(
    *,
    test_files_with_excerpts: List[Dict[str, str]],
    model_id: str,
) -> str:
    """Build the LLM prompt for batch review of N test scaffolds.

    ``test_files_with_excerpts`` is a list of dicts with keys:
      - ``component``: short component name
      - ``test_path``: absolute path to the test file
      - ``test_excerpt``: first ~3000 chars of the test file
      - ``hf_signature``: optional excerpt of the HF forward signature
    """
    components_block_lines: List[str] = []
    for i, entry in enumerate(test_files_with_excerpts, start=1):
        components_block_lines.append(f"--- COMPONENT {i}/{len(test_files_with_excerpts)}: {entry['component']} ---")
        components_block_lines.append(f"TEST FILE PATH: {entry['test_path']}")
        if entry.get("hf_signature"):
            components_block_lines.append(f"HF FORWARD SIGNATURE:\n{entry['hf_signature']}")
        components_block_lines.append(f"TEST FILE EXCERPT (first 3000 chars):\n{entry['test_excerpt']}")
        components_block_lines.append("")
    components_block = "\n".join(components_block_lines)

    return (
        f"You are reviewing {len(test_files_with_excerpts)} freshly-emitted PCC test scaffold(s) "
        f"for `{model_id}`. The scaffolds were auto-generated by a deterministic template; "
        f"some may have synthetic inputs that don't match the HF reference's expected "
        f"forward signature.\n"
        f"\n"
        f"For EACH component below, you must answer:\n"
        f"  1. Will `hf_module.forward(**inputs)` succeed when called with the inputs the test\n"
        f"     scaffold generates via `_make_arg_for()`?\n"
        f"  2. If not, what's the specific issue? Common patterns:\n"
        f"     - MUTUAL EXCLUSION: passing both `input_ids` AND `inputs_embeds` (HF rejects)\n"
        f"     - MISSING REQUIRED ARG: HF forward needs an arg that scaffold returns _OMIT for\n"
        f"     - WRONG SHAPE: synthetic tensor channels/seq_len don't match module's expected\n"
        f"     - WRONG DTYPE: int IDs cast to bf16, or float inputs cast to int\n"
        f"     - SEMANTIC MISMATCH: scaffold passes `pixel_values` to a text-only module\n"
        f"  3. If you can fix it by editing the test file (NOT the stub), apply the fix\n"
        f"     in-place via the Edit tool and confirm. The fix usually involves overriding\n"
        f"     `_build_torch_reference` or `_make_arg_for` for the specific arg(s) in the\n"
        f"     test file's local scope.\n"
        f"  4. If the issue requires editing the stub (`_stubs/<comp>.py`), do NOT fix it\n"
        f"     here — mark as `unfixable` so the iter loop's LLM handles the stub.\n"
        f"\n"
        f"DO NOT edit `_stubs/*.py` files. Only edit test files under `tests/pcc/`.\n"
        f"\n"
        f"{components_block}\n"
        f"\n"
        f"Output format — emit ONE verdict block per component, in order:\n"
        f"\n"
        f"VERDICT FOR <component_name>: <ok | patched | unfixable>\n"
        f"SUMMARY: <one-line explanation of what you found / fixed>\n"
        f"\n"
        f"After all verdicts, emit a final line:\n"
        f"DONE\n"
    )


_VERDICT_BLOCK_RE = re.compile(
    r"^\s*VERDICT FOR\s+(?P<component>\S+)\s*:\s*(?P<verdict>ok|patched|unfixable|unknown)\s*$",
    re.IGNORECASE | re.MULTILINE,
)
_SUMMARY_RE = re.compile(r"^\s*SUMMARY\s*:\s*(?P<summary>.+?)\s*$", re.MULTILINE)


def parse_review_verdict(agent_output: str) -> List[ReviewVerdict]:
    """Parse the agent's batch output into a list of ReviewVerdicts.

    Robust to:
      - Agents that emit extra commentary between verdict blocks
      - Missing summary lines (fills with empty string)
      - Unknown verdict strings (falls back to VERDICT_UNKNOWN)
      - Verdicts in different order than input (caller can re-sort)
    """
    verdicts: List[ReviewVerdict] = []
    if not agent_output:
        return verdicts
    # Find every VERDICT FOR ... line; for each, look for an immediate
    # following SUMMARY line within the next ~800 chars.
    for match in _VERDICT_BLOCK_RE.finditer(agent_output):
        comp = match.group("component").strip()
        verdict_raw = match.group("verdict").strip().lower()
        verdict = verdict_raw if verdict_raw in (VERDICT_OK, VERDICT_PATCHED, VERDICT_UNFIXABLE) else VERDICT_UNKNOWN
        # Try to find the SUMMARY line right after this VERDICT line.
        rest = agent_output[match.end() : match.end() + 800]
        summary_match = _SUMMARY_RE.search(rest)
        summary = summary_match.group("summary").strip() if summary_match else ""
        verdicts.append(
            ReviewVerdict(
                component=comp,
                verdict=verdict,
                summary=summary,
                patch_applied=(verdict == VERDICT_PATCHED),
            )
        )
    return verdicts


# ─── Orchestrator (calls _invoke_agent) ─────────────────────────────


def review_test_scaffolds(
    *,
    demo_dir: Path,
    test_files: List[Path],
    model_id: str,
    agent_bin: Optional[str],
    agent_model: str = "opus",
    timeout_s: int = 900,
    enabled: bool = True,
) -> List[ReviewVerdict]:
    """Batch-review N freshly-emitted PCC test scaffolds via the LLM.

    Best-effort: returns an empty list if any precondition fails
    (disabled, no agent_bin, no test files, import error, subprocess
    error). Caller treats empty as "no signal — let pytest catch any
    remaining issues."

    The LLM is allowed to Edit the test files in-place. After it
    returns, this function persists a sidecar
    ``test_scaffold_review.json`` for OUTCOME banner surfacing.
    """
    if not enabled:
        return []
    if not agent_bin:
        return []
    eligible = [p for p in test_files if is_review_target(p)]
    if not eligible:
        return []

    # Build per-test excerpts (cap the size to keep prompt bounded).
    entries: List[Dict[str, str]] = []
    for test_path in eligible:
        try:
            excerpt = test_path.read_text(encoding="utf-8")[:3000]
        except Exception:
            continue
        entries.append(
            {
                "component": _extract_component_name(test_path),
                "test_path": str(test_path),
                "test_excerpt": excerpt,
                "hf_signature": "",  # (future) can be populated by caller
            }
        )
    if not entries:
        return []

    prompt = build_review_prompt(test_files_with_excerpts=entries, model_id=model_id)

    try:
        from .agent import _invoke_agent
    except Exception as exc:
        print(f"  [test-scaffold-review] could not import _invoke_agent: {exc}", file=sys.stderr)
        return []

    try:
        # _invoke_agent returns rc; capture stdout via the handoff log
        # if possible. For simplicity here we use a temp log path.
        handoff_dir = demo_dir / "_handoff"
        handoff_dir.mkdir(parents=True, exist_ok=True)
        log_path = handoff_dir / "test_scaffold_review.log"
        rc = _invoke_agent(
            prompt,
            provider="claude",
            agent_bin=agent_bin,
            cwd=demo_dir.parent.parent.parent if demo_dir.parent.parent.parent.is_dir() else demo_dir,
            model=agent_model,
            timeout_s=timeout_s,
            complexity_bonus=0,
            iter_tag=f"test_scaffold_review_{len(entries)}",
            deliverable_dirs=[],
            expected_deliverable_files=None,
        )
    except Exception as exc:
        print(f"  [test-scaffold-review] _invoke_agent raised: {type(exc).__name__}: {exc}", file=sys.stderr)
        return []

    # Read back the agent's stdout from the handoff log.
    agent_output = ""
    try:
        if log_path.is_file():
            agent_output = log_path.read_text(encoding="utf-8")
    except Exception:
        pass

    verdicts = parse_review_verdict(agent_output)
    # Tag verdicts with subprocess rc for debug.
    for v in verdicts:
        v.rc = rc

    # Persist sidecar for OUTCOME banner.
    try:
        sidecar = demo_dir / "test_scaffold_review.json"
        sidecar.write_text(
            json.dumps(
                {
                    "reviewed_count": len(entries),
                    "verdicts": [
                        {
                            "component": v.component,
                            "verdict": v.verdict,
                            "summary": v.summary,
                            "patch_applied": v.patch_applied,
                        }
                        for v in verdicts
                    ],
                },
                indent=2,
            ),
            encoding="utf-8",
        )
    except Exception:
        pass

    # Console summary line.
    counts: Dict[str, int] = {}
    for v in verdicts:
        counts[v.verdict] = counts.get(v.verdict, 0) + 1
    if counts:
        print("  [test-scaffold-review] verdicts: " + ", ".join(f"{k}={v}" for k, v in sorted(counts.items())))

    return verdicts



#!/usr/bin/env python3
"""
Parse TT-Metal profiler reports and extract key metrics in a readable format.

This script parses the raw ops_perf_results CSV files generated by the tracy profiler
and produces cleaned-up prefill.csv and decode.csv files with relevant columns.

The script automatically:
  - Splits data into prefill and decode phases (based on first SdpaDecode op)
  - Extracts operation names from verbose OP CODE strings
  - Formats tensor shapes in a readable format
  - Calculates core utilization percentages
  - Optionally zeros out outlier ops that skew timing data

=============================================================================
USAGE
=============================================================================

    python scripts/parse_profiler_report.py <csv_file> [options]

=============================================================================
ARGUMENTS
=============================================================================

    csv_file            Path to the raw ops_perf_results_*.csv file from tracy

=============================================================================
OPTIONS
=============================================================================

    --sort-by COLUMN    Sort output by column (default: CALL_IDX)
                        Common columns: CALL_IDX, KERNEL_DUR_us, OP_NAME, CORES
    --filter-op NAME    Filter by operation name (substring match, case-insensitive)
                        Example: --filter-op Matmul, --filter-op AllGather
    --device-id ID      Filter by device ID (integer)
                        Example: --device-id 0
    --limit N           Limit output to first N rows per phase
    --phase PHASE       Show only 'prefill' or 'decode' phase
                        Default: show both phases separately
    --summary           Print summary statistics to terminal
                        Shows total time and percentage breakdown per operation
    --no-outliers       Zero out kernel duration for outlier ops that skew timing:
                        - DramPrefetcherOperation
                        - EmbeddingsDeviceOperation
    --output-dir DIR    Directory to write prefill.csv and decode.csv
                        Default: current directory

=============================================================================
OUTPUT FILES
=============================================================================

    prefill.csv         Parsed prefill phase operations
    decode.csv          Parsed decode phase operations
    summary.txt         Summary statistics (when run via run_profiler_sweep.sh)

    Output columns:
      - OP_NAME: Extracted operation name (e.g., MatmulDeviceOperation)
      - DEVICE_ID: Device ID
      - CALL_IDX: Global call count (unique identifier for each op invocation)
      - KERNEL_DUR_us: Kernel duration in microseconds
      - CORES: Number of cores used
      - AVAIL_CORES: Available worker cores
      - CORE_UTIL%: Core utilization percentage
      - MATH_FIDELITY: Math fidelity setting (HiFi2, HiFi4, LoFi)
      - IN0_SHAPE, IN1_SHAPE: Input tensor shapes
      - IN0_LAYOUT, IN0_DTYPE, IN0_MEM: Input tensor properties
      - OUT0_SHAPE, OUT0_MEM: Output tensor properties
      - FPU_UTIL%, DRAM_BW_UTIL%: Utilization percentages

=============================================================================
EXAMPLES
=============================================================================

    # Basic parsing with summary (most common usage)
    python scripts/parse_profiler_report.py report.csv --device-id 0 --summary --no-outliers

    # Parse and save to specific directory
    python scripts/parse_profiler_report.py report.csv --output-dir results/128/ --summary --no-outliers

    # Analyze only decode phase, sorted by kernel duration
    python scripts/parse_profiler_report.py report.csv --phase decode --sort-by KERNEL_DUR_us --summary

    # Filter to only attention operations
    python scripts/parse_profiler_report.py report.csv --filter-op Sdpa --summary

    # Filter to only matmul operations on device 0
    python scripts/parse_profiler_report.py report.csv --filter-op Matmul --device-id 0 --summary

    # Quick look at top 20 ops by duration
    python scripts/parse_profiler_report.py report.csv --sort-by KERNEL_DUR_us --limit 20

=============================================================================
RELATED SCRIPTS
=============================================================================

    run_profiler_sweep.sh         - Automates profiling across prompt lengths
    compare_profiler_results.py   - Compare results across prompt lengths
    compare_two_runs.py           - Compare two different runs (Excel output)
"""

import argparse
import csv
import re
import sys
from collections import defaultdict
from pathlib import Path

# Check if xlsxwriter is available for Excel output
XLSXWRITER_AVAILABLE = False
try:
    import xlsxwriter

    XLSXWRITER_AVAILABLE = True
except ImportError:
    pass

# Operations to zero out as outliers (they skew timing data)
OUTLIER_OPS = {"DramPrefetcherOperation", "EmbeddingsDeviceOperation"}


def extract_op_name(full_name: str) -> str:
    """Extract the operation name from the full OP CODE string.

    Examples:
        MeshDeviceOperationAdapter<ttnn::prim::AllGatherAsyncDeviceOperation> -> AllGatherAsyncDeviceOperation
        MeshDeviceOperationAdapter<ttnn::operations::matmul::MatmulDeviceOperation> -> MatmulDeviceOperation
    """
    # Handle MeshDeviceOperationAdapter<...::XxxOperation> - get the last component before >
    match = re.search(r"<.*::(\w+)>", full_name)
    if match:
        return match.group(1)
    # Handle other formats ending in Operation or Op
    match = re.search(r"(\w+Operation|\w+Op)$", full_name)
    if match:
        return match.group(1)
    return full_name


def format_shape(w, z, y, x) -> str:
    """Format tensor shape as WxZxYxX or simplified versions.

    Input format: "128[128]" or "4096[4096]"
    Output format: "128[128]x4096[4096]" or simplified if dimensions are 1
    """
    parts = []
    dims = [w, z, y, x]

    for val in dims:
        if val and val.strip():
            val = val.strip()
            # Skip dimensions that are just "1[1]"
            if val != "1[1]":
                parts.append(val)

    if not parts:
        return ""

    # Join with 'x'
    return "x".join(parts)


def clean_memory(mem: str) -> str:
    """Clean memory config string."""
    if not mem:
        return ""
    # Remove DEV_N_ prefix
    mem = re.sub(r"DEV_\d+_", "", mem)
    return mem


def parse_row(row: dict) -> dict:
    """Parse a CSV row and extract the required fields."""
    # Get kernel duration in microseconds
    kernel_dur_ns = row.get("DEVICE KERNEL DURATION [ns]", "0")
    try:
        kernel_dur_us = float(kernel_dur_ns) / 1000.0
    except (ValueError, TypeError):
        kernel_dur_us = 0.0

    # Get core counts
    try:
        cores = int(row.get("CORE COUNT", 0))
    except (ValueError, TypeError):
        cores = 0

    try:
        avail_cores = int(row.get("AVAILABLE WORKER CORE COUNT", 0))
    except (ValueError, TypeError):
        avail_cores = 0

    # Calculate core utilization
    core_util = (cores / avail_cores * 100) if avail_cores > 0 else 0.0

    # Format shapes
    in0_shape = format_shape(
        row.get("INPUT_0_W_PAD[LOGICAL]", ""),
        row.get("INPUT_0_Z_PAD[LOGICAL]", ""),
        row.get("INPUT_0_Y_PAD[LOGICAL]", ""),
        row.get("INPUT_0_X_PAD[LOGICAL]", ""),
    )

    in1_shape = format_shape(
        row.get("INPUT_1_W_PAD[LOGICAL]", ""),
        row.get("INPUT_1_Z_PAD[LOGICAL]", ""),
        row.get("INPUT_1_Y_PAD[LOGICAL]", ""),
        row.get("INPUT_1_X_PAD[LOGICAL]", ""),
    )

    out0_shape = format_shape(
        row.get("OUTPUT_0_W_PAD[LOGICAL]", ""),
        row.get("OUTPUT_0_Z_PAD[LOGICAL]", ""),
        row.get("OUTPUT_0_Y_PAD[LOGICAL]", ""),
        row.get("OUTPUT_0_X_PAD[LOGICAL]", ""),
    )

    # Get FPU and DRAM BW utilization
    try:
        fpu_util = float(row.get("PM FPU UTIL (%)", 0) or 0)
    except (ValueError, TypeError):
        fpu_util = 0.0

    try:
        dram_bw_util = float(row.get("DRAM BW UTIL (%)", 0) or 0)
    except (ValueError, TypeError):
        dram_bw_util = 0.0

    return {
        "OP_NAME": extract_op_name(row.get("OP CODE", "")),
        "DEVICE_ID": row.get("DEVICE ID", ""),
        "CALL_IDX": row.get("GLOBAL CALL COUNT", ""),
        "KERNEL_DUR_us": kernel_dur_us,
        "CORES": cores,
        "AVAIL_CORES": avail_cores,
        "CORE_UTIL%": core_util,
        "MATH_FIDELITY": row.get("MATH FIDELITY", ""),
        "IN0_SHAPE": in0_shape,
        "IN0_LAYOUT": row.get("INPUT_0_LAYOUT", ""),
        "IN0_DTYPE": row.get("INPUT_0_DATATYPE", ""),
        "IN0_MEM": clean_memory(row.get("INPUT_0_MEMORY", "")),
        "IN1_SHAPE": in1_shape,
        "IN1_MEM": clean_memory(row.get("INPUT_1_MEMORY", "")),
        "OUT0_SHAPE": out0_shape,
        "OUT0_MEM": clean_memory(row.get("OUTPUT_0_MEMORY", "")),
        "FPU_UTIL%": fpu_util,
        "DRAM_BW_UTIL%": dram_bw_util,
    }


def split_prefill_decode(data: list) -> tuple:
    """Split data into prefill and decode phases based on first SdpaDecode occurrence.

    Returns:
        (prefill_data, decode_data) tuple
    """
    # Find the first SdpaDecode index
    first_sdpa_decode_idx = None
    for i, row in enumerate(data):
        if "SdpaDecode" in row.get("OP_NAME", ""):
            first_sdpa_decode_idx = i
            break

    if first_sdpa_decode_idx is None:
        # No SdpaDecode found - all data is prefill
        return data, []

    prefill_data = data[:first_sdpa_decode_idx]
    decode_data = data[first_sdpa_decode_idx:]

    return prefill_data, decode_data


def zero_outliers(data: list) -> list:
    """Zero out kernel duration for outlier operations."""
    for row in data:
        if row.get("OP_NAME", "") in OUTLIER_OPS:
            row["KERNEL_DUR_us"] = 0.0
    return data


def filter_to_last_sdpa(data: list, seq_len: int = None) -> list:
    """Filter prefill data to keep only the last RingDistributedSdpa iteration.

    Prefers RingDistributedSdpa (batch=1 actual inference) over SDPAOperation (batch=32 warmup).
    Finds the last RingDistributedSdpa, then finds the iteration boundaries using
    EmbeddingsDeviceOperation markers.

    Args:
        data: List of parsed operation rows
        seq_len: Unused, kept for API compatibility

    Returns:
        Filtered data containing only the last prefill iteration
    """
    if not data:
        return data

    # Find all SDPA operations by type
    ring_sdpa_indices = []
    sdpa_op_indices = []
    embeddings_indices = []

    for i, row in enumerate(data):
        op_name = row.get("OP_NAME", "")
        if "RingDistributedSdpa" in op_name:
            ring_sdpa_indices.append(i)
        elif "SDPAOperation" in op_name:
            sdpa_op_indices.append(i)
        if "EmbeddingsDeviceOperation" in op_name:
            embeddings_indices.append(i)

    # Prefer RingDistributedSdpa (batch=1 path) if present
    if ring_sdpa_indices:
        last_sdpa_idx = ring_sdpa_indices[-1]
        sdpa_type = "RingDistributedSdpa"
        total_sdpa_calls = len(ring_sdpa_indices)
    elif sdpa_op_indices:
        last_sdpa_idx = sdpa_op_indices[-1]
        sdpa_type = "SDPAOperation"
        total_sdpa_calls = len(sdpa_op_indices)
    else:
        print("Warning: No SDPA operations found in prefill data")
        return data

    # Find the Embeddings before the last SDPA (iteration start)
    embeddings_before = [i for i in embeddings_indices if i < last_sdpa_idx]
    if not embeddings_before:
        print("Warning: No EmbeddingsDeviceOperation found before last SDPA")
        return data[last_sdpa_idx:]

    iteration_start = embeddings_before[-1]

    # Find the Embeddings after the last SDPA (next iteration start = current iteration end)
    embeddings_after = [i for i in embeddings_indices if i > last_sdpa_idx]
    if embeddings_after:
        # There's another iteration after - end before it
        iteration_end = embeddings_after[0]
        filtered = data[iteration_start:iteration_end]
    else:
        # No more iterations - keep to end of data
        filtered = data[iteration_start:]

    print(
        f"Prefill filter: Kept last {sdpa_type} iteration ({len(filtered)} ops), removed {total_sdpa_calls - 1} earlier iterations"
    )

    return filtered


def normalize_sdpa_names(data: list) -> list:
    """Normalize SDPA operation names for comparison.

    Maps both SDPAOperation and RingDistributedSdpaDeviceOperation to 'SDPA'
    so they can be compared across different sequence lengths.
    """
    for row in data:
        op_name = row.get("OP_NAME", "")
        if "SDPAOperation" in op_name or "RingDistributedSdpa" in op_name:
            row["OP_NAME"] = "SDPA"
    return data


def normalize_matmul_names(data: list) -> list:
    """Normalize Matmul operation names for comparison.

    Maps MinimalMatmulDeviceOperation to 'MatmulDeviceOperation'
    so they can be compared across different sequence lengths.
    (MinimalMatmul is used for long sequences, regular Matmul for short sequences)
    """
    for row in data:
        op_name = row.get("OP_NAME", "")
        if "MinimalMatmulDeviceOperation" in op_name:
            row["OP_NAME"] = "MatmulDeviceOperation"
    return data


def filter_decode_to_last_iteration(data: list) -> list:
    """Filter decode data to keep only the last decode iteration.

    Finds the last SdpaDecodeDeviceOperation, then finds the EmbeddingsDeviceOperation
    that precedes it (marking the start of that iteration), and keeps only data
    from that point onwards.

    Args:
        data: List of parsed decode operation rows

    Returns:
        Filtered data containing only the last decode iteration
    """
    if not data:
        return data

    # Find all SdpaDecodeDeviceOperation indices
    sdpa_decode_indices = []
    for i, row in enumerate(data):
        op_name = row.get("OP_NAME", "")
        if "SdpaDecode" in op_name:
            sdpa_decode_indices.append(i)

    if not sdpa_decode_indices:
        print("Warning: No SdpaDecodeDeviceOperation found in decode data")
        return data

    # Find the last SdpaDecodeDeviceOperation
    last_sdpa_idx = sdpa_decode_indices[-1]

    # Find all EmbeddingsDeviceOperation indices before the last SdpaDecode
    embeddings_indices = []
    for i, row in enumerate(data):
        if i >= last_sdpa_idx:
            break
        op_name = row.get("OP_NAME", "")
        if "EmbeddingsDeviceOperation" in op_name:
            embeddings_indices.append(i)

    if not embeddings_indices:
        print("Warning: No EmbeddingsDeviceOperation found before last SdpaDecode")
        return data

    # The start of the last iteration is the last Embeddings before the last SdpaDecode
    last_iteration_start = embeddings_indices[-1]

    # Keep only data from the last iteration start
    filtered = data[last_iteration_start:]

    total_iterations = len(sdpa_decode_indices)
    print(f"Decode filter: Kept last iteration (removed {total_iterations - 1} earlier iterations)")

    return filtered


def average_decode_ops(data: list) -> list:
    """Average consecutive decode operations of the same type and device.

    Groups only CONSECUTIVE operations by (name, device_id) and computes average timing.
    Renames ops to show repetition count (e.g., "OpName x 10").

    Args:
        data: List of parsed decode operation rows

    Returns:
        List with consecutive same-ops averaged, preserving order
    """
    if not data:
        return data

    # Group consecutive ops by (op_name, device_id)
    groups = []
    current_group = []
    current_key = None  # (op_name, device_id)

    for row in data:
        op_name = row.get("OP_NAME", "Unknown")
        device_id = row.get("DEVICE_ID", "")
        key = (op_name, device_id)
        if key == current_key:
            current_group.append(row)
        else:
            if current_group:
                groups.append((current_key[0], current_group))  # Store op_name and group
            current_key = key
            current_group = [row]

    # Don't forget the last group
    if current_group:
        groups.append((current_key[0], current_group))

    # Create aggregated rows for each consecutive group
    # Store TOTAL time (not average) so comparisons show actual time contribution
    averaged = []
    for op_name, rows in groups:
        count = len(rows)

        # Calculate totals for timing (sum, not average)
        total_kernel_dur = sum(r.get("KERNEL_DUR_us", 0.0) for r in rows)
        avg_fpu_util = sum(r.get("FPU_UTIL%", 0.0) for r in rows) / count
        avg_dram_bw_util = sum(r.get("DRAM_BW_UTIL%", 0.0) for r in rows) / count

        # Get min/max cores across all invocations
        cores_list = [r.get("CORES", 0) for r in rows if r.get("CORES", 0) > 0]
        min_cores = min(cores_list) if cores_list else 0
        max_cores = max(cores_list) if cores_list else 0

        # Use first row as template, update with totals
        avg_row = rows[0].copy()
        avg_row["OP_NAME"] = f"{op_name} x {count}" if count > 1 else op_name
        avg_row["KERNEL_DUR_us"] = total_kernel_dur  # Total time, not average
        avg_row["CORES"] = max_cores  # Use max cores for the aggregated row
        avg_row["FPU_UTIL%"] = avg_fpu_util
        avg_row["DRAM_BW_UTIL%"] = avg_dram_bw_util

        averaged.append(avg_row)

    print(f"Decode averaging: {len(data)} ops -> {len(averaged)} consecutive groups")

    return averaged


def compute_summary(data: list, phase_name: str = None) -> dict:
    """Compute summary statistics for operations.

    Returns:
        Dictionary with op_name -> {total_time_us, count, percentage, min_cores, max_cores}
    """
    op_stats = defaultdict(lambda: {"total_time_us": 0.0, "count": 0, "min_cores": float("inf"), "max_cores": 0})
    total_time = 0.0

    for row in data:
        op_name = row.get("OP_NAME", "Unknown")
        kernel_dur = row.get("KERNEL_DUR_us", 0.0)
        cores = row.get("CORES", 0)
        op_stats[op_name]["total_time_us"] += kernel_dur
        op_stats[op_name]["count"] += 1
        if cores > 0:
            op_stats[op_name]["min_cores"] = min(op_stats[op_name]["min_cores"], cores)
            op_stats[op_name]["max_cores"] = max(op_stats[op_name]["max_cores"], cores)
        total_time += kernel_dur

    # Calculate percentages and fix min_cores for ops with no core data
    for op_name in op_stats:
        if total_time > 0:
            op_stats[op_name]["percentage"] = (op_stats[op_name]["total_time_us"] / total_time) * 100
        else:
            op_stats[op_name]["percentage"] = 0.0
        # If min_cores is still inf, set to 0
        if op_stats[op_name]["min_cores"] == float("inf"):
            op_stats[op_name]["min_cores"] = 0

    return {"phase": phase_name, "total_time_us": total_time, "op_stats": dict(op_stats)}


def print_summary(summary: dict):
    """Print summary statistics to terminal."""
    phase = summary.get("phase", "All")
    total_time = summary.get("total_time_us", 0.0)
    op_stats = summary.get("op_stats", {})

    if not op_stats:
        print(f"\n=== {phase} Summary ===")
        print("No data to summarize")
        return

    # Sort by total time descending
    sorted_ops = sorted(op_stats.items(), key=lambda x: x[1]["total_time_us"], reverse=True)

    print(f"\n{'='*80}")
    print(f"=== {phase} Summary ===")
    print(f"{'='*80}")
    print(f"Total time: {total_time:.2f} us ({total_time/1000:.2f} ms)")
    print(f"{'='*80}")

    # Calculate column widths
    max_op_len = max(len(op) for op in op_stats.keys())
    max_op_len = max(max_op_len, len("OP_NAME"))

    header = f"{'OP_NAME':<{max_op_len}}  {'TOTAL_TIME_us':>14}  {'COUNT':>6}  {'PERCENT':>8}  {'MIN_CORES':>10}  {'MAX_CORES':>10}"
    print(header)
    print("-" * len(header))

    for op_name, stats in sorted_ops:
        print(
            f"{op_name:<{max_op_len}}  {stats['total_time_us']:>14.2f}  {stats['count']:>6}  {stats['percentage']:>7.2f}%  {stats['min_cores']:>10}  {stats['max_cores']:>10}"
        )


def write_csv(data: list, output_path: Path, phase_label: str = None):
    """Write data to a CSV file."""
    if not data:
        print(f"No data to write for {phase_label or 'output'}")
        return

    columns = [
        "OP_NAME",
        "DEVICE_ID",
        "CALL_IDX",
        "KERNEL_DUR_us",
        "CORES",
        "AVAIL_CORES",
        "CORE_UTIL%",
        "MATH_FIDELITY",
        "IN0_SHAPE",
        "IN0_LAYOUT",
        "IN0_DTYPE",
        "IN0_MEM",
        "IN1_SHAPE",
        "IN1_MEM",
        "OUT0_SHAPE",
        "OUT0_MEM",
        "FPU_UTIL%",
        "DRAM_BW_UTIL%",
    ]

    with open(output_path, "w", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(columns)
        for row in data:
            values = []
            for col in columns:
                val = row.get(col, "")
                if isinstance(val, float):
                    if col in ["KERNEL_DUR_us"]:
                        values.append(f"{val:.2f}")
                    elif col in ["CORE_UTIL%", "FPU_UTIL%", "DRAM_BW_UTIL%"]:
                        values.append(f"{val:.1f}")
                    else:
                        values.append(str(val))
                else:
                    values.append(str(val))
            writer.writerow(values)

    print(f"Wrote {len(data)} rows to {output_path}")

    # Also write xlsx if xlsxwriter is available
    if XLSXWRITER_AVAILABLE:
        xlsx_path = output_path.with_suffix(".xlsx")
        write_xlsx(data, xlsx_path, columns, phase_label)


def write_xlsx(data: list, output_path: Path, columns: list, phase_label: str = None):
    """Write data to an Excel file with gradient formatting on duration columns."""
    if not XLSXWRITER_AVAILABLE or not data:
        return

    wb = xlsxwriter.Workbook(str(output_path))
    ws = wb.add_worksheet(phase_label or "data")

    # Style definitions
    header_format = wb.add_format(
        {"bold": True, "bg_color": "#DAEEF3", "align": "center", "valign": "vcenter", "text_wrap": True, "border": 1}
    )

    cell_format = wb.add_format({"border": 1, "align": "left"})
    number_format = wb.add_format({"border": 1, "align": "right", "num_format": "0.00"})
    int_format = wb.add_format({"border": 1, "align": "right", "num_format": "0"})

    # Find duration column index
    dur_col_idx = None
    for idx, col in enumerate(columns):
        if col == "KERNEL_DUR_us":
            dur_col_idx = idx
            break

    # Write headers
    col_widths = []
    for col_idx, header in enumerate(columns):
        ws.write(0, col_idx, header, header_format)
        col_widths.append(len(str(header)))

    # Write data rows
    for row_idx, row in enumerate(data, 1):
        for col_idx, col in enumerate(columns):
            val = row.get(col, "")
            col_widths[col_idx] = max(col_widths[col_idx], len(str(val)))

            if isinstance(val, float):
                ws.write(row_idx, col_idx, val, number_format)
            elif isinstance(val, int):
                ws.write(row_idx, col_idx, val, int_format)
            else:
                # Try to convert string numbers
                try:
                    if "." in str(val):
                        ws.write(row_idx, col_idx, float(val), number_format)
                    elif str(val).lstrip("-").isdigit():
                        ws.write(row_idx, col_idx, int(val), int_format)
                    else:
                        ws.write(row_idx, col_idx, str(val), cell_format)
                except (ValueError, AttributeError):
                    ws.write(row_idx, col_idx, str(val), cell_format)

    # Set column widths
    for col_idx, width in enumerate(col_widths):
        ws.set_column(col_idx, col_idx, min(width + 2, 50))

    # Apply data bar (cell fill gradient) to duration column
    num_data_rows = len(data)
    if num_data_rows > 0 and dur_col_idx is not None:
        ws.conditional_format(
            1,
            dur_col_idx,
            num_data_rows,
            dur_col_idx,
            {
                "type": "data_bar",
                "bar_color": "#F8696B",  # Red fill
                "bar_solid": True,  # Solid fill (not gradient bar)
                "bar_direction": "left",  # Fill from left
            },
        )

    # Freeze header row and first column (OP_NAME)
    ws.freeze_panes(1, 1)

    wb.close()
    print(f"Wrote {len(data)} rows to {output_path}")


def main():
    parser = argparse.ArgumentParser(description="Parse TT-Metal profiler reports")
    parser.add_argument("csv_file", help="Path to the CSV file")
    parser.add_argument("--sort-by", default="CALL_IDX", help="Sort by column (default: CALL_IDX)")
    parser.add_argument("--filter-op", help="Filter by operation name (substring match)")
    parser.add_argument("--device-id", type=int, help="Filter by device ID")
    parser.add_argument("--limit", type=int, help="Limit output to N rows")
    parser.add_argument(
        "--phase",
        choices=["prefill", "decode"],
        help="Show only 'prefill' or 'decode' phase (default: show both separately)",
    )
    parser.add_argument("--summary", action="store_true", help="Show summary statistics (total time and %% per op)")
    parser.add_argument(
        "--no-outliers",
        action="store_true",
        help="Zero out kernel duration for outlier ops (DramPrefetcher, EmbeddingsDeviceOperation)",
    )
    parser.add_argument(
        "--output-dir",
        type=str,
        default=".",
        help="Directory to write prefill.csv and decode.csv (default: current directory)",
    )
    parser.add_argument(
        "--seq-len",
        type=int,
        help="Sequence length hint for SDPA filtering (SDPAOperation for seq_len<=1024, RingDistributedSdpa for seq_len>1024). "
        "If not provided, auto-detects based on which SDPA type is present.",
    )
    parser.add_argument(
        "--filter-prefill-warmup",
        action="store_true",
        default=True,
        help="Filter prefill data to keep only the last SDPA iteration (removes compilation/trace warmup) (default: enabled)",
    )
    parser.add_argument(
        "--no-filter-prefill-warmup",
        action="store_false",
        dest="filter_prefill_warmup",
        help="Disable prefill warmup filtering",
    )
    parser.add_argument(
        "--normalize-sdpa",
        action="store_true",
        default=True,
        help="Normalize SDPA names (map SDPAOperation and RingDistributedSdpa to 'SDPA') for comparison (default: enabled)",
    )
    parser.add_argument(
        "--no-normalize-sdpa",
        action="store_false",
        dest="normalize_sdpa",
        help="Disable SDPA name normalization",
    )
    parser.add_argument(
        "--normalize-matmul",
        action="store_true",
        default=True,
        help="Normalize Matmul names (map MinimalMatmulDeviceOperation to MatmulDeviceOperation) for comparison (default: enabled)",
    )
    parser.add_argument(
        "--no-normalize-matmul",
        action="store_false",
        dest="normalize_matmul",
        help="Disable Matmul name normalization",
    )
    parser.add_argument(
        "--avg-decode",
        action="store_true",
        default=True,
        help="Average decode ops across token generations and show count (e.g., 'OpName x 10') (default: enabled)",
    )
    parser.add_argument(
        "--no-avg-decode",
        action="store_false",
        dest="avg_decode",
        help="Disable decode op averaging",
    )
    parser.add_argument(
        "--filter-decode-warmup",
        action="store_true",
        default=True,
        help="Filter decode data to keep only the last iteration (removes compilation/trace warmup) (default: enabled)",
    )
    parser.add_argument(
        "--no-filter-decode-warmup",
        action="store_false",
        dest="filter_decode_warmup",
        help="Disable decode warmup filtering",
    )

    args = parser.parse_args()

    csv_path = Path(args.csv_file)
    if not csv_path.exists():
        print(f"Error: File not found: {csv_path}")
        sys.exit(1)

    # Parse the CSV
    data = []
    with open(csv_path, "r") as f:
        reader = csv.DictReader(f)
        for row in reader:
            # Skip host-only entries (no device ID or no core count)
            # These are summary rows without actual device execution data
            device_id = row.get("DEVICE ID", "").strip()
            core_count = row.get("CORE COUNT", "").strip()
            if not device_id or not core_count:
                continue

            parsed = parse_row(row)

            # Apply filters
            if args.filter_op and args.filter_op.lower() not in parsed["OP_NAME"].lower():
                continue
            if args.device_id is not None and str(args.device_id) != str(parsed["DEVICE_ID"]):
                continue

            data.append(parsed)

    # Sort by CALL_IDX first to ensure proper order for phase splitting
    try:
        data.sort(key=lambda x: float(x.get("CALL_IDX", 0)) if x.get("CALL_IDX") else 0)
    except (ValueError, TypeError):
        pass

    # Zero out outliers if requested
    if args.no_outliers:
        data = zero_outliers(data)

    # Split into prefill/decode phases
    prefill_data, decode_data = split_prefill_decode(data)

    # Apply prefill warmup filtering (keep only last SDPA iteration)
    if args.filter_prefill_warmup and prefill_data:
        prefill_data = filter_to_last_sdpa(prefill_data, args.seq_len)

    # Normalize SDPA names if requested
    if args.normalize_sdpa:
        prefill_data = normalize_sdpa_names(prefill_data)

    # Normalize Matmul names if requested
    if args.normalize_matmul:
        prefill_data = normalize_matmul_names(prefill_data)

    # Filter decode warmup if requested (keep only last iteration)
    if args.filter_decode_warmup and decode_data:
        decode_data = filter_decode_to_last_iteration(decode_data)

    # Average decode ops if requested
    if args.avg_decode and decode_data:
        decode_data = average_decode_ops(decode_data)

    # Determine which data to show based on --phase
    # Default behavior (no --phase): show both prefill and decode separately
    if args.phase == "prefill":
        display_data = [("Prefill", prefill_data)]
    elif args.phase == "decode":
        display_data = [("Decode", decode_data)]
    else:
        # 'both' or None (default) - show both phases separately
        display_data = [("Prefill", prefill_data), ("Decode", decode_data)]

    # Prepare output directory
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    for phase_label, phase_data in display_data:
        if not phase_data:
            if phase_label:
                print(f"\n=== {phase_label} Phase ===")
                print("No data for this phase")
            continue

        # Sort by requested column
        sort_col = args.sort_by
        if sort_col in phase_data[0] if phase_data else []:
            try:
                # Try numeric sort first
                phase_data.sort(key=lambda x: float(x.get(sort_col, 0)) if x.get(sort_col) else 0)
            except (ValueError, TypeError):
                # Fall back to string sort
                phase_data.sort(key=lambda x: str(x.get(sort_col, "")))

        # Limit
        limited_data = phase_data
        if args.limit:
            limited_data = phase_data[: args.limit]

        # Write to CSV file
        output_filename = f"{phase_label.lower()}.csv" if phase_label else "output.csv"
        output_path = output_dir / output_filename
        write_csv(limited_data, output_path, phase_label)

        # Print summary to terminal
        if args.summary:
            summary = compute_summary(phase_data, phase_label or "All")
            print_summary(summary)


if __name__ == "__main__":
    main()

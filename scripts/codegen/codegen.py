import re
from dataclasses import dataclass
from typing import List, Dict, TextIO, NoReturn


@dataclass
class Field:
    name: str
    type: str
    struct_idx: int | None
    array_size_idx: int | None


@dataclass
class Struct:
    name: str
    fields: List[Field]
    array_sizes: List[str]
    struct_ids: List[int]


class CodeGen:
    SCALAR_TYPES = {
        "char",
        "int8_t",
        "int16_t",
        "int32_t",
        "int64_t",
        "byte",
        "uint8_t",
        "uint16_t",
        "uint32_t",
        "uint64_t",
        "bool",
    }

    @classmethod
    def is_scalar_type(cls, type_name):
        if match := re.fullmatch("std::atomic<(\w+)>", type_name):
            type_name = match.group(1)
        return type_name in cls.SCALAR_TYPES

    def __init__(self, input_file: TextIO, driver_ns: str, interface_ns: str, interface_name: str):
        self.input_file = input_file
        self.lineno = 0
        self.structs: List[Struct] = []
        self.struct_ids: Dict[str, int] = {}
        self.driver_ns = driver_ns
        self.interface_ns = interface_ns
        self.interface_name = interface_name

    def getline(self) -> str | None:
        skipping = 0
        while line := self.input_file.readline():
            self.lineno += 1
            if skipping == 0:
                if line.find("// CODEGEN:skip") != -1:
                    continue
                if (idx := line.find("//")) != -1:
                    line = line[:idx]
                line = line.strip()
                if line.startswith("#if"):
                    skipping = 1
                    continue
                if line == "" or line.startswith("#"):
                    continue
                return line
            else:
                if line.startswith("#if"):
                    skipping += 1
                if line.startswith("#endif"):
                    skipping -= 1
        return None

    def parse_error(self, line, reason) -> NoReturn:
        print(f"Encountered parse error at {self.input_file.name}:{self.lineno}:")
        print(line)
        print("Reason: ", reason)
        raise ValueError("Failed to parse input file")

    def parse(self) -> None:
        while line := self.getline():
            if re.match(r"(?:static )?constexpr|static_assert", line):
                continue
            match = re.fullmatch(r"(?:struct|union) (\w+) {", line)
            if not match:
                self.parse_error(line, "expect struct definition")
            struct_name = match.group(1)
            struct_def = self.parse_struct(struct_name)
            struct_id = len(self.structs)
            self.structs.append(struct_def)
            self.struct_ids[struct_name] = struct_id

    def parse_struct(self, name) -> Struct:
        fields: List[Field] = []
        array_sizes: List[str] = []
        struct_ids: List[int] = []
        distinct_array_sizes: Dict[str, int] = {}
        level = 1
        while level > 0:
            line = self.getline()
            if not line:
                self.parse_error("<end of file>", "incomplete struct definition")
            if re.fullmatch(r"}(?: __attribute__\(\(\w+\)\))?;", line):
                level -= 1
                continue
            # anonymous structs/unions are allowed
            if re.fullmatch(r"(?:struct|union) {", line):
                level += 1
                continue
            match = re.fullmatch(r"(?:volatile )?(?:struct | union )?([\w<>: ]+) +(\w+)(?:\[(\w+)\])? *(?:=.*)?;", line)
            if not match:
                self.parse_error(line, "expect struct field definition")
            type_name = match.group(1)
            field_name = match.group(2)
            array_size = match.group(3)
            if array_size is not None:
                array_size_idx = distinct_array_sizes.get(array_size)
                if array_size_idx is None:
                    distinct_array_sizes[array_size] = array_size_idx = len(array_sizes)
                    array_sizes.append(array_size)
            else:
                array_size_idx = None
            struct_id = self.struct_ids.get(type_name)
            if struct_id is not None:
                struct_ids.append(struct_id)
            elif not self.is_scalar_type(type_name):
                self.parse_error(line, f"unresolved type {type_name}")
            fields.append(Field(field_name, type_name, struct_id, array_size_idx))
        return Struct(name, fields, array_sizes, struct_ids)

    def generate_interface_header(self, file: TextIO):
        print("#include <cstddef>", file=file)
        print("#include <cstdint>", file=file)
        print("#include <vector>", file=file)
        print('#include "struct_view_driver.h"', file=file)
        print(f"// Generated from {self.input_file.name}.  DO NOT EDIT THIS FILE.", file=file)
        print(f"namespace {self.interface_ns} {{", file=file)
        print("namespace fields {", file=file)
        for struct in self.structs:
            print(f"enum class {struct.name} : size_t {{", file=file)
            print(*[field.name for field in struct.fields], sep=", ", file=file)
            print("};", file=file)
        print("}  // namespace fields", file=file)
        print("namespace detail {", file=file)
        print("struct Impl {", file=file)
        for struct in self.structs:
            print(f"{self.driver_ns}::StructInfo {struct.name};", file=file)
        print("};", file=file)
        print("}  // namespace detail", file=file)
        print("namespace template_impl {", file=file)
        for struct in self.structs:
            print(
                f"template <typename T>\n"
                f"struct {struct.name} : public {self.driver_ns}::BaseStructView<T, {struct.name}<T>> {{\n"
                f"using BaseStructView = ::tt::tt_metal::detail::BaseStructView<T, {struct.name}<T>>;\n"
                f"using fields = fields::{struct.name};",
                file=file,
            )
            print(f"using BaseStructView::BaseStructView;", file=file)
            # print(f"ViewImpl(const {self.driver_ns}::StructInfo* info, {const}std::byte* base) : {base}(info, base) {{}}", file=file);
            print("// Magic numbers are generated by code generator.  See comments at top of file.", file=file)
            for field_id, field in enumerate(struct.fields):
                if field.array_size_idx is None:
                    if field.struct_idx is None:
                        # scalar field
                        print(
                            f"auto& {field.name}() const {{"
                            f"return BaseStructView::template scalar_field<{field.type}>({field_id});"
                            f"}}",
                            file=file,
                        )
                    else:
                        # struct field
                        struct_info_idx = len(struct.fields) + len(struct.array_sizes) + field.struct_idx
                        print(
                            f"auto {field.name}() const {{ "
                            f"return BaseStructView::template struct_field<{field.type}<T>>({field_id}, {struct_info_idx});"
                            f"}}",
                            file=file,
                        )
                else:
                    if field.struct_idx is None:
                        # scalar array
                        array_size_idx = len(struct.fields) + field.array_size_idx
                        print(
                            f"auto {field.name}() const {{"
                            f"return BaseStructView::template scalar_array<{field.type}>({field_id}, {array_size_idx});"
                            f"}}",
                            file=file,
                        )
                    else:
                        # struct array
                        struct_info_idx = len(struct.fields) + len(struct.array_sizes) + field.struct_idx
                        array_size_idx = len(struct.fields) + field.array_size_idx
                        print(
                            f"auto {field.name}() const {{"
                            f"return BaseStructView::template struct_array<{field.type}<T>>({field_id}, {struct_info_idx}, {array_size_idx});"
                            f"}}",
                            file=file,
                        )
            print("};", file=file)
        print("}  // namespace template_impl", file=file)

        print("namespace types {", file=file)
        for struct in self.structs:
            print(
                f"class {struct.name} : public {self.driver_ns}::StructStorage<template_impl::{struct.name}, {struct.name}> {{",
                file=file,
            )
            print(f"public:\n" f"using StructStorage::StructStorage;\n" f"enum class fields : size_t {{", file=file)
            print(*[field.name for field in struct.fields], sep=", ", file=file)
            print("};", file=file)
            for field in struct.fields:
                print(f"decltype(auto) {field.name}() {{return view(*this).{field.name}();}}", file=file)
                print(f"decltype(auto) {field.name}() const {{return const_view(*this).{field.name}();}}", file=file)
            print("};", file=file)
        print("}  // namespace types\n" "using namespace types;", file=file)

        print(
            "class Factory {\n"
            "private:\n"
            "const detail::Impl* impl_;\n"
            "public:\n"
            "Factory(const detail::Impl& impl) : impl_(&impl) {}",
            file=file,
        )
        for struct in self.structs:
            for const, view in (("", "view"), ("const ", "const_view")):
                print(f"// Create {view} from arbitrary buffers - not memory safe", file=file)
                print(
                    f"types::{struct.name}::{view} {struct.name}({const}void* base) const {{\n"
                    f"return {{&impl_->{struct.name}, static_cast<{const}std::byte*>(base)}};"
                    f"}}",
                    file=file,
                )
                print(f"// Create memory-owning object", file=file)
            print(
                f"types::{struct.name} {struct.name}() const {{\n" f"return {{&impl_->{struct.name}}};" f"}}", file=file
            )
        print("};", file=file)
        print(f"}}  // namespace {self.interface_ns}", file=file)

    def generate_impl_header(self, file: TextIO, include_filename: str):
        print(f"// Generated from {self.input_file.name}.  DO NOT EDIT THIS FILE.", file=file)
        print(
            "// This file is to be embedded as part of arch-specifc code in HAL, and should not be used anywhere else.\n"
            "// It should be included within a arch-specific namespace.\n",
            file=file,
        )
        print(f"namespace raw_structs{{\n" f'#include "{include_filename}"\n' f"}}  //namespace", file=file)
        print(
            "namespace offsets {\n"
            f"using namespace raw_structs;  // in case the generated code needs to refer to the constants defined there...",
            file=file,
        )
        for struct in self.structs:
            print(f"static const uintptr_t {struct.name}[] = {{", file=file)
            print(f"sizeof(raw_structs::{struct.name}),", file=file)
            for field in struct.fields[1:]:
                print(f"static_cast<uintptr_t>(offsetof(raw_structs::{struct.name}, {field.name})),", file=file)
            for array_size in struct.array_sizes:
                print(f"static_cast<uintptr_t>({array_size}),", file=file)
            for struct_id in struct.struct_ids:
                print(f"reinterpret_cast<uintptr_t>(&{self.structs[struct_id].name}[0]),", file=file)
            print("};", file=file)
            offsets_len = len(struct.fields) + len(struct.array_sizes) + len(struct.struct_ids)
            print(f"static_assert(sizeof({struct.name}) == {offsets_len} * sizeof(uintptr_t));", file=file)
        print("}  // namespace offsets", file=file)
        print(f"static {self.interface_ns}::detail::Impl impl_ {{", file=file)
        for struct in self.structs:
            print(f"{{offsets::{struct.name}}},", file=file)
        print("};", file=file)


import pprint

filename = "dev_msgs.h"
with open(filename, "r") as f:
    cg = CodeGen(f, "::tt::tt_metal::detail", "tt::tt_metal::hal::dev_msgs", "")
    cg.parse()
    pprint.pprint(cg.structs)
    with open("dev_msgs_interface.h", "w") as g:
        cg.generate_interface_header(g)
    with open("dev_msgs_impl.hpp", "w") as g:
        cg.generate_impl_header(g, "dev_msgs.h")

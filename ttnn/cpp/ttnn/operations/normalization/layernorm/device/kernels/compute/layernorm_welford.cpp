#include <cstdint>

#define BCAST_LLKOP EltwiseBinaryType::ELWMUL
#define BCAST_DIM BroadcastType::COL

#include "api/compute/bcast.h"
#include "api/compute/eltwise_binary.h"
#include "api/compute/eltwise_binary_sfpu.h"
#include "api/compute/eltwise_unary/rsqrt.h"
#include "api/compute/welford.h"
#include "api/compute/transpose_wh.h"
#include "api/compute/compute_kernel_hw_startup.h"
#include "ttnn/operations/normalization/kernel_util/compute/memory.h"
#include "ttnn/operations/normalization/kernel_util/generic/blocked_range.h"
#include "api/dataflow/circular_buffer.h"

namespace kutil = norm::kernel_util;
namespace generic = kutil::generic;

void kernel_main() {
    uint32_t NCHt = get_arg_val<uint32_t>(0);
    constexpr uint32_t Wt = get_compile_time_arg_val(0);
    constexpr uint32_t blk = get_compile_time_arg_val(1);
    constexpr uint32_t do_gamma = get_compile_time_arg_val(2);
    constexpr uint32_t do_beta = get_compile_time_arg_val(3);
    constexpr bool FLOAT32_DTYPE = get_compile_time_arg_val(4) == 1;
    constexpr uint32_t W = get_compile_time_arg_val(5);
    constexpr uint32_t tile_width = get_compile_time_arg_val(6);
    constexpr bool fuse_pre_add = static_cast<bool>(get_compile_time_arg_val(8));

    constexpr uint32_t onetile = 1;

    // CB indices - configurable via named compile-time args for kernel chaining support
    constexpr auto cb_eps = get_named_compile_time_arg_val("cb_eps");  // single tile generated by the reader
    constexpr auto cb_in = get_named_compile_time_arg_val("cb_in");    // input x or a for fused pre-add (x=a+b)
    constexpr auto cb_inb = get_named_compile_time_arg_val("cb_inb");  // input b for fused pre-add
    constexpr auto cb_out = get_named_compile_time_arg_val("cb_out");  // output
    constexpr auto cb_gamma = get_named_compile_time_arg_val("cb_gamma");
    constexpr auto cb_beta = get_named_compile_time_arg_val("cb_beta");
    constexpr auto cb_xmm = get_named_compile_time_arg_val("cb_xmm");  // x - E[x]

    constexpr auto cb_ex = get_named_compile_time_arg_val("cb_ex");                    // E[x]
    constexpr auto cb_ex2 = get_named_compile_time_arg_val("cb_ex2");                  // E[(x-E[x])^2]
    constexpr auto cb_ex2pe = get_named_compile_time_arg_val("cb_ex2pe");              // E[(x-E[x])^2]+eps
    constexpr auto cb_fusion = get_named_compile_time_arg_val("cb_fusion");            // stream gamma/beta
    constexpr auto cb_reciprocals = get_named_compile_time_arg_val("cb_reciprocals");  // Pre-computed reciprocals
    CircularBuffer cb_eps_obj(cb_eps);
    CircularBuffer cb_in_obj(cb_in);
    CircularBuffer cb_inb_obj(cb_inb);
    CircularBuffer cb_out_obj(cb_out);
    CircularBuffer cb_gamma_obj(cb_gamma);
    CircularBuffer cb_beta_obj(cb_beta);
    CircularBuffer cb_xmm_obj(cb_xmm);
    CircularBuffer cb_ex_obj(cb_ex);
    CircularBuffer cb_ex2_obj(cb_ex2);
    CircularBuffer cb_ex2pe_obj(cb_ex2pe);
    CircularBuffer cb_fusion_obj(cb_fusion);

    constexpr auto cb_im_or_out = (do_gamma | do_beta) ? cb_fusion : cb_out;
    CircularBuffer cb_im_or_out_obj(cb_im_or_out);

    //  Either in or in + b if doing fused pre-add
    constexpr auto cb_x = []() -> auto {
        if constexpr (fuse_pre_add) {
            return get_named_compile_time_arg_val("cb_x");
        } else {
            return cb_in;
        }
    }();
    CircularBuffer cb_x_obj(cb_x);

    // Welford-fp32 alias of cb_x. Shares L1 memory with cb_x but has its own buffer index
    // configured with unpack_to_dest_mode=UnpackToDestFp32. Welford's transpose_wh_tile reads
    // through cb_x_welford to get full fp32 into DEST; the post-welford eltwise keeps reading
    // cb_x via SrcA. When welford_fp32_alias is 0, cb_x_welford == cb_x and the alias's wait/pop
    // calls below operate on the same CB as cb_x.
    constexpr auto cb_x_welford = get_named_compile_time_arg_val("cb_x_welford");
    constexpr bool welford_fp32_alias = get_named_compile_time_arg_val("welford_fp32_alias") != 0;
    CircularBuffer cb_x_welford_obj(cb_x_welford);

    constexpr uint32_t dst0 = 0;
    constexpr uint32_t input_dst = 0;  // Input tile for Welford's algorithm
    constexpr uint32_t mean_dst = 1;
    constexpr uint32_t var_dst = 2;

    // The number of valid columns in the last tile in width dimension.
    // Because the Welford's llk is given transposed data, skip some rows when
    // we want to skip some columns from getting processed by layer_norm.
    constexpr uint32_t last_tile_rows = (W % tile_width) == 0 ? tile_width : W % tile_width;

    cb_eps_obj.wait_front(1);  // comes from the reader

    if constexpr (fuse_pre_add) {
        binary_op_init_common(cb_in, cb_inb, cb_x);
        pack_reconfig_data_format(cb_x);
    } else {
        compute_kernel_hw_startup(cb_in, cb_ex);
        pack_reconfig_data_format(cb_ex);
    }

    // Get pointer to the reciprocal LUT
    using recip_lut_t = std::array<uint32_t, W>;
    auto p_reciprocals = kutil::compute::memory::get_pointer_to_cb_data<recip_lut_t>(cb_reciprocals, 0);

    // Intermediate buffers need to be reserved/pushed/popped
    // in full blocks
    const auto total_buffer_size = generic::blocks(Wt, blk).total_with_remainder();

    for (uint32_t ncht = 0; ncht < NCHt; ncht++) {
        if constexpr (fuse_pre_add) {
            // x = in + b
            add_tiles_init(cb_in, cb_inb);
            reconfig_data_format(cb_in, cb_inb);
            pack_reconfig_data_format(cb_x);
            for (auto block : generic::blocks(Wt, blk)) {
                // In/inb come from the reader and need to be
                // synced on full block size. Keep cb_x aligned
                // to full block size as well so pre-add/no-pre-add
                // can be handled the same way.
                cb_in_obj.wait_front(block.full_block_size());
                cb_inb_obj.wait_front(block.full_block_size());
                tile_regs_acquire();
                for (auto i : block.local()) {
                    add_tiles(cb_in, cb_inb, i, i, i);
                }
                tile_regs_commit();
                cb_in_obj.pop_front(block.full_block_size());
                cb_inb_obj.pop_front(block.full_block_size());

                cb_x_obj.reserve_back(block.full_block_size());
                if constexpr (welford_fp32_alias) {
                    // Fused: compute is the producer of cb_x (post-add sum). Push the alias
                    // alongside so welford-section wait_front on cb_x_welford sees the tiles.
                    cb_x_welford_obj.reserve_back(block.full_block_size());
                }
                tile_regs_wait();
                for (auto i : block.local()) {
                    pack_tile(i, cb_x);
                }
                tile_regs_release();
                cb_x_obj.push_back(block.full_block_size());  // push the sum into the same buffer
                if constexpr (welford_fp32_alias) {
                    cb_x_welford_obj.push_back(block.full_block_size());
                }
            }
            reconfig_data_format(cb_in, cb_x, cb_inb, cb_ex);
        }

        // Simultaneous calculation of E[x] and Var[x] using Welford's algorithm.
        //
        // Welford reads input tiles through cb_x_welford, which shares L1 memory with cb_x but
        // is configured for UnpackToDest fp32 (vs cb_x's default TF32 SrcA path). The reader
        // already pushed cb_x_welford in non-fused mode; in fused mode the compute pushes it
        // right after pack_tile to cb_x (see fused_pre_add section above).
        //
        // The post-welford eltwise reads cb_x directly (FPU binary ops can't use UnpackToDest).
        // cb_x and cb_x_welford have independent semaphores so we wait_front and pop_front
        // them separately. When welford_fp32_alias is 0, cb_x_welford == cb_x so the two sets
        // of semaphore ops collapse onto the same CB and the alias-side waits/pops are
        // redundant -- gated by welford_fp32_alias.
        uint32_t start_N = 0;
        reconfig_data_format_srca(cb_x_welford);
        // Full transpose_wh hw init when the alias is active. Without this the unpacker is missing
        // hw config bits for cb_x_welford's fresh buffer index (math/pack hw_configure paths) that
        // compute_kernel_hw_startup only programmed for cb_in.  reconfig_data_format_srca alone
        // updates only src/dst format and tile size, which isn't enough when the buffer index
        // wasn't seen by compute_kernel_hw_startup at the top of the kernel.  For the non-alias
        // path cb_x_welford == cb_x == cb_in, so the existing hw config is already correct and
        // a single transpose_wh_init_short before the loop suffices.
        if constexpr (welford_fp32_alias) {
            transpose_wh_init(cb_x_welford, cb_ex);
        } else {
            transpose_wh_init_short(cb_x_welford);
        }
        tile_regs_acquire();
        welford_init();
        // When cb_x_welford is configured for UnpackToDest fp32 (welford_fp32_alias=true),
        // transpose_wh_tile takes the UnpackToDest path which calls llk_math_transpose_dest.
        // That writes to SFPU replay buffer slot 0 -- the same slot welford_init programmed
        // with the welford recurrence. Re-establish welford state after each transpose_wh_tile
        // so welford_update replays welford ops instead of stale transpose-dest ops. LREG4/5
        // (the running mean / M2 accumulator) survive transpose_dest because it only uses FPU
        // MOVs. When welford_fp32_alias is false (e.g. fp32_dest_acc_en=false path), the unpack
        // dst format is not Float32 so transpose_wh_tile takes the SrcA path which does not
        // touch the replay buffer, and the welford_reinit/sfpu_init calls are no-ops by intent.
        // Process all but the last tile
        for (uint32_t wt = 0; wt < (Wt - 1); ++wt) {
            if constexpr (welford_fp32_alias) {
                cb_x_welford_obj.wait_front(wt + 1);
                // welford_init (or the previous iteration's welford_reinit recovery) left SFPU
                // replay slot 0 holding the welford recurrence. The fp32 transpose_wh_tile uses
                // a MOP that references slot 0 for its transpose instructions, so re-init
                // transpose state to put transpose code back into slot 0.
                transpose_wh_init_short(cb_x_welford);
            } else {
                cb_x_obj.wait_front(wt + 1);
            }
            transpose_wh_tile(cb_x_welford, wt, input_dst);
            if constexpr (welford_fp32_alias) {
                // transpose_wh_tile took the UnpackToDest fp32 path which clobbered the welford
                // recurrence in SFPU replay slot 0. Restore welford state before welford_update.
                // welford_reinit also reprograms unpack-A for an UnpackToDest (transpose=0) read;
                // the next iteration's transpose_wh_init_short will switch it back.
                welford_reinit(cb_x_welford);
                MATH((llk_math_welfords_sfpu_init()));
            }
            welford_update<W>(input_dst, start_N, *p_reciprocals);
            start_N += tile_width;
        }

        // Process the last tile
        // cb_x is synced on full blocks, so we need to wait for the
        // last tile + any remaining in the last block
        const auto num_to_wait = generic::blocks(Wt, blk).total_with_remainder();
        if constexpr (welford_fp32_alias) {
            cb_x_welford_obj.wait_front(num_to_wait);
            // Same reason as the loop: ensure slot 0 holds transpose instructions before
            // the fp32 transpose_wh_tile reads from it.
            transpose_wh_init_short(cb_x_welford);
        } else {
            cb_x_obj.wait_front(num_to_wait);
        }
        transpose_wh_tile(cb_x_welford, Wt - 1, input_dst);
        if constexpr (welford_fp32_alias) {
            welford_reinit(cb_x_welford);
            MATH((llk_math_welfords_sfpu_init()));
        }
        welford_update_rows<W>(input_dst, start_N, 0, last_tile_rows, *p_reciprocals);

        // Store the mean and variance to the destination registers
        welford_finalize_to_row<W>(mean_dst, W - 1, *p_reciprocals);
        tile_regs_commit();

        // Note: intentionally NOT popping cb_x_welford here. With multi-buffer-index CBs the
        // underlying allocation appears to track a shared read pointer (matching matmul's
        // shared output+interm pattern where the producer reuses freed slots). Popping the
        // alias would also free cb_x's slot, making the subsequent eltwise see stale/zero
        // data. The cb_x_welford semaphore just stays at produced=consumed offset; the data is
        // logically released when cb_x is popped at the end of the eltwise section.
        // TODO: verify whether multi-format CB allocations have shared or per-index read
        // pointers; if per-index, we'd want to pop here to allow reader reuse across NCHt
        // iterations on tight-L1 configurations.

        // Transpose mean and var back to columns
        cb_ex_obj.reserve_back(onetile);
        cb_ex2_obj.reserve_back(onetile);
        tile_regs_wait();
        pack_reconfig_data_format(cb_ex);
        pack_tile(mean_dst, cb_ex);
        pack_reconfig_data_format(cb_ex2);
        pack_tile(var_dst, cb_ex2);
        tile_regs_release();
        cb_ex_obj.push_back(onetile);
        cb_ex2_obj.push_back(onetile);

        cb_ex_obj.wait_front(onetile);
        cb_ex2_obj.wait_front(onetile);
        reconfig_data_format_srca(cb_ex);
        transpose_wh_init_short(cb_ex);
        tile_regs_acquire();
        transpose_wh_tile(cb_ex, 0, mean_dst);
        transpose_wh_tile(cb_ex2, 0, var_dst);
        tile_regs_commit();

        cb_ex_obj.pop_front(onetile);
        cb_ex2_obj.pop_front(onetile);

        cb_ex_obj.reserve_back(onetile);
        cb_ex2_obj.reserve_back(onetile);

        pack_reconfig_data_format(cb_ex);
        tile_regs_wait();
        pack_tile(mean_dst, cb_ex);
        pack_reconfig_data_format(cb_ex2);
        pack_tile(var_dst, cb_ex2);
        tile_regs_release();

        cb_ex_obj.push_back(onetile);
        cb_ex2_obj.push_back(onetile);

        // x - E[x]
        // Reuse cb_x since we didn't pop anything from it
        if constexpr (FLOAT32_DTYPE) {
            reconfig_data_format(cb_x, cb_ex);
        }
        cb_ex_obj.wait_front(onetile);  // should have 1 tile
        cb_xmm_obj.reserve_back(total_buffer_size);
        sub_bcast_cols_init_short(cb_x, cb_ex);
        for (auto block : generic::blocks(Wt, blk)) {
            tile_regs_acquire();
            for (auto i : block.local()) {
                sub_tiles_bcast_cols(cb_x, cb_ex, i, 0, i);
            }
            tile_regs_commit();
            tile_regs_wait();
            for (auto i : block.local()) {
                pack_tile(i, cb_xmm);
            }
            tile_regs_release();
            cb_xmm_obj.push_back(block.full_block_size());
            cb_x_obj.pop_front(block.full_block_size());
        }
        cb_ex_obj.pop_front(1);
        cb_xmm_obj.wait_front(total_buffer_size);

        if constexpr (!fuse_pre_add) {
            reconfig_data_format_srca(cb_x, cb_xmm);
        }

        // Var(x) + eps
        if constexpr (FLOAT32_DTYPE) {
            reconfig_data_format(cb_ex2, cb_eps);
        }
        cb_ex2_obj.wait_front(onetile);  // should have 1 tile
        tile_regs_acquire();
        add_tiles_init(cb_ex2, cb_eps);
        add_tiles(cb_ex2, cb_eps, 0, 0, dst0);
        rsqrt_tile_init();
        rsqrt_tile(dst0);
        tile_regs_commit();
        cb_ex2_obj.pop_front(onetile);

        cb_ex2pe_obj.reserve_back(onetile);
        tile_regs_wait();
        pack_tile(dst0, cb_ex2pe);
        tile_regs_release();
        cb_ex2pe_obj.push_back(onetile);

        // Remainder of the layernorm operation
        // norm(x) * gamma + beta,
        // where norm(x) is:
        // (x - E[x]) / sqrt(E[(x-E[x])^2] + eps)
        cb_ex2pe_obj.wait_front(onetile);
        for (auto block : generic::blocks(Wt, blk)) {
            reconfig_data_format(cb_xmm, cb_ex2pe);
            if constexpr (do_gamma == 0 && do_beta == 0) {
                pack_reconfig_data_format(cb_out);
            } else {
                pack_reconfig_data_format(cb_fusion);
            }

            mul_bcast_cols_init_short(cb_xmm, cb_ex2pe);
            tile_regs_acquire();
            for (auto i : block.local()) {
                // cb_xmm[wt+wtr] since we pop Wt from cb_xmm after the entire loop
                mul_tiles_bcast_cols(cb_xmm, cb_ex2pe, block.to_global(i), 0, i);
            }
            tile_regs_commit();

            cb_im_or_out_obj.reserve_back(block.full_block_size());
            tile_regs_wait();
            for (auto i : block.local()) {
                pack_tile(i, cb_im_or_out);  // pack either to intermediate (cb_fusion or out0)
            }
            tile_regs_release();
            cb_im_or_out_obj.push_back(
                block.full_block_size());  // if no gamma/beta are provided, this will be passed on to the writer

            if constexpr (do_gamma) {
                if constexpr (do_beta == 0) {
                    pack_reconfig_data_format(cb_out);
                }
                reconfig_data_format_srcb(cb_ex2pe, cb_gamma);
                uint32_t cb_outg = do_beta ? cb_fusion : cb_out;
                CircularBuffer cb_outg_obj(cb_outg);
                mul_bcast_rows_init_short(cb_fusion, cb_gamma);
                cb_gamma_obj.wait_front(
                    block.start() + block.full_block_size());  // we don't pop, TODO: only wait on first ht
                cb_fusion_obj.wait_front(block.full_block_size());
                tile_regs_acquire();
                for (auto i : block.local()) {
                    mul_tiles_bcast_rows(cb_fusion, cb_gamma, i, block.to_global(i), i);  // tile *= 1/(sum(exp(x)))
                }
                tile_regs_commit();
                // We don't pop gamma since it's 1,1,1,Wt and we reuse it for all NCHt
                cb_fusion_obj.pop_front(block.full_block_size());

                cb_outg_obj.reserve_back(block.full_block_size());
                tile_regs_wait();
                for (auto i : block.local()) {
                    pack_tile(i, cb_outg);  // pack either to intermediate (cb_fusion or out0)
                }
                tile_regs_release();
                cb_outg_obj.push_back(block.full_block_size());
            }
            if constexpr (do_beta) {
                pack_reconfig_data_format(cb_out);
                if constexpr (do_gamma) {
                    reconfig_data_format_srcb(cb_gamma, cb_beta);
                } else {
                    reconfig_data_format_srcb(cb_ex2pe, cb_beta);
                }

                add_bcast_rows_init_short(cb_fusion, cb_beta);
                cb_beta_obj.wait_front(
                    block.start() + block.full_block_size());  // TODO: optimization - only wait on first ht
                cb_fusion_obj.wait_front(block.full_block_size());
                tile_regs_acquire();
                for (auto i : block.local()) {
                    add_tiles_bcast_rows(cb_fusion, cb_beta, i, block.to_global(i), i);  // tile *= 1/(sum(exp(x)))
                }
                tile_regs_commit();
                cb_fusion_obj.pop_front(block.full_block_size());
                // We don't pop beta since it's 1,1,1,Wt and we reuse it for all NCHt

                cb_out_obj.reserve_back(block.full_block_size());
                tile_regs_wait();
                for (auto i : block.local()) {
                    pack_tile(i, cb_out);  // pack either to intermediate (cb_fusion or out0)
                }
                tile_regs_release();
                cb_out_obj.push_back(block.full_block_size());
            }
        }
        cb_ex2pe_obj.pop_front(onetile);
        cb_xmm_obj.pop_front(total_buffer_size);

    }  // NCHt loop
}

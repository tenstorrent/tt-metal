#include <cstdint>

#define BCAST_LLKOP EltwiseBinaryType::ELWMUL
#define BCAST_DIM BroadcastType::COL

#include "api/compute/bcast.h"
#include "api/compute/eltwise_binary.h"
#include "api/compute/eltwise_binary_sfpu.h"
#include "api/compute/eltwise_unary/rsqrt.h"
#include "api/compute/welford.h"
#include "api/compute/transpose.h"
#include "api/compute/compute_kernel_hw_startup.h"
#include "ttnn/operations/normalization/kernel_util/compute/memory.h"
#include "ttnn/operations/normalization/kernel_util/generic/blocked_range.h"
#include "api/dataflow/dataflow_buffer.h"

namespace kutil = norm::kernel_util;
namespace generic = kutil::generic;

void kernel_main() {
    uint32_t NCHt = get_arg_val<uint32_t>(0);
    constexpr uint32_t Wt = get_compile_time_arg_val(0);
    constexpr uint32_t blk = get_compile_time_arg_val(1);
    constexpr uint32_t do_gamma = get_compile_time_arg_val(2);
    constexpr uint32_t do_beta = get_compile_time_arg_val(3);
    constexpr bool FLOAT32_DTYPE = get_compile_time_arg_val(4) == 1;
    constexpr uint32_t W = get_compile_time_arg_val(5);
    constexpr uint32_t tile_width = get_compile_time_arg_val(6);
    constexpr bool fuse_pre_add = static_cast<bool>(get_compile_time_arg_val(8));

    constexpr uint32_t onetile = 1;

    // CB indices - configurable via named compile-time args for kernel chaining support
    constexpr auto dfb_eps = get_named_compile_time_arg_val("cb_eps");  // single tile generated by the reader
    constexpr auto dfb_in = get_named_compile_time_arg_val("cb_in");    // input x or a for fused pre-add (x=a+b)
    constexpr auto dfb_inb = get_named_compile_time_arg_val("cb_inb");  // input b for fused pre-add
    constexpr auto dfb_out = get_named_compile_time_arg_val("cb_out");  // output
    constexpr auto dfb_gamma = get_named_compile_time_arg_val("cb_gamma");
    constexpr auto dfb_beta = get_named_compile_time_arg_val("cb_beta");
    constexpr auto dfb_xmm = get_named_compile_time_arg_val("cb_xmm");  // x - E[x]

    constexpr auto dfb_ex = get_named_compile_time_arg_val("cb_ex");                    // E[x]
    constexpr auto dfb_ex2 = get_named_compile_time_arg_val("cb_ex2");                  // E[(x-E[x])^2]
    constexpr auto dfb_ex2pe = get_named_compile_time_arg_val("cb_ex2pe");              // E[(x-E[x])^2]+eps
    constexpr auto dfb_fusion = get_named_compile_time_arg_val("cb_fusion");            // stream gamma/beta
    constexpr auto dfb_reciprocals = get_named_compile_time_arg_val("cb_reciprocals");  // Pre-computed reciprocals
    DataflowBuffer dfb_eps_obj(dfb_eps);
    DataflowBuffer dfb_in_obj(dfb_in);
    DataflowBuffer dfb_inb_obj(dfb_inb);
    DataflowBuffer dfb_out_obj(dfb_out);
    DataflowBuffer dfb_gamma_obj(dfb_gamma);
    DataflowBuffer dfb_beta_obj(dfb_beta);
    DataflowBuffer dfb_xmm_obj(dfb_xmm);
    DataflowBuffer dfb_ex_obj(dfb_ex);
    DataflowBuffer dfb_ex2_obj(dfb_ex2);
    DataflowBuffer dfb_ex2pe_obj(dfb_ex2pe);
    DataflowBuffer dfb_fusion_obj(dfb_fusion);

    constexpr auto dfb_im_or_out = (do_gamma | do_beta) ? dfb_fusion : dfb_out;
    DataflowBuffer dfb_im_or_out_obj(dfb_im_or_out);

    //  Either in or in + b if doing fused pre-add
    constexpr auto dfb_x = []() -> auto {
        if constexpr (fuse_pre_add) {
            return get_named_compile_time_arg_val("cb_x");
        } else {
            return dfb_in;
        }
    }();
    DataflowBuffer dfb_x_obj(dfb_x);

    // Welford-fp32 alias of cb_x. Shares SRAM with cb_x but has its own buffer index
    // configured with UnpackToDestFp32. Welford's transpose_tile reads
    // through cb_x_welford to get full fp32 into DEST; the post-welford eltwise keeps reading
    // cb_x via SrcA. When welford_fp32_alias is false, cb_x_welford == cb_x.
    constexpr auto dfb_x_welford = get_named_compile_time_arg_val("cb_x_welford");
    constexpr bool welford_fp32_alias = get_named_compile_time_arg_val("welford_fp32_alias") != 0;
    DataflowBuffer dfb_x_welford_obj(dfb_x_welford);

    constexpr uint32_t dst0 = 0;
    constexpr uint32_t input_dst = 0;  // Input tile for Welford's algorithm
    constexpr uint32_t mean_dst = 1;
    constexpr uint32_t var_dst = 2;

    // The number of valid columns in the last tile in width dimension.
    // Because the Welford's llk is given transposed data, skip some rows when
    // we want to skip some columns from getting processed by layer_norm.
    constexpr uint32_t last_tile_rows = (W % tile_width) == 0 ? tile_width : W % tile_width;

    dfb_eps_obj.wait_front(1);  // comes from the reader

    if constexpr (fuse_pre_add) {
        binary_op_init_common(dfb_in, dfb_inb, dfb_x);
        pack_reconfig_data_format(dfb_x);
    } else {
        compute_kernel_hw_startup(dfb_in, dfb_ex);
        pack_reconfig_data_format(dfb_ex);
    }

    // Get pointer to the reciprocal LUT
    using recip_lut_t = std::array<uint32_t, W>;
    auto p_reciprocals = kutil::compute::memory::get_pointer_to_cb_data<recip_lut_t>(dfb_reciprocals, 0);

    // Intermediate buffers need to be reserved/pushed/popped
    // in full blocks
    const auto total_buffer_size = generic::blocks(Wt, blk).total_with_remainder();

    for (uint32_t ncht = 0; ncht < NCHt; ncht++) {
        if constexpr (fuse_pre_add) {
            // x = in + b
            add_tiles_init(dfb_in, dfb_inb);
            reconfig_data_format(dfb_in, dfb_inb);
            pack_reconfig_data_format(dfb_x);
            for (auto block : generic::blocks(Wt, blk)) {
                // In/inb come from the reader and need to be
                // synced on full block size. Keep cb_x aligned
                // to full block size as well so pre-add/no-pre-add
                // can be handled the same way.
                dfb_in_obj.wait_front(block.full_block_size());
                dfb_inb_obj.wait_front(block.full_block_size());
                tile_regs_acquire();
                for (auto i : block.local()) {
                    add_tiles(dfb_in, dfb_inb, i, i, i);
                }
                tile_regs_commit();
                dfb_in_obj.pop_front(block.full_block_size());
                dfb_inb_obj.pop_front(block.full_block_size());

                dfb_x_obj.reserve_back(block.full_block_size());
                if constexpr (welford_fp32_alias) {
                    // Must be done in the compute kernel: on the fuse_pre_add path compute is the
                    // producer of cb_x via the add_tiles -> pack_tile sequence below; the reader
                    // never writes cb_x. Push the alias alongside cb_x so Welford's wait_front on
                    // cb_x_welford sees the tiles.
                    dfb_x_welford_obj.reserve_back(block.full_block_size());
                }
                tile_regs_wait();
                for (auto i : block.local()) {
                    pack_tile(i, dfb_x);
                }
                tile_regs_release();
                dfb_x_obj.push_back(block.full_block_size());  // push the sum into the same buffer
                if constexpr (welford_fp32_alias) {
                    dfb_x_welford_obj.push_back(block.full_block_size());
                }
            }
            reconfig_data_format(dfb_in, dfb_x, dfb_inb, dfb_ex);
        }

        // Simultaneous calculation of E[x] and Var[x] using Welford's algorithm.
        //
        // Welford reads input tiles through cb_x_welford, which shares SRAM with cb_x but
        // is configured for UnpackToDestFp32 (vs cb_x's default TF32 SrcA path).
        //
        // The post-welford eltwise reads cb_x directly (FPU binary ops can't use UnpackToDest).
        // cb_x and cb_x_welford have independent read/write pointers so we wait_front and pop_front
        // them separately. When welford_fp32_alias is 0, cb_x_welford == cb_x so the two sets
        // of semaphore ops collapse onto the same CB and the alias-side waits/pops are
        // redundant -- gated by welford_fp32_alias.
        uint32_t start_N = 0;
        reconfig_data_format_srca(dfb_x_welford);
        // Reconfigure the transpose op for the welford intake CB. When the alias is active,
        // cb_x_welford has UnpackToDestFp32 mode so transpose_tile preserves fp32 precision.
        transpose_init(dfb_x_welford);
        tile_regs_acquire();
        welford_init();
        // Welford's recurrence and the fp32 transpose collide in the math thread's replay
        // buffer. The buffer has 32 slots, conventionally split between SFPU [0, 16) and
        // FPU [16, 32). Welford violates that split: welford_init records 32 instructions
        // at slots [0, 32) (4 LREG variants of 8 instructions each, fully unrolled), and
        // welford_update replays all four variants per block.
        //
        // When cb_x_welford is configured for UnpackToDest fp32 (welford_fp32_alias=true),
        // transpose_tile takes the UnpackToDest path. Its math-side init
        // (llk_math_transpose_dest_init, invoked from transpose_init inside the loop
        // below) records 16 instructions at slots [16, 32) for the transpose-dest setup,
        // clobbering welford's LREG2/LREG3 portions. The recovery after each transpose_tile
        // re-records all 32 slots with the welford recurrence so welford_update replays welford
        // ops instead of stale transpose-dest ops. LREG4/5 (the running mean / M2 accumulator)
        // survive transpose_dest because it only uses FPU MOVs.
        //
        // When welford_fp32_alias is false (e.g. fp32_dest_acc_en=false path), the unpack dst
        // format is not Float32 so transpose_tile takes the SrcA path. That path skips
        // llk_math_transpose_dest entirely, so the math-thread replay buffer is untouched
        // and no recovery is needed.
        // Process all but the last tile
        for (uint32_t wt = 0; wt < (Wt - 1); ++wt) {
            if constexpr (welford_fp32_alias) {
                dfb_x_welford_obj.wait_front(wt + 1);
                // SFPU replay slots [0, 32) currently hold the welford recurrence (see outer
                // comment block above). transpose_init re-records slots [16, 32) with
                // the transpose-dest setup so transpose_tile below can replay them.
                transpose_init(dfb_x_welford);
            } else {
                dfb_x_obj.wait_front(wt + 1);
            }
            transpose_tile(dfb_x_welford, wt, input_dst);
            if constexpr (welford_fp32_alias) {
                // transpose_tile took the UnpackToDestFp32 path. Its math-side init clobbered
                // the welford recurrence at SFPU replay slots [16, 32).
                // welford_init<WelfordInitMode::PreserveStats>() re-records all 32 slots with
                // the welford recurrence; PreserveStats keeps the running mean / M2 accumulator
                // in LREG4/5. UNPACK A is left in transpose=1;
                // welford_update is pure SFPU and does not consume that state, and the next
                // iteration's transpose_init reprograms it.
                welford_init<WelfordInitMode::PreserveStats>();
            }
            welford_update<W>(input_dst, start_N, *p_reciprocals);
            start_N += tile_width;
        }

        // Process the last tile
        // cb_x is synced on full blocks, so we need to wait for the
        // last tile + any remaining in the last block
        const auto num_to_wait = generic::blocks(Wt, blk).total_with_remainder();
        if constexpr (welford_fp32_alias) {
            dfb_x_welford_obj.wait_front(num_to_wait);
            transpose_init(dfb_x_welford);
        } else {
            dfb_x_obj.wait_front(num_to_wait);
        }
        transpose_tile(dfb_x_welford, Wt - 1, input_dst);
        if constexpr (welford_fp32_alias) {
            welford_init<WelfordInitMode::PreserveStats>();
        }
        welford_update_rows<W>(input_dst, start_N, 0, last_tile_rows, *p_reciprocals);

        // Store the mean and variance to the destination registers
        welford_finalize_to_row<W>(mean_dst, W - 1, *p_reciprocals);
        tile_regs_commit();

        // Pop cb_x_welford so its rd_ptr advances in lock-step with cb_x's pop in the eltwise
        // loop below. Multi-buffer-index CB indices have independent read/write pointers
        // but share the underlying SRAM; popping the alias only
        // advances cb_x_welford's own rd_ptr, leaving cb_x's state untouched. Without this
        // pop, subsequent NCHt iterations would read stale tiles from the start of the buffer
        // (the reader's push_back advances the wr_ptr, but the alias's rd_ptr stays at 0).
        if constexpr (welford_fp32_alias) {
            dfb_x_welford_obj.pop_front(total_buffer_size);
        }

        // Transpose mean and var back to columns
        dfb_ex_obj.reserve_back(onetile);
        dfb_ex2_obj.reserve_back(onetile);
        tile_regs_wait();
        pack_reconfig_data_format(dfb_ex);
        pack_tile(mean_dst, dfb_ex);
        pack_reconfig_data_format(dfb_ex2);
        pack_tile(var_dst, dfb_ex2);
        tile_regs_release();
        dfb_ex_obj.push_back(onetile);
        dfb_ex2_obj.push_back(onetile);

        dfb_ex_obj.wait_front(onetile);
        dfb_ex2_obj.wait_front(onetile);
        reconfig_data_format_srca(dfb_ex);
        transpose_init(dfb_ex);
        tile_regs_acquire();
        transpose_tile(dfb_ex, 0, mean_dst);
        transpose_tile(dfb_ex2, 0, var_dst);
        tile_regs_commit();

        dfb_ex_obj.pop_front(onetile);
        dfb_ex2_obj.pop_front(onetile);

        dfb_ex_obj.reserve_back(onetile);
        dfb_ex2_obj.reserve_back(onetile);

        pack_reconfig_data_format(dfb_ex);
        tile_regs_wait();
        pack_tile(mean_dst, dfb_ex);
        pack_reconfig_data_format(dfb_ex2);
        pack_tile(var_dst, dfb_ex2);
        tile_regs_release();

        dfb_ex_obj.push_back(onetile);
        dfb_ex2_obj.push_back(onetile);

        // x - E[x]
        // Reuse cb_x since we didn't pop anything from it
        if constexpr (FLOAT32_DTYPE) {
            reconfig_data_format(dfb_x, dfb_ex);
        }
        dfb_ex_obj.wait_front(onetile);  // should have 1 tile
        dfb_xmm_obj.reserve_back(total_buffer_size);
        sub_bcast_cols_init_short(dfb_x, dfb_ex);
        for (auto block : generic::blocks(Wt, blk)) {
            tile_regs_acquire();
            for (auto i : block.local()) {
                sub_tiles_bcast_cols(dfb_x, dfb_ex, i, 0, i);
            }
            tile_regs_commit();
            tile_regs_wait();
            for (auto i : block.local()) {
                pack_tile(i, dfb_xmm);
            }
            tile_regs_release();
            dfb_xmm_obj.push_back(block.full_block_size());
            dfb_x_obj.pop_front(block.full_block_size());
        }
        dfb_ex_obj.pop_front(1);
        dfb_xmm_obj.wait_front(total_buffer_size);

        if constexpr (!fuse_pre_add) {
            reconfig_data_format_srca(dfb_x, dfb_xmm);
        }

        // Var(x) + eps
        if constexpr (FLOAT32_DTYPE) {
            reconfig_data_format(dfb_ex2, dfb_eps);
        }
        dfb_ex2_obj.wait_front(onetile);  // should have 1 tile
        tile_regs_acquire();
        add_tiles_init(dfb_ex2, dfb_eps);
        add_tiles(dfb_ex2, dfb_eps, 0, 0, dst0);
        rsqrt_tile_init();
        rsqrt_tile(dst0);
        tile_regs_commit();
        dfb_ex2_obj.pop_front(onetile);

        dfb_ex2pe_obj.reserve_back(onetile);
        tile_regs_wait();
        pack_tile(dst0, dfb_ex2pe);
        tile_regs_release();
        dfb_ex2pe_obj.push_back(onetile);

        // Remainder of the layernorm operation
        // norm(x) * gamma + beta,
        // where norm(x) is:
        // (x - E[x]) / sqrt(E[(x-E[x])^2] + eps)
        dfb_ex2pe_obj.wait_front(onetile);
        for (auto block : generic::blocks(Wt, blk)) {
            reconfig_data_format(dfb_xmm, dfb_ex2pe);
            if constexpr (do_gamma == 0 && do_beta == 0) {
                pack_reconfig_data_format(dfb_out);
            } else {
                pack_reconfig_data_format(dfb_fusion);
            }

            mul_bcast_cols_init_short(dfb_xmm, dfb_ex2pe);
            tile_regs_acquire();
            for (auto i : block.local()) {
                // cb_xmm[wt+wtr] since we pop Wt from cb_xmm after the entire loop
                mul_tiles_bcast_cols(dfb_xmm, dfb_ex2pe, block.to_global(i), 0, i);
            }
            tile_regs_commit();

            dfb_im_or_out_obj.reserve_back(block.full_block_size());
            tile_regs_wait();
            for (auto i : block.local()) {
                pack_tile(i, dfb_im_or_out);  // pack either to intermediate (cb_fusion or out0)
            }
            tile_regs_release();
            dfb_im_or_out_obj.push_back(
                block.full_block_size());  // if no gamma/beta are provided, this will be passed on to the writer

            if constexpr (do_gamma) {
                if constexpr (do_beta == 0) {
                    pack_reconfig_data_format(dfb_out);
                }
                reconfig_data_format_srcb(dfb_ex2pe, dfb_gamma);
                uint32_t dfb_outg = do_beta ? dfb_fusion : dfb_out;
                DataflowBuffer dfb_outg_obj(dfb_outg);
                mul_bcast_rows_init_short(dfb_fusion, dfb_gamma);
                dfb_gamma_obj.wait_front(
                    block.start() + block.full_block_size());  // we don't pop, TODO: only wait on first ht
                dfb_fusion_obj.wait_front(block.full_block_size());
                tile_regs_acquire();
                for (auto i : block.local()) {
                    mul_tiles_bcast_rows(dfb_fusion, dfb_gamma, i, block.to_global(i), i);  // tile *= 1/(sum(exp(x)))
                }
                tile_regs_commit();
                // We don't pop gamma since it's 1,1,1,Wt and we reuse it for all NCHt
                dfb_fusion_obj.pop_front(block.full_block_size());

                dfb_outg_obj.reserve_back(block.full_block_size());
                tile_regs_wait();
                for (auto i : block.local()) {
                    pack_tile(i, dfb_outg);  // pack either to intermediate (cb_fusion or out0)
                }
                tile_regs_release();
                dfb_outg_obj.push_back(block.full_block_size());
            }
            if constexpr (do_beta) {
                pack_reconfig_data_format(dfb_out);
                if constexpr (do_gamma) {
                    reconfig_data_format_srcb(dfb_gamma, dfb_beta);
                } else {
                    reconfig_data_format_srcb(dfb_ex2pe, dfb_beta);
                }

                add_bcast_rows_init_short(dfb_fusion, dfb_beta);
                dfb_beta_obj.wait_front(
                    block.start() + block.full_block_size());  // TODO: optimization - only wait on first ht
                dfb_fusion_obj.wait_front(block.full_block_size());
                tile_regs_acquire();
                for (auto i : block.local()) {
                    add_tiles_bcast_rows(dfb_fusion, dfb_beta, i, block.to_global(i), i);  // tile *= 1/(sum(exp(x)))
                }
                tile_regs_commit();
                dfb_fusion_obj.pop_front(block.full_block_size());
                // We don't pop beta since it's 1,1,1,Wt and we reuse it for all NCHt

                dfb_out_obj.reserve_back(block.full_block_size());
                tile_regs_wait();
                for (auto i : block.local()) {
                    pack_tile(i, dfb_out);  // pack either to intermediate (cb_fusion or out0)
                }
                tile_regs_release();
                dfb_out_obj.push_back(block.full_block_size());
            }
        }
        dfb_ex2pe_obj.pop_front(onetile);
        dfb_xmm_obj.pop_front(total_buffer_size);

    }  // NCHt loop
}

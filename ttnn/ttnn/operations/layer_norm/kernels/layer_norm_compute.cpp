// Layer Norm - Compute Kernel
// Runs on math RISC-V core (TRISC), performs FPU/SFPU operations.
//
// Stage 2 (subtract_mean):
//   P1: reduce<SUM, REDUCE_ROW, WaitUpfrontNoPop>(cb_input -> cb_mean_rstd) with 1/W scaler
//   P2: sub<COL, NoWaitNoPop, WaitAndPopPerTile>(cb_input, cb_mean_rstd -> cb_output)
//       manual cb_pop_front(cb_input, Wt)

#include "api/compute/compute_kernel_hw_startup.h"
#include "ttnn/cpp/ttnn/kernel_lib/reduce_helpers_compute.hpp"
#include "ttnn/cpp/ttnn/kernel_lib/binary_op_helpers.hpp"

// Compile-time args
constexpr uint32_t Ht = get_compile_time_arg_val(0);
constexpr uint32_t Wt = get_compile_time_arg_val(1);
constexpr uint32_t has_gamma = get_compile_time_arg_val(2);
constexpr uint32_t has_beta = get_compile_time_arg_val(3);

// CB indices
constexpr uint32_t cb_input = 0;
constexpr uint32_t cb_scaler = 1;
constexpr uint32_t cb_eps = 2;
constexpr uint32_t cb_gamma = 3;
constexpr uint32_t cb_beta = 4;
constexpr uint32_t cb_output = 16;
constexpr uint32_t cb_mean_rstd = 24;
constexpr uint32_t cb_x_minus_mean = 25;
constexpr uint32_t cb_variance = 26;
constexpr uint32_t cb_diff_sq_temp = 27;

void kernel_main() {
    // Hardware startup -- configures unpacker, math pipeline, packer
    compute_kernel_hw_startup(cb_input, cb_scaler, cb_output);

    // Wait for persistent CBs (scaler generated by reader, never popped)
    cb_wait_front(cb_scaler, 1);

    // Main loop: Ht tile-rows
    for (uint32_t ht = 0; ht < Ht; ++ht) {
        // ============================================================
        // Phase 1: Mean reduction
        // reduce<SUM, REDUCE_ROW, WaitUpfrontNoPop>
        //   A: cb_input [Wt tiles, freshly pushed by reader, NoPop -- persist for P2]
        //   B: cb_scaler [1 tile, program-lifetime, never popped]
        //   Out: cb_mean_rstd [1 tile]
        // Scaler = 1/W, so SUM * (1/W) = mean
        // ============================================================
        compute_kernel_lib::
            reduce<PoolType::SUM, ReduceDim::REDUCE_ROW, compute_kernel_lib::ReduceInputPolicy::WaitUpfrontNoPop>(
                cb_input, cb_scaler, cb_mean_rstd, compute_kernel_lib::ReduceInputBlockShape::row(Wt));

        // ============================================================
        // Phase 2: Subtract mean (x - mean)
        // sub<COL, NoWaitNoPop, WaitAndPopPerTile>
        //   A: cb_input [Wt tiles, already waited from P1, NoWaitNoPop]
        //   B: cb_mean_rstd [1 tile, freshly pushed by P1, popped by helper]
        //   Out: cb_output [Wt tiles]
        // ============================================================
        compute_kernel_lib::sub<
            compute_kernel_lib::BroadcastDim::COL,
            compute_kernel_lib::BinaryInputPolicy::NoWaitNoPop,
            compute_kernel_lib::BinaryInputPolicy::WaitAndPopPerTile>(
            cb_input, cb_mean_rstd, cb_output, compute_kernel_lib::BinaryInputBlockShape::row(Wt));

        // Manual pop of cb_input -- neither P1 (WaitUpfrontNoPop) nor P2 (NoWaitNoPop) popped it
        cb_pop_front(cb_input, Wt);
    }
}

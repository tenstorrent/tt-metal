#pragma once

#include <iosfwd>
#include <string>
#include <string_view>
#include <unordered_map>
#include <vector>

namespace tt::jit_build {

using ParsedDependencies = std::unordered_map<std::string, std::vector<std::string>>;

// Suffix for the source-complete dependency-hash sidecar written next to an ELF by
// preprocess-and-ship. Unlike the link-inputs ".dephash", it lists every source, header, and link
// input, so a warmed cache's ELF can be validated for reuse without the intermediate objects.
inline constexpr std::string_view FULL_DEPHASH_SUFFIX = ".fulldephash";

// Parses a Makefile-style dependency file (generated by gcc -MMD)
ParsedDependencies parse_dependency_file(std::istream& file);

// Writes the hashes of the dependencies of `obj` to `hash_file`.
// Sets badbit on `hash_file` on any failure.
void write_dependency_hashes(
    const ParsedDependencies& dependencies,
    const std::string& out_dir,
    const std::string& obj,
    std::ostream& hash_file);

// Reads dependencies from .d file and writes their hashes to .hash file.
// Deletes the .hash file on any failure.
void write_dependency_hashes(const std::string& out_dir, const std::string& obj, const std::string& hash_path);

// Returns true if all dependencies' hashes match those stored in `hash_file`.
bool dependencies_up_to_date(std::istream& hash_file);

// Returns true if all dependencies' hashes match those stored in the .hash file.
bool dependencies_up_to_date(const std::string& out_dir, const std::string& obj);

// Returns true if all dependencies' hashes match those stored in the sidecar at `hash_file_path`.
// Returns false if the file is missing or malformed. Unlike the overload above, the caller supplies
// the full sidecar path directly (e.g. an ELF's FULL_DEPHASH_SUFFIX sidecar).
bool dependencies_up_to_date_file(const std::string& hash_file_path);

// Clears process-local cache of dependency file hashes.
void clear_file_hash_cache();

}  // namespace tt::jit_build

        sfpi::vFloat x = sfpi::dst_reg[0];
        sfpi::vFloat result = piecewise_polynomial_eval<ERF_NUM_DEGREE, ERF_NUM_SEGMENTS, ERF_LUT_SIZE>(ERF_LUT, x);

inline void calculate_digamma() {
    for (int d = 0; d < ITERATIONS; d++) {
        sfpi::vFloat x = sfpi::dst_reg[0];
        sfpi::vFloat result = piecewise_polynomial_eval<DIGAMMA_NUM_DEGREE, DIGAMMA_NUM_SEGMENTS, DIGAMMA_LUT_SIZE>(DIGAMMA_LUT, x);
        sfpi::dst_reg[0] = result;
        sfpi::dst_reg++;
    }
}


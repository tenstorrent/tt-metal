def _load_env_override() -> dict[str, Any] | None:
    raw = os.environ.get("HY_PATCH_EMBED_CONV_OVERRIDE")
    if not raw:
        return None
    return json.loads(raw)


def _load_env_configs_json() -> dict[str, dict[str, Any]] | None:
    path = os.environ.get("HY_PATCH_EMBED_CONV_CONFIGS_JSON")
    if not path:
        return None
    with open(path) as f:
        data = json.load(f)
    if not isinstance(data, dict):
        raise ValueError(f"HY_PATCH_EMBED_CONV_CONFIGS_JSON must be a dict, got {type(data)}")
    return data


def resolve_layer_config(layer_name: str) -> dict[str, Any]:
    """Resolve config dict for ``layer_name`` (env override > JSON file > winners > default)."""
    override = _load_env_override()
    if override is not None:
        return {**DEFAULT_PATCH_EMBED_CONV_CONFIGS.get(layer_name, {}), **override}

    file_cfgs = _load_env_configs_json()
    if file_cfgs is not None and layer_name in file_cfgs:
        return {**DEFAULT_PATCH_EMBED_CONV_CONFIGS.get(layer_name, {}), **file_cfgs[layer_name]}

    if layer_name in PATCH_EMBED_CONV_CONFIGS:
        return {**DEFAULT_PATCH_EMBED_CONV_CONFIGS.get(layer_name, {}), **PATCH_EMBED_CONV_CONFIGS[layer_name]}

    return dict(DEFAULT_PATCH_EMBED_CONV_CONFIGS.get(layer_name, DEFAULT_PATCH_EMBED_CONV_CONFIGS["conv0"]))


def config_dict_to_conv2d_config(cfg: dict[str, Any], *, dtype) -> ttnn.Conv2dConfig:
    """Build ``ttnn.Conv2dConfig`` from a JSON-friendly dict."""
    layout_key = cfg.get("shard_layout", "auto")
    if layout_key not in _SHARD_LAYOUT_MAP:
        raise ValueError(f"unknown shard_layout {layout_key!r}; expected one of {sorted(_SHARD_LAYOUT_MAP)}")
    shard_layout = _SHARD_LAYOUT_MAP[layout_key]
    kwargs: dict[str, Any] = {
        "weights_dtype": dtype,
        "shard_layout": shard_layout,
        "act_block_h_override": int(cfg.get("act_block_h_override", 0)),
        "enable_act_double_buffer": bool(cfg.get("enable_act_double_buffer", False)),
        "deallocate_activation": bool(cfg.get("deallocate_activation", False)),
        "enable_weights_double_buffer": bool(cfg.get("enable_weights_double_buffer", False)),
        "reallocate_halo_output": bool(cfg.get("reallocate_halo_output", False)),
    }
    return ttnn.Conv2dConfig(**kwargs)


def make_layer_conv2d_config(layer_name: str, *, dtype, sharded: bool = True) -> ttnn.Conv2dConfig:
    """Resolve and build Conv2dConfig for a named patch_embed/final_layer conv."""
    if not sharded:
        return ttnn.Conv2dConfig(weights_dtype=dtype, shard_layout=None)
    return config_dict_to_conv2d_config(resolve_layer_config(layer_name), dtype=dtype)


def iter_sweep_config_dicts(*, skip_height: bool | None = None) -> list[dict[str, Any]]:
    """Cartesian product of knobs for the sweep harness (skips useless WIDTH×ABH combos).

    HEIGHT_SHARDED OOMs on BH for the wide patch_embed shapes (1024/4096-ch @ 8×8).
    Skip it by default; set HY_PATCH_EMBED_CONV_SWEEP_INCLUDE_HEIGHT=1 to force.
    """
    if skip_height is None:
        skip_height = os.environ.get("HY_PATCH_EMBED_CONV_SWEEP_INCLUDE_HEIGHT", "0") == "0"
    layouts = ("auto", "width", "block") if skip_height else ("auto", "height", "width", "block")
    act_block_hs = (0, 32, 64)
    bools = (False, True)
    out: list[dict[str, Any]] = []
    for layout in layouts:
        abhs = (0,) if layout == "width" else act_block_hs
        for abh in abhs:
            for adb in bools:
                for dealloc in bools:
                    for wdb in bools:
                        # weights double-buffer is for block sharding; skip elsewhere.
                        if wdb and layout != "block":
                            continue
                        out.append(
                            {
                                "shard_layout": layout,
                                "act_block_h_override": abh,
                                "enable_act_double_buffer": adb,
                                "deallocate_activation": dealloc,
                                "enable_weights_double_buffer": wdb,
                                "reallocate_halo_output": dealloc,  # pair with dealloc as TTNN recommends
                            }
                        )
    return out


def config_id(cfg: dict[str, Any]) -> str:
    return (
        f"{cfg['shard_layout']}"
        f"_abh{cfg['act_block_h_override']}"
        f"_adb{int(cfg['enable_act_double_buffer'])}"
        f"_da{int(cfg['deallocate_activation'])}"
        f"_wdb{int(cfg.get('enable_weights_double_buffer', False))}"
    )

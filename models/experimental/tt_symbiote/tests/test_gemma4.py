"""Test for Gemma4 31B-IT (instruction-tuned) model with TTNN backend.

Gemma4 31B is a dense multimodal model with:
- 60 heterogeneous decoder layers (50 sliding-window + 10 global attention)
- Sliding attention: 32Q/16KV heads, head_dim=256, window=1024
- Global attention: 32Q/4KV heads, head_dim=512, K=V sharing, partial RoPE
- GeGLU FFN (intermediate_size=21504)
- V-norm (RMSNorm without learnable scale) on all layers
- Logit soft-capping (30.0)
"""

import os
import time

import pytest
import torch
from tqdm import tqdm
from transformers import AutoModelForCausalLM, AutoTokenizer

import ttnn
from models.experimental.tt_symbiote.core.run_config import DispatchManager
from models.experimental.tt_symbiote.utils.device_management import set_device
from models.experimental.tt_symbiote.utils.module_replacement import register_module_replacement_dict
import transformers
from models.experimental.tt_symbiote.core.run_config import TracedRun
from models.experimental.tt_symbiote.modules.linear import TTNNLinearIColShardedWRowSharded

assert transformers.__version__.startswith(
    "5."
), "This test requires transformers version 5.0.0.dev0. Try: `pip install git+https://github.com/huggingface/transformers.git`"


MESH_DEVICE_MAP = {
    "N150": (1, 1),
    "N300": (1, 2),
    "N150x4": (1, 4),
    "T3K": (1, 8),
    "TG": (8, 4),
    "P150": (1, 1),
    "P300": (1, 2),
    "P150x4": (1, 4),
    "P150x8": (1, 8),
    "BHGLX": (8, 4),
}


@pytest.mark.parametrize(
    "device_params",
    [{"trace_region_size": 200000000, "num_command_queues": 1, "fabric_config": ttnn.FabricConfig.FABRIC_1D_RING}],
    indirect=True,
)
@pytest.mark.parametrize(
    "mesh_device",
    [MESH_DEVICE_MAP.get(os.environ.get("MESH_DEVICE"), len(ttnn.get_device_ids()))],
    indirect=True,
)
@pytest.mark.parametrize("max_new_tokens", [1024], ids=["1024tok"])
def test_gemma4(mesh_device, max_new_tokens):
    """Test Gemma4 31B-IT (instruction-tuned) model with TTNN acceleration.

    Gemma4 31B-IT is the instruction-tuned variant of the base Gemma4 31B checkpoint.
    Architecture is identical to the base model (60 heterogeneous decoder layers):
    - 50 sliding-window attention layers (16 KV heads, head_dim=256, window=1024)
    - 10 global attention layers (4 KV heads, head_dim=512, K=V sharing, partial RoPE)
    - GeGLU FFN on all layers
    - Layer pattern: 5 sliding then 1 global, repeated 10 times
    The tokenizer ships with chat_template.jinja; apply_chat_template() is used for
    all prompt construction. Generation stops at <eos> (token 1) or <turn|> (token 106).
    """

    torch.set_grad_enabled(False)
    model_name = "google/gemma-4-31B-it"
    print(f"Loading model {model_name}...")
    tokenizer = AutoTokenizer.from_pretrained(model_name)
    # google/gemma-4-31B-it has model_type="gemma4" which maps to Gemma4ForConditionalGeneration
    # in both AutoModelForCausalLM and AutoModelForImageTextToText — they resolve to the same class.
    # AutoModelForCausalLM is used here; the loaded class is identical either way.
    model = AutoModelForCausalLM.from_pretrained(model_name, torch_dtype=torch.bfloat16)

    # Phase 2: TTNN module replacements (follows Ling 3-pass pattern)
    from models.experimental.tt_symbiote.modules.gemma4_attention import (
        TTNNGemma4PagedAttentionKVCache,
    )
    from models.experimental.tt_symbiote.modules.gemma4_modules import (
        TTNNGemma4ScaledEmbedding,
        TTNNGemma4DecoderLayer,
    )
    from models.experimental.tt_symbiote.modules.normalization import TTNNDistributedRMSNorm
    from models.experimental.tt_symbiote.models.gemma4_text import TTNNGemma4TextModel, TTNNGemma4ForCausalLM

    # Get HF classes from the loaded model
    # Gemma4 is multimodal: model.model is Gemma4Model (wrapper), text model is at language_model
    text_model = model.model.language_model
    decoder_class = text_model.layers[0].__class__  # Gemma4TextDecoderLayer
    norm_class = text_model.layers[0].input_layernorm.__class__  # Gemma4RMSNorm
    embed_class = text_model.embed_tokens.__class__  # Gemma4TextScaledWordEmbedding
    text_model_class = text_model.__class__  # Gemma4TextModel

    print(f"Decoder class: {decoder_class.__name__}")
    print(f"Norm class: {norm_class.__name__}")
    print(f"Embed class: {embed_class.__name__}")

    # The -it tokenizer ships with chat_template.jinja loaded automatically by AutoTokenizer.
    # apply_chat_template produces: <bos><|turn>user\n{msg}<turn|>\n<|turn>model\n<|channel>thought\n<channel|>
    # The <|channel>thought\n<channel|> suffix is a template-level no-op marker that suppresses
    # thinking mode — it is emitted by the template, not generated by the model.
    msg_128 = (
        "What is your favorite condiment? There are so many condiments to choose from, each bringing "
        "its unique flavor and texture to enhance different dishes. Do you prefer the classic taste of "
        "ketchup, the creamy richness of mayonnaise, the spicy kick of mustard, or perhaps something "
        "more exotic like sriracha or hoisin sauce? Maybe you enjoy the tangy zest of salsa or the "
        "smooth and savory taste of aioli. Share what your favorite condiment is and why you love it. "
        "Does it remind you of a specific dish or meal?"
    )
    msg_1024 = (
        "Can you provide a detailed analysis of the impact of climate change on global agriculture, "
        "including specific examples of how changing weather patterns, increased frequency of extreme "
        "events, and shifting growing seasons are affecting crop yields and food security in different "
        "regions around the world? Additionally, please discuss potential adaptation strategies that "
        "farmers and policymakers can implement to mitigate these impacts and ensure sustainable food "
        "production in the face of ongoing climate change."
    )
    msg = msg_128 if max_new_tokens == 128 else msg_1024
    messages = [{"role": "user", "content": msg}]
    prompt = tokenizer.apply_chat_template(
        messages,
        tokenize=False,
        add_generation_prompt=True,
        enable_thinking=False,
    )
    # Use first parameter's device (model.device may not exist for multi-modal models)
    model_device = next(model.parameters()).device
    inputs = tokenizer(prompt, return_tensors="pt").to(model_device)

    # Some tokenizers produce token_type_ids that the model does not expect
    if "token_type_ids" in inputs:
        del inputs["token_type_ids"]

    # Three-pass replacement matching Ling pattern.
    # Gemma4RMSNorm is shared between language_model and vision_tower, but
    # vision tower norms have incompatible dims for 8-device sharding.
    # Exclude all vision_tower and embed_vision modules from replacement.
    exclude_vision = {name for name, _ in model.named_modules() if "vision_tower" in name or "embed_vision" in name}

    # Pass 1: decoder layers, final norm, embedding
    nn_to_ttnn = {
        decoder_class: TTNNGemma4DecoderLayer,
        norm_class: TTNNDistributedRMSNorm,
        embed_class: TTNNGemma4ScaledEmbedding,
    }
    modules = register_module_replacement_dict(model, nn_to_ttnn, model_config=None, exclude_replacement=exclude_vision)

    # Exclude lm_head from the generic linear replacement; handle it separately.
    # lm_head uses TTNNLinearIColShardedWRowSharded:
    #   - col-sharded input (device i has hidden[:, :, 672*i:672*(i+1)])
    #   - col-sharded weight (device i has weight rows for its input slice)
    #   - reduce_scatter → each device holds [1, 1, 32768] (vocab/8)
    # ConcatMesh2dToTensor assembles 8 × [1,1,32768] → [1,1,262144] correctly.
    # (AllReduced would leave each device with full [1,1,262144]; concatenation
    #  would then produce [1,1,2097152] → wrong argmax → garbage tokens.)
    nn_to_ttnn2 = {
        torch.nn.Linear: TTNNLinearIColShardedWRowSharded,
    }
    modules.update(register_module_replacement_dict(model, nn_to_ttnn2, model_config=None))

    # Pass 3: model wrapper — replaces Gemma4TextModel with TTNNGemma4TextModel
    # which handles input_ids→embedding conversion (trace-safe) and iterates
    # layers without ModuleList slicing.
    nn_to_ttnn_model = {
        text_model_class: TTNNGemma4TextModel,
    }
    modules.update(register_module_replacement_dict(model, nn_to_ttnn_model, model_config=None))

    # Pass 4: CausalLM wrapper — handles lm_head + logit soft-capping on CPU.
    # HF's TorchTTNNTensor dispatch is broken for softcapping ops (div/tanh/mul).
    causal_lm_wrapper = TTNNGemma4ForCausalLM.from_torch(model)
    causal_lm_wrapper._unique_name = "TTNNGemma4ForCausalLM"
    modules[causal_lm_wrapper.module_name] = causal_lm_wrapper

    set_device(model, mesh_device)

    causal_lm_wrapper.to_device(mesh_device)

    # Enable fp32 accumulation on all linear layers to match CPU's float32
    # internal accumulation for bfloat16 matmuls. Walk ALL sub-modules
    # recursively since the modules dict only contains top-level replacements,
    # not linear layers nested inside decoder layers (QKV, O, gate_up, down).
    fp32_kernel_config = ttnn.init_device_compute_kernel_config(
        mesh_device.arch(),
        math_fidelity=ttnn.MathFidelity.HiFi4,
        math_approx_mode=False,
        fp32_dest_acc_en=True,
        packer_l1_acc=True,
    )
    from models.experimental.tt_symbiote.core.module import TTNNModule

    fp32_count = 0
    visited = set()

    def _set_fp32_recursive(obj):
        nonlocal fp32_count
        if id(obj) in visited:
            return
        visited.add(id(obj))
        if hasattr(obj, "compute_kernel_config") and not callable(getattr(obj, "compute_kernel_config")):
            obj.compute_kernel_config = fp32_kernel_config
            fp32_count += 1
        for attr_name in vars(obj):
            attr = getattr(obj, attr_name, None)
            if isinstance(attr, TTNNModule):
                _set_fp32_recursive(attr)

    for v in modules.values():
        _set_fp32_recursive(v)
    print(f"Set fp32 accumulation on {fp32_count} linear modules")

    # Create paged KV cache for Gemma4 (dual cache: sliding + global)
    text_config = model.config.text_config
    global_indices = {i for i, lt in enumerate(text_config.layer_types) if lt == "full_attention"}
    kv_cache = TTNNGemma4PagedAttentionKVCache(
        text_config=text_config,
        global_layer_indices=global_indices,
        device=mesh_device,
        sliding_max_num_blocks=16,
        global_max_num_blocks=128,
    )
    kv_cache.to_device(mesh_device)

    print(f"Preprocessing {len(modules)} TTNN modules weights...")
    for k, v in tqdm(modules.items()):
        v.preprocess_weights()
        v.move_weights_to_device()

    print("Running inference...")

    # For gemma-4-31B-it, generation stops at <eos> (token 1) or <turn|> (token 106).
    # tokenizer.eot_token_id == 106 == <turn|>; this is the natural end-of-model-turn marker.
    eos_token_ids = {tokenizer.eos_token_id, tokenizer.eot_token_id}

    # --- Warmup Phase 1: JIT priming (prefill + 2 decode steps) ---
    cur_token, _, _ = causal_lm_wrapper.decode_one_step(
        input_ids=inputs["input_ids"],
        past_key_values=kv_cache,
        use_cache=True,
        logits_to_keep=1,
    )
    for _ in range(1):
        token_input = torch.tensor([[cur_token]], dtype=torch.long)
        cur_token, _, _ = causal_lm_wrapper.decode_one_step(
            input_ids=token_input,
            past_key_values=kv_cache,
            use_cache=True,
        )
    kv_cache.reset()
    TracedRun.release_all()

    # --- Warmup Phase 2: trace capture (prefill + 4 decode steps) ---
    cur_token, _, _ = causal_lm_wrapper.decode_one_step(
        input_ids=inputs["input_ids"],
        past_key_values=kv_cache,
        use_cache=True,
        logits_to_keep=1,
    )
    for _ in range(3):
        token_input = torch.tensor([[cur_token]], dtype=torch.long)
        cur_token, _, _ = causal_lm_wrapper.decode_one_step(
            input_ids=token_input,
            past_key_values=kv_cache,
            use_cache=True,
        )
    kv_cache.reset()

    # --- Actual generation (on-device argmax, 4-byte D2H per step) ---
    DispatchManager.clear_timings()
    prefill_len = inputs["input_ids"].shape[1]

    prefill_start = time.perf_counter()
    cur_token, _, _ = causal_lm_wrapper.decode_one_step(
        input_ids=inputs["input_ids"],
        past_key_values=kv_cache,
        use_cache=True,
        logits_to_keep=1,
    )
    ttnn.synchronize_device(mesh_device)
    prefill_end = time.perf_counter()

    generated_ids = [cur_token]

    decode_start = time.perf_counter()
    for step in range(max_new_tokens - 1):
        print(f"Decoding step {step+1} (token_id={cur_token})...")
        token_input = torch.tensor([[cur_token]], dtype=torch.long)
        cur_token, _, _ = causal_lm_wrapper.decode_one_step(
            input_ids=token_input,
            past_key_values=kv_cache,
            use_cache=True,
        )
        generated_ids.append(cur_token)
        if cur_token in eos_token_ids:
            break

    ttnn.synchronize_device(mesh_device)
    decode_end = time.perf_counter()

    prefill_time = prefill_end - prefill_start
    decode_time = decode_end - decode_start
    total_time = prefill_time + decode_time
    num_decode_tokens = len(generated_ids)
    decode_tps = num_decode_tokens / decode_time if decode_time > 0 else 0
    prefill_tps = prefill_len / prefill_time if prefill_time > 0 else 0

    generated_text = tokenizer.decode(generated_ids)
    print(f"Gemma4 OUTPUT: {generated_text}")
    print(f"Prefill: {prefill_len} tokens in {prefill_time:.2f}s ({prefill_tps:.2f} t/s)")
    print(f"Decode:  {num_decode_tokens} tokens in {decode_time:.2f}s ({decode_tps:.2f} t/s)")
    print(f"Total:   {prefill_len + num_decode_tokens} tokens in {total_time:.2f}s")

    assert len(generated_text.strip()) > 0, "Generated output should not be empty"

    DispatchManager.save_stats_to_file("gemma4_timing_stats.csv")
    TracedRun.release_all()

"""
Per-layer KV cache accuracy test for GPT-OSS prefill.

For each decoder layer, compares the K and V tensors written into the TT KV cache
during prefill against the HF reference oracle.  A mismatch in an early layer quickly
localises bugs in QKV projection, RoPE, or ttnn.fill_cache.

Requires oracle data generated by hf_reference_oracle.py --save-kv:
  python3 models/demos/gpt_oss_d_p/tests/accuracy/hf_reference_oracle.py \\
      --out /data/jmalone/gpt_oss_ref \\
      --prompt "What are the prime factors of 1?" \\
      --save-kv --max-layers 5

Then run this test (1×8 mesh, TP=8):
  export HF_MODEL=/data/jmalone/.cache/huggingface/hub/...
  export TT_MESH_GRAPH_DESC_PATH=...
  python3 models/demos/gpt_oss_d_p/tests/accuracy/kv_cache_prefill.py \\
      --rows 1 --cols 8 \\
      --oracle-dir /data/jmalone/gpt_oss_ref \\
      --prompt "What are the prime factors of 1?" \\
      --max-layers 5

Notes:
  - Non-paged attention only. Paged cache has a different block layout and requires
    a page_table to reconstruct; handle separately if needed.
  - SP>1 (multi-row mesh): the forward pass splits the sequence across rows, but the
    KV cache is not SP-sharded. Each row writes its shard of positions independently.
    For SP>1 this script reads row-0 only (which holds positions 0..isl_per_row-1).
    Full SP>1 KV gather support is a future extension.
  - The TT cache is stored in bfloat8_b after ttnn.fill_cache; expect PCC ~0.98-0.99
    vs the bf16 HF reference rather than near 1.0.
"""

import argparse
import gc
import json
import pathlib
import sys

import numpy as np
import torch

import ttnn

PCC_THRESHOLD = 0.97


def _pcc(a: torch.Tensor, b: torch.Tensor) -> float:
    a = a.flatten().float()
    b = b.flatten().float()
    am = a - a.mean()
    bm = b - b.mean()
    denom = am.norm() * bm.norm()
    return float((am * bm).sum() / (denom + 1e-8))


def _sp_shard_embed(mesh, model, padded_ids, sp, isl_per_row):
    t = torch.tensor(padded_ids, dtype=torch.int64).reshape(sp, 1, 1, isl_per_row)
    tokens = ttnn.from_torch(
        t,
        device=mesh,
        dtype=ttnn.uint32,
        layout=ttnn.ROW_MAJOR_LAYOUT,
        mesh_mapper=ttnn.ShardTensor2dMesh(mesh, mesh_shape=mesh.shape, dims=(0, None)),
    )
    emb = ttnn.embedding(tokens, model.embedding_weight, layout=ttnn.TILE_LAYOUT, dtype=ttnn.bfloat8_b)
    tokens.deallocate(True)
    if len(emb.shape) == 3:
        emb = ttnn.unsqueeze_to_4D(emb)
    return emb


def _gather_kv_cache(cache_tensor, mesh, real_len):
    """Read K or V cache back to CPU as float32 [num_kv_heads, real_len, head_dim].

    The cache is TP-sharded: column i holds heads [i*local_kv_heads, (i+1)*local_kv_heads).
    All mesh rows hold identical copies for non-users_row_sharded caches (batch=1), so we
    read only row-0 and concatenate along the heads dim (dim 1).

    cache_tensor per device shape: [batch, local_kv_heads, max_seq_len, head_dim]
    """
    sp, tp = mesh.shape
    device_tensors = ttnn.get_device_tensors(cache_tensor)
    col_slices = []
    for col in range(tp):
        t = ttnn.to_torch(device_tensors[col]).float()  # [batch, local_kv_heads, max_seq, head_dim]
        col_slices.append(t[0])  # [local_kv_heads, max_seq, head_dim]
    full = torch.cat(col_slices, dim=0)  # [num_kv_heads, max_seq, head_dim]
    return full[:, :real_len, :]  # [num_kv_heads, real_len, head_dim]


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--rows", type=int, default=1)
    ap.add_argument("--cols", type=int, default=8)
    ap.add_argument("--prompt", type=str, default="What are the prime factors of 1?")
    ap.add_argument(
        "--oracle-dir",
        type=str,
        required=True,
        help="directory containing ref_results.json produced by hf_reference_oracle.py --save-kv",
    )
    ap.add_argument(
        "--max-layers",
        type=int,
        default=None,
        help="compare only the first N layers (default: all layers present in oracle)",
    )
    ap.add_argument(
        "--pcc-threshold",
        type=float,
        default=PCC_THRESHOLD,
        help=f"minimum PCC for a layer to pass (default: {PCC_THRESHOLD})",
    )
    args = ap.parse_args()

    oracle_dir = pathlib.Path(args.oracle_dir)
    results_path = oracle_dir / "ref_results.json"
    if not results_path.exists():
        print(f"[kv_cache] ERROR: {results_path} not found. Run hf_reference_oracle.py first.", flush=True)
        return 1

    with results_path.open() as f:
        records = json.load(f)

    record = next((r for r in records if r.get("prompt") == args.prompt), None)
    if record is None:
        print(f"[kv_cache] ERROR: no oracle record for prompt {args.prompt!r}", flush=True)
        return 1

    if "kv_files" not in record or not record["kv_files"]:
        print(
            "[kv_cache] ERROR: oracle record has no kv_files. " "Re-run hf_reference_oracle.py with --save-kv.",
            flush=True,
        )
        return 1

    available_layers = sorted(int(k) for k in record["kv_files"])
    if args.max_layers is not None:
        layers_to_test = [i for i in available_layers if i < args.max_layers]
    else:
        layers_to_test = available_layers

    if not layers_to_test:
        print("[kv_cache] ERROR: no layers to test (check --max-layers vs oracle layer count)", flush=True)
        return 1

    print(
        f"[kv_cache] testing KV cache for layers {layers_to_test[0]}..{layers_to_test[-1]} "
        f"({len(layers_to_test)} total) against oracle",
        flush=True,
    )

    from transformers import AutoConfig, AutoTokenizer

    from models.common.utility_functions import is_blackhole
    from models.demos.gpt_oss.config import MeshConfig, ModeConfig
    from models.demos.gpt_oss.tt.model_config import ModelArgs
    from models.demos.gpt_oss.utils.general_utils import get_default_num_links
    from models.demos.gpt_oss_d_p.tt.ccl import CCLManager
    from models.demos.gpt_oss_d_p.tt.model import Model

    shape = (args.rows, args.cols)
    sp = shape[0]

    if shape == (1, 1):
        fabric = None
        topology = ttnn.Topology.Linear
    elif is_blackhole():
        fabric = ttnn.FabricConfig.FABRIC_1D
        topology = ttnn.Topology.Linear
    else:
        fabric = ttnn.FabricConfig.FABRIC_1D_RING
        topology = ttnn.Topology.Ring

    print(f"[kv_cache] mesh={shape} SP={shape[0]} TP={shape[1]} fabric={fabric}", flush=True)

    if fabric is not None:
        ttnn.set_fabric_config(fabric)
    mesh = ttnn.open_mesh_device(ttnn.MeshShape(*shape))
    print(f"[kv_cache] mesh opened: {tuple(mesh.shape)} ndev={mesh.get_num_devices()}", flush=True)

    try:
        model_args = ModelArgs(mesh_device=mesh)
        hf_config = AutoConfig.from_pretrained(model_args.model_path, trust_remote_code=True)
        tok = AutoTokenizer.from_pretrained(model_args.model_path, trust_remote_code=True)

        ids = tok(args.prompt)["input_ids"]
        real_len = len(ids)
        block = sp * 32
        max_seq_len = ((real_len + block - 1) // block) * block
        isl_per_row = max_seq_len // sp
        padded = ids + [tok.pad_token_id or 1] * (max_seq_len - real_len)
        print(f"[kv_cache] prompt_tokens={real_len} padded_seq={max_seq_len}", flush=True)

        state_dict = ModelArgs.load_state_dict(model_args.weights_path, dummy_weights=False)
        cache = model_args.weight_cache_path(ttnn.bfloat8_b)
        mesh_config = MeshConfig(
            shape, decode=ModeConfig(tp=shape[1], ep=shape[0]), prefill=ModeConfig(tp=shape[1], sp=1, ep=1)
        )
        ccl = CCLManager(mesh, num_links=get_default_num_links(mesh), topology=topology)

        model = Model(
            mesh_device=mesh,
            hf_config=hf_config,
            state_dict=state_dict,
            ccl_manager=ccl,
            mesh_config=mesh_config,
            tensor_cache_path=str(cache),
            create_kv_cache=True,
            max_local_batch_size=1,
        )
        del state_dict
        gc.collect()
        print("[kv_cache] model built; weights on device", flush=True)

        emb = _sp_shard_embed(mesh, model, padded, sp, isl_per_row)

        seq_len = emb.shape[-2]
        rope_mats = [
            model.rope_setup.cos_matrix_prefill[:, :, :seq_len, :],
            model.rope_setup.sin_matrix_prefill[:, :, :seq_len, :],
        ]

        hidden_states = emb
        results_table: list[tuple[int, float, float, bool]] = []
        any_fail = False

        max_layer_idx = max(layers_to_test)

        for i, decoder_layer in enumerate(model.layers):
            layer_kv = decoder_layer.self_attn.kv_cache  # [k_cache, v_cache] on device

            hidden_states = decoder_layer(
                hidden_states,
                position_embeddings=rope_mats,
                position_idx=None,
                page_table=None,
                kv_cache=layer_kv,
                is_decode=False,
            )

            if i not in layers_to_test:
                if i > max_layer_idx:
                    break
                continue

            ttnn.synchronize_device(mesh)
            k_cache, v_cache = layer_kv

            tt_k = _gather_kv_cache(k_cache, mesh, real_len)  # [num_kv_heads, real_len, head_dim]
            tt_v = _gather_kv_cache(v_cache, mesh, real_len)

            oracle_k = torch.tensor(
                np.load(oracle_dir / record["kv_files"][str(i)]["k"]), dtype=torch.float32
            )  # [num_kv_heads, seq_len, head_dim]
            oracle_v = torch.tensor(np.load(oracle_dir / record["kv_files"][str(i)]["v"]), dtype=torch.float32)

            pcc_k = _pcc(tt_k, oracle_k)
            pcc_v = _pcc(tt_v, oracle_v)
            passed = pcc_k >= args.pcc_threshold and pcc_v >= args.pcc_threshold
            if not passed:
                any_fail = True
            results_table.append((i, pcc_k, pcc_v, passed))
            status = "PASS" if passed else "FAIL"
            print(f"[kv_cache] layer {i:3d}: K PCC={pcc_k:.4f}  V PCC={pcc_v:.4f}  {status}", flush=True)

            if i >= max_layer_idx:
                hidden_states.deallocate(True)
                break

        print("", flush=True)
        print(f"{'Layer':>6}  {'K PCC':>8}  {'V PCC':>8}  {'Status'}", flush=True)
        print("-" * 38, flush=True)
        for layer_idx, pcc_k, pcc_v, passed in results_table:
            print(f"{layer_idx:>6}  {pcc_k:>8.4f}  {pcc_v:>8.4f}  {'PASS' if passed else 'FAIL'}", flush=True)

        if any_fail:
            failed = [i for i, _, _, p in results_table if not p]
            print(
                f"\n[kv_cache] FAIL — {len(failed)} layer(s) below PCC threshold {args.pcc_threshold}: {failed}",
                flush=True,
            )
            return 1

        print(
            f"\n[kv_cache] PASS — all {len(results_table)} layer(s) above PCC threshold {args.pcc_threshold}",
            flush=True,
        )
        return 0

    finally:
        ttnn.close_mesh_device(mesh)


if __name__ == "__main__":
    sys.exit(main())

"""
Per-layer activation accuracy test for GPT-OSS prefill.

For each decoder layer, compares the hidden-state output of the TT model against the
HF reference oracle.  A mismatch in an early layer localises divergence quickly; all
layers above PCC_THRESHOLD must pass for the test to succeed.

Requires oracle data generated by hf_reference_oracle.py --save-layers:
  python3 models/demos/gpt_oss_d_p/tests/accuracy/hf_reference_oracle.py \\
      --out /data/jmalone/gpt_oss_ref \\
      --prompt "What are the prime factors of 1?" \\
      --save-layers --max-layers 5

Then run this test (1×8 mesh, TP=8):
  export HF_MODEL=/data/jmalone/.cache/huggingface/hub/...
  export TT_MESH_GRAPH_DESC_PATH=...
  python3 models/demos/gpt_oss_d_p/tests/accuracy/layer_activations_prefill.py \\
      --oracle-dir /data/jmalone/gpt_oss_ref \\
      --prompt "What are the prime factors of 1?" \\
      --max-layers 5

Notes:
  - Only SP=1 (single-row) meshes are supported.  Use --cols 8 (default) for a 1×8
    T3K mesh or --cols 1 for a single card.
  - kv_cache is not populated; SDPA operates on the freshly-computed K/V directly so
    hidden-state activations are identical to the cache-filling path.
"""

import argparse
import gc
import hashlib
import json
import pathlib
import sys

import numpy as np
import torch

import ttnn

PCC_THRESHOLD = 0.97


def _pcc(a: torch.Tensor, b: torch.Tensor) -> float:
    a = a.flatten().float()
    b = b.flatten().float()
    am = a - a.mean()
    bm = b - b.mean()
    denom = am.norm() * bm.norm()
    return float((am * bm).sum() / (denom + 1e-8))


def _sp_shard_embed(mesh, model, padded_ids, sp, isl_per_row):
    t = torch.tensor(padded_ids, dtype=torch.int64).reshape(sp, 1, 1, isl_per_row)
    tokens = ttnn.from_torch(
        t,
        device=mesh,
        dtype=ttnn.uint32,
        layout=ttnn.ROW_MAJOR_LAYOUT,
        mesh_mapper=ttnn.ShardTensor2dMesh(mesh, mesh_shape=mesh.shape, dims=(0, None)),
    )
    emb = ttnn.embedding(tokens, model.embedding_weight, layout=ttnn.TILE_LAYOUT, dtype=ttnn.bfloat8_b)
    tokens.deallocate(True)
    if len(emb.shape) == 3:
        emb = ttnn.unsqueeze_to_4D(emb)
    return emb


def _gather_hidden_states(tt_tensor, mesh) -> torch.Tensor:
    """Return hidden states as a CPU float32 tensor [seq_len, hidden_size].

    Between layers, each TP device holds a full-width replicated copy of the
    hidden states ([1, 1, seq_len, hidden_size]).  We read from device 0
    (first device in the first SP row).  Only SP=1 meshes are supported.
    """
    device_tensors = ttnn.get_device_tensors(tt_tensor)
    hs = ttnn.to_torch(device_tensors[0]).float()  # [1, 1, seq_len, hidden_size]
    return hs[0, 0]  # [seq_len, hidden_size]


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--rows", type=int, default=1)
    ap.add_argument("--cols", type=int, default=8)
    ap.add_argument("--prompt", type=str, default="What are the prime factors of 1?")
    ap.add_argument(
        "--oracle-dir",
        type=str,
        required=True,
        help="directory containing ref_results.json produced by hf_reference_oracle.py --save-layers",
    )
    ap.add_argument(
        "--max-layers",
        type=int,
        default=None,
        help="compare only the first N layers (default: all layers present in oracle)",
    )
    ap.add_argument(
        "--pcc-threshold",
        type=float,
        default=PCC_THRESHOLD,
        help=f"minimum PCC for a layer to pass (default: {PCC_THRESHOLD})",
    )
    args = ap.parse_args()

    if args.rows > 1:
        print(
            f"[layer_act] ERROR: SP={args.rows} (multi-row mesh) is not supported by this test. "
            "Use --rows 1 (default) with --cols 8 or --cols 1.",
            flush=True,
        )
        return 1

    oracle_dir = pathlib.Path(args.oracle_dir)
    results_path = oracle_dir / "ref_results.json"
    if not results_path.exists():
        print(f"[layer_act] ERROR: {results_path} not found. Run hf_reference_oracle.py first.", flush=True)
        return 1

    with results_path.open() as f:
        records = json.load(f)

    key = hashlib.sha256(args.prompt.encode()).hexdigest()[:12]
    record = next((r for r in records if r.get("prompt") == args.prompt), None)
    if record is None:
        print(f"[layer_act] ERROR: no oracle record for prompt {args.prompt!r}", flush=True)
        return 1

    if "layer_files" not in record or not record["layer_files"]:
        print(
            "[layer_act] ERROR: oracle record has no layer_files. " "Re-run hf_reference_oracle.py with --save-layers.",
            flush=True,
        )
        return 1

    available_layers = sorted(int(k) for k in record["layer_files"])
    if args.max_layers is not None:
        layers_to_test = [i for i in available_layers if i < args.max_layers]
    else:
        layers_to_test = available_layers

    if not layers_to_test:
        print("[layer_act] ERROR: no layers to test (check --max-layers vs oracle layer count)", flush=True)
        return 1

    print(
        f"[layer_act] testing layers {layers_to_test[0]}..{layers_to_test[-1]} "
        f"({len(layers_to_test)} total) against oracle",
        flush=True,
    )

    from transformers import AutoConfig, AutoTokenizer

    from models.common.utility_functions import is_blackhole
    from models.demos.gpt_oss.config import MeshConfig, ModeConfig
    from models.demos.gpt_oss.tt.model_config import ModelArgs
    from models.demos.gpt_oss.utils.general_utils import get_default_num_links
    from models.demos.gpt_oss_d_p.tt.ccl import CCLManager
    from models.demos.gpt_oss_d_p.tt.model import Model

    shape = (args.rows, args.cols)
    sp = shape[0]

    if shape == (1, 1):
        fabric = None
        topology = ttnn.Topology.Linear
    elif is_blackhole():
        fabric = ttnn.FabricConfig.FABRIC_1D
        topology = ttnn.Topology.Linear
    else:
        fabric = ttnn.FabricConfig.FABRIC_1D_RING
        topology = ttnn.Topology.Ring

    print(f"[layer_act] mesh={shape} TP={shape[1]} fabric={fabric}", flush=True)

    if fabric is not None:
        ttnn.set_fabric_config(fabric)
    mesh = ttnn.open_mesh_device(ttnn.MeshShape(*shape))
    print(f"[layer_act] mesh opened: {tuple(mesh.shape)} ndev={mesh.get_num_devices()}", flush=True)

    try:
        model_args = ModelArgs(mesh_device=mesh)
        hf_config = AutoConfig.from_pretrained(model_args.model_path, trust_remote_code=True)
        tok = AutoTokenizer.from_pretrained(model_args.model_path, trust_remote_code=True)

        ids = tok(args.prompt)["input_ids"]
        real_len = len(ids)
        block = sp * 32
        max_seq_len = ((real_len + block - 1) // block) * block
        isl_per_row = max_seq_len // sp
        padded = ids + [tok.pad_token_id or 1] * (max_seq_len - real_len)
        print(f"[layer_act] prompt_tokens={real_len} padded_seq={max_seq_len}", flush=True)

        state_dict = ModelArgs.load_state_dict(model_args.weights_path, dummy_weights=False)
        cache = model_args.weight_cache_path(ttnn.bfloat8_b)
        mesh_config = MeshConfig(shape, decode=ModeConfig(tp=shape[1], ep=shape[0]))
        ccl = CCLManager(mesh, num_links=get_default_num_links(mesh), topology=topology)

        model = Model(
            mesh_device=mesh,
            hf_config=hf_config,
            state_dict=state_dict,
            ccl_manager=ccl,
            mesh_config=mesh_config,
            tensor_cache_path=str(cache),
            create_kv_cache=False,
            max_local_batch_size=1,
        )
        del state_dict
        gc.collect()
        print("[layer_act] model built; weights on device", flush=True)

        emb = _sp_shard_embed(mesh, model, padded, sp, isl_per_row)

        seq_len = emb.shape[-2]
        rope_mats = [
            model.rope_setup.cos_matrix_prefill[:, :, :seq_len, :],
            model.rope_setup.sin_matrix_prefill[:, :, :seq_len, :],
        ]

        hidden_states = emb
        results_table: list[tuple[int, float, bool]] = []
        any_fail = False

        max_layer_idx = max(layers_to_test)

        for i, decoder_layer in enumerate(model.layers):
            hidden_states = decoder_layer(
                hidden_states,
                position_embeddings=rope_mats,
                position_idx=None,
                page_table=None,
                kv_cache=None,
                is_decode=False,
            )

            if i not in layers_to_test:
                if i > max_layer_idx:
                    break
                continue

            ttnn.synchronize_device(mesh)
            tt_hs = _gather_hidden_states(hidden_states, mesh)  # [seq_len, hidden_size]

            oracle_file = oracle_dir / record["layer_files"][str(i)]
            oracle_hs = torch.tensor(np.load(oracle_file), dtype=torch.float32)  # [1, seq_len, hidden_size]
            oracle_hs = oracle_hs[0]  # [seq_len, hidden_size]

            # Compare only real (non-padded) token positions to avoid pad-token noise.
            tt_cmp = tt_hs[:real_len]
            ref_cmp = oracle_hs[:real_len]

            pcc = _pcc(tt_cmp, ref_cmp)
            passed = pcc >= args.pcc_threshold
            if not passed:
                any_fail = True
            results_table.append((i, pcc, passed))
            status = "PASS" if passed else "FAIL"
            print(f"[layer_act] layer {i:3d}: PCC={pcc:.4f} {status}", flush=True)

            if i >= max_layer_idx:
                # Free device memory and stop — no need to run remaining layers.
                hidden_states.deallocate(True)
                break

        print("", flush=True)
        print(f"{'Layer':>6}  {'PCC':>8}  {'Status'}", flush=True)
        print("-" * 30, flush=True)
        for layer_idx, pcc, passed in results_table:
            print(f"{layer_idx:>6}  {pcc:>8.4f}  {'PASS' if passed else 'FAIL'}", flush=True)

        if any_fail:
            failed = [i for i, _, p in results_table if not p]
            print(
                f"\n[layer_act] FAIL — {len(failed)} layer(s) below PCC threshold {args.pcc_threshold}: {failed}",
                flush=True,
            )
            return 1

        print(
            f"\n[layer_act] PASS — all {len(results_table)} layer(s) above PCC threshold {args.pcc_threshold}",
            flush=True,
        )
        return 0

    finally:
        ttnn.close_mesh_device(mesh)


if __name__ == "__main__":
    sys.exit(main())

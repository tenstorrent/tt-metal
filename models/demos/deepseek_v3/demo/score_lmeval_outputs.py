"""
Score demo.py outputs using the same lm-eval task logic as tti-eval.

Run with the evals venv Python, e.g.:
  ~/.workflow_venvs/.venv_evals_common/bin/python score_lmeval_outputs.py \
    --prompts-file /data/deepseek/prompts/demo_aime24_full.json \
    --output-file /data/deepseek/prompts/demo_aime24_full_output.json
"""

from __future__ import annotations

import argparse
import difflib
import json
import re
from collections import defaultdict

from lm_eval.tasks import TaskManager
from lm_eval.tasks.aime import utils as aime_utils

TASK_ALIASES = {
    # Historical/tti-eval naming aliases.
    "r1_aime24": "aime24",
}

GPQA_CHOICE_RE = re.compile(r"\(([A-D])\)")
GPQA_BOXED_RE = re.compile(r"\\boxed(?:\s*\{)?\s*\(?([A-D])\)?\s*(?:\})?", re.IGNORECASE)


def score_aime24(doc: dict, pred: str) -> dict[str, float]:
    return aime_utils.process_results(doc, [pred])


def extract_gpqa_choice(pred: str) -> str | None:
    boxed_matches = GPQA_BOXED_RE.findall(pred)
    if boxed_matches:
        return f"({boxed_matches[-1].upper()})"
    choice_matches = GPQA_CHOICE_RE.findall(pred.upper())
    if choice_matches:
        return f"({choice_matches[-1]})"
    answer_match = re.search(r"(?i)(?:the answer is|final answer is|answer:)\s*\(?([A-D])\)?", pred)
    if answer_match:
        return f"({answer_match.group(1).upper()})"
    return None


def score_gpqa(doc: dict, pred: str) -> dict[str, float]:
    extracted = extract_gpqa_choice(pred)
    target = str(doc["answer"]).strip().upper()
    return {"exact_match": 1.0 if extracted == target else 0.0}


LOCAL_TASK_SCORERS = {
    "aime24": score_aime24,
    "r1_aime24": score_aime24,
    "r1_gpqa_diamond": score_gpqa,
    "gpqa_diamond": score_gpqa,
}


def resolve_task_name(task_name: str, manager: TaskManager) -> str | None:
    if task_name in manager.task_index:
        return task_name
    alias = TASK_ALIASES.get(task_name)
    if alias and alias in manager.task_index:
        return alias
    if task_name.startswith("r1_"):
        stripped = task_name[len("r1_") :]
        if stripped in manager.task_index:
            return stripped
    return None


def main() -> None:
    parser = argparse.ArgumentParser(description="Score demo.py outputs with lm-eval task logic")
    parser.add_argument("--prompts-file", required=True, help="Prompt JSON used for demo.py")
    parser.add_argument(
        "--output-file",
        required=True,
        help="demo.py output JSON file (generated by demo.py)",
    )
    args = parser.parse_args()

    with open(args.prompts_file, "r", encoding="utf-8") as f:
        prompts = json.load(f)
    with open(args.output_file, "r", encoding="utf-8") as f:
        outputs = json.load(f)

    gens = outputs.get("generations", [])
    if len(prompts) != len(gens):
        raise SystemExit(f"Prompt count ({len(prompts)}) != generations count ({len(gens)})")

    manager = TaskManager()
    tasks: dict[str, object] = {}
    alias_notes: dict[str, str] = {}

    # metrics[task][metric] -> list of values
    metrics = defaultdict(lambda: defaultdict(list))

    for i, (prompt_item, gen_item) in enumerate(zip(prompts, gens), start=1):
        task_name = prompt_item.get("task")
        if not task_name:
            raise SystemExit(f"Prompt item {i} missing 'task' field")
        doc = prompt_item.get("doc")
        if doc is None:
            raise SystemExit(f"Prompt item {i} missing 'doc' field")

        pred = gen_item.get("text", "")
        scorer = LOCAL_TASK_SCORERS.get(task_name)
        if scorer is not None:
            result = scorer(doc, pred)
        else:
            resolved = resolve_task_name(task_name, manager)
            if resolved is None:
                suggestions = difflib.get_close_matches(task_name, manager.task_index.keys(), n=5)
                hint = f" Closest: {', '.join(suggestions)}" if suggestions else ""
                raise SystemExit(f"Unknown lm-eval task '{task_name}'.{hint}")
            if resolved != task_name:
                alias_notes[task_name] = resolved
            if resolved not in tasks:
                tasks[resolved] = manager.load_task_or_group(resolved)[resolved]
            task = tasks[resolved]
            result = task.process_results(doc, [pred])
        for metric_name, value in result.items():
            metrics[task_name][metric_name].append(value)

    # Print summary
    for task_name, task_metrics in metrics.items():
        display = task_name
        if task_name in alias_notes:
            display = f"{task_name} (alias of {alias_notes[task_name]})"
        print(f"\nTask: {display}")
        for metric_name, values in task_metrics.items():
            mean = sum(values) / len(values) if values else 0.0
            print(f"  {metric_name}: {mean:.4f} ({len(values)} samples)")


if __name__ == "__main__":
    main()

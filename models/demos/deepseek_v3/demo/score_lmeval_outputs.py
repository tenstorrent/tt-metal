"""
Score demo.py outputs using the same lm-eval task logic as tti-eval.

Run with the evals venv Python, e.g.:
  ~/.workflow_venvs/.venv_evals_common/bin/python score_lmeval_outputs.py \
    --prompts-file /data/deepseek/prompts/demo_aime24_full.json \
    --output-file /data/deepseek/prompts/demo_aime24_full_output.json
"""

from __future__ import annotations

import argparse
import json
from collections import defaultdict

from lm_eval.tasks import TaskManager


def main() -> None:
    parser = argparse.ArgumentParser(description="Score demo.py outputs with lm-eval task logic")
    parser.add_argument("--prompts-file", required=True, help="Prompt JSON used for demo.py")
    parser.add_argument(
        "--output-file",
        required=True,
        help="demo.py output JSON file (generated by demo.py)",
    )
    args = parser.parse_args()

    with open(args.prompts_file, "r", encoding="utf-8") as f:
        prompts = json.load(f)
    with open(args.output_file, "r", encoding="utf-8") as f:
        outputs = json.load(f)

    gens = outputs.get("generations", [])
    if len(prompts) != len(gens):
        raise SystemExit(f"Prompt count ({len(prompts)}) != generations count ({len(gens)})")

    manager = TaskManager()
    tasks = {}

    # metrics[task][metric] -> list of values
    metrics = defaultdict(lambda: defaultdict(list))

    for i, (prompt_item, gen_item) in enumerate(zip(prompts, gens), start=1):
        task_name = prompt_item.get("task")
        if not task_name:
            raise SystemExit(f"Prompt item {i} missing 'task' field")
        if task_name not in tasks:
            tasks[task_name] = manager.load_task_or_group(task_name)[task_name]

        task = tasks[task_name]
        doc = prompt_item.get("doc")
        if doc is None:
            raise SystemExit(f"Prompt item {i} missing 'doc' field")

        pred = gen_item.get("text", "")
        result = task.process_results(doc, [pred])
        for metric_name, value in result.items():
            metrics[task_name][metric_name].append(value)

    # Print summary
    for task_name, task_metrics in metrics.items():
        print(f"\nTask: {task_name}")
        for metric_name, values in task_metrics.items():
            mean = sum(values) / len(values) if values else 0.0
            print(f"  {metric_name}: {mean:.4f} ({len(values)} samples)")


if __name__ == "__main__":
    main()

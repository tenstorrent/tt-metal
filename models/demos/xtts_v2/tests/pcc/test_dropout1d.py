def _normalize_out(out):
    if isinstance(out, tuple) and out:
        out = out[0]
    if hasattr(out, "last_hidden_state"):
        out = out.last_hidden_state
    if isinstance(out, (list, tuple)) and out:
        out = out[0]
    return out


class _StageTimeout(Exception):
    pass


def _stage_timeout_handler(signum, frame):
    raise _StageTimeout(
        "stage exceeded TT_PLANNER_TEST_STAGE_S budget; the previous step "
        "(see last [bringup] stage=... print) did not return in time"
    )


def _set_stage_timeout(seconds: int):
    if seconds <= 0:
        return
    try:
        import signal as _signal
        _signal.signal(_signal.SIGALRM, _stage_timeout_handler)
        _signal.alarm(seconds)
    except (ValueError, AttributeError, ImportError):
        pass


def _clear_stage_timeout():
    try:
        import signal as _signal
        _signal.alarm(0)
    except (ValueError, AttributeError, ImportError):
        pass


def _is_mesh_device(device):
    """Mesh-agnostic helper: True iff `device` is a multi-device mesh."""
    try:
        if isinstance(device, ttnn.MeshDevice):
            return True
    except AttributeError:
        pass
    return hasattr(device, "get_device_ids") or hasattr(device, "get_devices")


def _ttnn_from_torch_mesh_safe(tensor, device, *,
                                dtype=ttnn.bfloat16, layout=ttnn.TILE_LAYOUT):
    """`ttnn.from_torch` that works on both single-device and mesh devices.

    On a mesh, the tensor is replicated to every device so per-device compute
    produces identical shards (readable from device 0)."""
    t = tensor.to(torch.bfloat16) if dtype == ttnn.bfloat16 else tensor
    if _is_mesh_device(device):
        try:
            return ttnn.from_torch(
                t, dtype=dtype, layout=layout, device=device,
                mesh_mapper=ttnn.ReplicateTensorToMesh(device),
            )
        except (AttributeError, TypeError):
            pass
    return ttnn.from_torch(t, dtype=dtype, layout=layout, device=device)


def _ttnn_to_torch_mesh_safe(ttnn_tensor, device):
    """`ttnn.to_torch` that works on both single-device and mesh devices.

    Uses the same `mesh_composer=ConcatMeshToTensor(...)` pattern that the
    existing tt-metal demos use, which is the only known-stable way to read
    back from a MeshDevice (bare `to_torch` and `get_device_tensors[0]` can
    busy-loop in the C extension with the GIL held).

    A device synchronize is issued first so any pending kernels finish
    before the readback (otherwise the readback can deadlock on a kernel
    that never completes)."""
    # CPU-fallback `__call__` paths legitimately return `torch.Tensor`
    # (e.g. when a stub was rolled back to `.bak` after the auto-iterate
    # cap-out, or when a `_apply_*` helper invoked the torch reference
    # because the ttnn path raised). Without this guard,
    # `ttnn.to_torch(...)` would try `tensor.to_torch(mesh_composer=...)`
    # on a torch.Tensor and raise:
    #   AttributeError: 'Tensor' object has no attribute 'to_torch'
    # — turning a clean numerical comparison into an opaque crash.
    # Observed in the 2026-05-22 sam2-hiera-small final pytest after
    # decoder_head was restored from `.bak`.
    if isinstance(ttnn_tensor, torch.Tensor):
        return ttnn_tensor
    try:
        if hasattr(ttnn, "synchronize_device"):
            ttnn.synchronize_device(device)
        elif hasattr(device, "synchronize"):
            device.synchronize()
    except Exception:
        pass

    if _is_mesh_device(device):
        for mk_composer in (
            lambda: ttnn.concat_mesh_to_tensor_composer(device, 0),
            lambda: ttnn.ConcatMeshToTensor(device, dim=0),
        ):
            try:
                composer = mk_composer()
            except (AttributeError, TypeError):
                continue
            try:
                t = ttnn.to_torch(ttnn_tensor, mesh_composer=composer)
                if t is None:
                    continue
                if t.ndim >= 1 and t.shape[0] > 1:
                    n_devices = 1
                    try:
                        ids = device.get_device_ids() if hasattr(device, "get_device_ids") else []
                        n_devices = len(ids) or 1
                    except Exception:
                        pass
                    if n_devices > 1 and t.shape[0] % n_devices == 0:
                        t = t[: t.shape[0] // n_devices]
                return t
            except Exception:
                continue
    return ttnn.to_torch(ttnn_tensor)


# `l1_small_size` is the per-chip allocation reserved for L1_SMALL buffers,
# which `ttnn.conv2d` (specifically the sliding-window/halo helper) and
# `ttnn.max_pool2d` use for scratch. With the historical default of `{}`
# (i.e. l1_small_size=0), the FIRST conv2d in any vision/CNN model raises
# `TT_FATAL: bank size is 0 B` and falls back to a slow torch-on-host
# implementation. That fallback is correct but turns a 30s on-device PCC
# test into a 5-15min one, blows past the auto-iterate wall-clock budget,
# and pollutes the compute split with runtime CPU fallbacks that the LLM
# would otherwise correctly mark MIXED. 24576 (24 KiB) matches the
# established convention used by every existing vision demo in this repo
# (see e.g. `models/demos/vision/segmentation/sam2_hiera_small/demo/
# demo_for_image_classification.py:98`). It is harmless on models that
# don't use conv2d/max_pool2d — those L1_SMALL banks just stay empty.
@pytest.mark.parametrize("device_params", [{"l1_small_size": 24576}], indirect=True)
def test_dropout1d(device_params, device):
    stage_budget_s = int(os.environ.get("TT_PLANNER_TEST_STAGE_S", "120"))

    try:
        _seed = int(os.environ.get("TT_PLANNER_TEST_SEED", "0"))
    except ValueError:
        _seed = 0
    torch.manual_seed(_seed)

    print("[bringup] stage=build_torch_reference", flush=True)
    _set_stage_timeout(stage_budget_s)
    try:
        torch_module, sample_kwargs, primary = _build_torch_reference()
    finally:
        _clear_stage_timeout()

    try:
        torch_module = torch_module.float()
    except Exception:
        pass
    for _k in list(sample_kwargs.keys()):
        _v = sample_kwargs[_k]
        if isinstance(_v, torch.Tensor) and _v.is_floating_point():
            sample_kwargs[_k] = _v.to(torch.float32)
    _pn, _pt = primary
    if isinstance(_pt, torch.Tensor) and _pt.is_floating_point():
        primary = (_pn, _pt.to(torch.float32))

    print("[bringup] stage=torch_forward", flush=True)
    _set_stage_timeout(stage_budget_s)
    try:
        with torch.no_grad():
            try:
                torch_out = torch_module(**sample_kwargs)
            except Exception as exc:
                pytest.skip(
                    f"HF reference forward({list(sample_kwargs.keys())}) raised "
                    f"{type(exc).__name__} for {COMPONENT_NAME}: {exc} -- "
                    f"the synthetic inputs from _make_arg_for() are incompatible "
                    f"with this submodule's expected shapes. Either edit the test "
                    f"to provide model-specific kwargs, or skip this Phase-2 PCC "
                    f"and validate via the top-level demo instead."
                )
    finally:
        _clear_stage_timeout()
    torch_out = _normalize_out(torch_out)

    print("[bringup] stage=ttnn_build", flush=True)
    _set_stage_timeout(stage_budget_s)
    try:
        ttnn_module = _build_ttnn_port(device, torch_module)
    finally:
        _clear_stage_timeout()

    primary_name, primary_tensor = primary

    print(f"[bringup] stage=ttnn_input (mesh={_is_mesh_device(device)})", flush=True)
    _set_stage_timeout(stage_budget_s)
    try:
        ttnn_input = _ttnn_from_torch_mesh_safe(primary_tensor, device)
    finally:
        _clear_stage_timeout()

    print("[bringup] stage=ttnn_forward", flush=True)
    _set_stage_timeout(stage_budget_s)
    try:
        ttnn_extra_kwargs = {
            k: v for k, v in sample_kwargs.items() if k != primary_name
        }
        ttnn_out = ttnn_module(ttnn_input, **ttnn_extra_kwargs)
    finally:
        _clear_stage_timeout()
    if isinstance(ttnn_out, tuple) and ttnn_out:
        ttnn_out = ttnn_out[0]

    print("[bringup] stage=ttnn_to_torch", flush=True)
    _set_stage_timeout(stage_budget_s)
    try:
        ttnn_out_torch = _ttnn_to_torch_mesh_safe(ttnn_out, device).to(torch.float32)
    finally:
        _clear_stage_timeout()

    if isinstance(torch_out, torch.Tensor):
        torch_out = torch_out.to(torch.float32)

    ok, pcc = comp_pcc(torch_out, ttnn_out_torch, PCC_TARGET)
    print(f"[bringup] achieved PCC={pcc} target={PCC_TARGET} component={COMPONENT_NAME}", flush=True)
    assert ok, (
        f"PCC {pcc} below target {PCC_TARGET} for {COMPONENT_NAME} of "
        f"{HF_MODEL_ID} (primary arg `{primary_name}`)"
    )

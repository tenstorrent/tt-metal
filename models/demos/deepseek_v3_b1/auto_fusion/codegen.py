"""
Unified kernel C++ code generation.

Generates a single .cpp file that compiles for NCRISC, BRISC, and TRISC,
in the same style as the hand-fused attention_block_kernel.cpp:

1. #include directives for all micro-op headers
2. Core struct with per-core compile-time role flags
3. void kernel_main() with triple-compiled sections (#if COMPILE_FOR_*)
4. Per-RISC: CTArgs type aliases + RTArgs initialization
5. Shared execution body with if constexpr guards and DeviceZoneScopedN
"""

from __future__ import annotations

import re
from typing import Dict, List, Optional

from models.demos.deepseek_v3_b1.auto_fusion.types import OpNode


class UnifiedKernelCodegen:
    """
    Generates a unified kernel .cpp file from a compiled FusionGraph.
    """

    def __init__(self, graph, schedule: List[str], cb_allocs: Dict, emit_profiler_zones: bool = False):
        self._graph = graph
        self._schedule = schedule
        self._cb_allocs = cb_allocs
        self._nodes = {n.id: n for n in graph.nodes}
        self._emit_profiler_zones = emit_profiler_zones
        # Compute per-RISC common RT arg base indices for each op
        self._trisc_rt_base = {}
        idx = 0
        for nid in schedule:
            self._trisc_rt_base[nid] = idx
            node = self._nodes[nid]
            idx += len(node.spec.trisc.common_runtime_args)

    def generate(self) -> str:
        """Generate the complete unified kernel source."""
        parts = []
        parts.append(self._emit_header())
        parts.append(self._emit_includes())
        parts.append(self._emit_core_struct())
        parts.append(self._emit_kernel_main_open())
        parts.append(self._emit_ncrisc_section())
        parts.append(self._emit_brisc_section())
        parts.append(self._emit_trisc_section())
        parts.append(self._emit_execution_body())
        parts.append("}\n")
        return "\n".join(parts)

    def _emit_header(self) -> str:
        return (
            "// SPDX-FileCopyrightText: © 2026 Tenstorrent AI ULC\n"
            "// SPDX-License-Identifier: Apache-2.0\n"
            "//\n"
            "// Auto-generated fused kernel. DO NOT EDIT.\n"
            "// Generated by auto_fusion infrastructure.\n"
        )

    def _emit_includes(self) -> str:
        """Emit #include directives for all unique micro-op headers."""
        lines = []
        seen = set()

        # Always include kernel_op_api and kernel_utils
        lines.append('#include "../../../unified_kernels/kernel_op_api.hpp"')
        lines.append('#include "../../../unified_kernels/kernel_utils.hpp"')

        for node_id in self._schedule:
            node = self._nodes[node_id]
            header = node.spec.header
            if header not in seen:
                seen.add(header)
                lines.append(f'#include "../../../{header}"')

            for extra in node.spec.extra_includes:
                if extra not in seen:
                    seen.add(extra)
                    lines.append(f'#include "../../../{extra}"')

        return "\n".join(lines) + "\n"

    def _emit_core_struct(self) -> str:
        """Emit the Core struct with compile-time role flags."""
        lines = [
            "// Compile-time role flags for dead code elimination via if constexpr",
            "struct Core {",
        ]

        emitted_flags = set()

        for node_id in self._schedule:
            node = self._nodes[node_id]
            flag = node.placement.role_flag
            if flag not in emitted_flags:
                emitted_flags.add(flag)
                lines.append(f"    static constexpr bool {flag} = " f'get_named_compile_time_arg_val("{flag}") == 1;')

            # Emit sender/receiver flags if op template uses them
            spec = node.spec
            if "{is_sender}" in spec.op_template:
                sender_flag = f"is_{node_id}_sender"
                if sender_flag not in emitted_flags:
                    emitted_flags.add(sender_flag)
                    lines.append(
                        f"    static constexpr bool {sender_flag} = "
                        f'get_named_compile_time_arg_val("{sender_flag}") == 1;'
                    )
            if "{is_receiver}" in spec.op_template:
                receiver_flag = f"is_{node_id}_receiver"
                if receiver_flag not in emitted_flags:
                    emitted_flags.add(receiver_flag)
                    lines.append(
                        f"    static constexpr bool {receiver_flag} = "
                        f'get_named_compile_time_arg_val("{receiver_flag}") == 1;'
                    )

            # Emit custom bool flags from ct_args (e.g., use_per_core_sender_idx)
            for ct_key, ct_val in node.ct_args.items():
                if ct_key.startswith("_"):
                    continue  # Skip internal keys
                flag_placeholder = f"{{{ct_key}}}"
                if flag_placeholder in spec.op_template and isinstance(ct_val, bool):
                    custom_flag = f"{node_id}_{ct_key}"
                    if custom_flag not in emitted_flags:
                        emitted_flags.add(custom_flag)
                        lines.append(
                            f"    static constexpr bool {custom_flag} = "
                            f'get_named_compile_time_arg_val("{custom_flag}") == 1;'
                        )

        lines.append("};")
        return "\n".join(lines) + "\n"

    def _emit_kernel_main_open(self) -> str:
        return "void kernel_main() {\n"

    # =========================================================================
    # Per-RISC sections
    # =========================================================================

    def _op_takes_args(self, node: OpNode) -> bool:
        """Check if the Op class takes RTArgs in operator()."""
        spec = node.spec
        return bool(spec.ncrisc.rt_args_type or spec.brisc.rt_args_type or spec.trisc.rt_args_type)

    def _emit_ncrisc_section(self) -> str:
        """Emit NCRISC (reader) section with CTArgs + RTArgs + sharded buffer setup."""
        lines = ["#if defined(COMPILE_FOR_NCRISC)"]

        for node_id in self._schedule:
            node = self._nodes[node_id]
            prefix = node_id
            spec = node.spec
            contract = spec.ncrisc
            lines.append(f"    // --- {node_id} (NCRISC) ---")

            # CTArgs type alias (must always be defined for the common execution body)
            ct_type = self._resolve_ct_type(contract, node, "ncrisc")
            if ct_type:
                lines.append(f"    using {prefix}_CTArgs = {ct_type};")
            else:
                lines.append(f"    struct {prefix}_CTArgs {{}};")

            # Named CT arg reads for sharded buffer setup
            for port_name in contract.setup_sharded:
                # Only setup sharded buffers for external (tensor-backed) ports
                key = (node_id, port_name)
                if port_name not in node.cb_bindings:
                    continue
                if key in self._cb_allocs and not getattr(self._cb_allocs[key], "is_external", True):
                    continue
                cb_arg = f"{prefix}_{port_name}_cb"
                lines.append(f"    constexpr uint32_t {cb_arg} = " f'get_named_compile_time_arg_val("{cb_arg}");')

            # RTArgs initialization
            if self._op_takes_args(node):
                if contract.rt_args_fields:
                    fields = self._build_rt_args_init(contract, node, prefix)
                    lines.append(f"    {contract.rt_args_type} {prefix}_args{{{fields}}};")
                elif contract.rt_args_type:
                    lines.append(f"    {contract.rt_args_type} {prefix}_args{{}};")
                else:
                    lines.append(f"    struct {prefix}_DummyArgs {{}}; {prefix}_DummyArgs {prefix}_args{{}};")

            # Sharded buffer setup inside if constexpr
            sharded_ports = [
                p
                for p in contract.setup_sharded
                if p in node.cb_bindings
                and ((node_id, p) not in self._cb_allocs or getattr(self._cb_allocs[(node_id, p)], "is_external", True))
            ]
            if sharded_ports:
                active_flag = self._get_active_flag(node, "ncrisc")
                lines.append(f"    if constexpr ({active_flag}) {{")
                for port_name in sharded_ports:
                    cb_arg = f"{prefix}_{port_name}_cb"
                    num_pages_arg = f"{prefix}_{port_name}_num_pages"
                    lines.append(
                        f"        constexpr uint32_t {num_pages_arg} = "
                        f'get_named_compile_time_arg_val("{num_pages_arg}");'
                    )
                    lines.append(f"        unified_kernels::setup_sharded_buffer({cb_arg}, {num_pages_arg});")
                lines.append("    }")

            lines.append("")

        lines.append("")
        return "\n".join(lines)

    def _emit_brisc_section(self) -> str:
        """Emit BRISC (writer) section."""
        lines = ["#elif defined(COMPILE_FOR_BRISC)"]

        for node_id in self._schedule:
            node = self._nodes[node_id]
            prefix = node_id
            spec = node.spec
            contract = spec.brisc
            lines.append(f"    // --- {node_id} (BRISC) ---")

            # CTArgs type alias (must always be defined for the common execution body)
            ct_type = self._resolve_ct_type(contract, node, "brisc")
            if ct_type:
                lines.append(f"    using {prefix}_CTArgs = {ct_type};")
            else:
                lines.append(f"    struct {prefix}_CTArgs {{}};")

            # Emit constexpr for CB indices needed by read_ptr/write_ptr in RT args
            if contract.rt_args_fields:
                for _, source in contract.rt_args_fields:
                    if source.startswith("read_ptr:") or source.startswith("write_ptr:"):
                        cb_name = source.split(":")[1]
                        cb_var = f"{prefix}_{cb_name}"
                        lines.append(
                            f"    constexpr uint32_t {cb_var} = " f'get_named_compile_time_arg_val("{cb_var}");'
                        )

            # RTArgs initialization
            if self._op_takes_args(node):
                if contract.rt_args_fields:
                    fields = self._build_rt_args_init(contract, node, prefix)
                    lines.append(f"    {contract.rt_args_type} {prefix}_args{{{fields}}};")
                elif contract.rt_args_type:
                    lines.append(f"    {contract.rt_args_type} {prefix}_args{{}};")
                else:
                    lines.append(f"    struct {prefix}_DummyArgs {{}}; {prefix}_DummyArgs {prefix}_args{{}};")
            lines.append("")

        lines.append("")
        return "\n".join(lines)

    def _emit_trisc_section(self) -> str:
        """Emit TRISC (compute) section with CTArgs + RTArgs."""
        lines = ["#elif defined(COMPILE_FOR_TRISC)"]

        for node_id in self._schedule:
            node = self._nodes[node_id]
            prefix = node_id
            spec = node.spec
            contract = spec.trisc
            lines.append(f"    // --- {node_id} (TRISC) ---")

            # CTArgs type alias (must always be defined for the common execution body)
            ct_type = self._resolve_ct_type(contract, node, "trisc")
            if ct_type:
                lines.append(f"    using {prefix}_CTArgs = {ct_type};")
            else:
                lines.append(f"    struct {prefix}_CTArgs {{}};")

            # RTArgs initialization (skip if op takes no args)
            if self._op_takes_args(node):
                if contract.rt_args_fields:
                    fields = self._build_rt_args_init(contract, node, prefix)
                    lines.append(f"    {contract.rt_args_type} {prefix}_args{{{fields}}};")
                elif contract.rt_args_type:
                    lines.append(f"    {contract.rt_args_type} {prefix}_args{{}};")
                else:
                    lines.append(f"    struct {prefix}_DummyArgs {{}}; {prefix}_DummyArgs {prefix}_args{{}};")
            lines.append("")

        # deepseek_compute_kernel_init() inside active core guard to avoid
        # running on inactive cores. Only include flags for ops with non-noop TRISC.
        trisc_flags = []
        for nid in self._schedule:
            node = self._nodes[nid]
            if not node.spec.trisc.is_noop:
                flag = node.placement.role_flag
                if flag not in trisc_flags:
                    trisc_flags.append(flag)
        if trisc_flags:
            if len(trisc_flags) == 1:
                guard = f"Core::{trisc_flags[0]}"
            else:
                guard = " || ".join(f"Core::{f}" for f in trisc_flags)
            lines.append(f"    if constexpr ({guard}) {{")
            lines.append("        deepseek_compute_kernel_init();")
            lines.append("    }")
        lines.append("#endif")
        lines.append("")
        return "\n".join(lines)

    # =========================================================================
    # Execution body
    # =========================================================================

    def _emit_execution_body(self) -> str:
        """Emit the main execution body with op invocations."""
        lines = []

        # Ops with init (like mcast) - emit init calls first
        init_ops = []
        teardown_ops = []
        for node_id in self._schedule:
            node = self._nodes[node_id]
            if node.spec.has_init:
                init_ops.append(node_id)
            if node.spec.has_teardown:
                teardown_ops.append(node_id)

        # Declare persistent op instances and call init
        for node_id in init_ops:
            node = self._nodes[node_id]
            op_type = self._build_op_type(node)
            lines.append(f"    {node.spec.namespace}::{node.spec.struct_name}::" f"{op_type} {node_id}_op;")
            if self._emit_profiler_zones:
                lines.append("    {")
                lines.append(f'        DeviceZoneScopedN("{node_id.upper()}_INIT");')
                lines.append(f"        {node_id}_op.init({node_id}_args);")
                lines.append("    }")
            else:
                lines.append(f"    {node_id}_op.init({node_id}_args);")
            lines.append("")

        # Main body
        for node_id in self._schedule:
            node = self._nodes[node_id]
            takes_args = self._op_takes_args(node)
            arg_str = f"{node_id}_args" if takes_args else ""
            lines.append(f"    // {node_id}")

            if self._emit_profiler_zones:
                lines.append("    {")
                lines.append(f'        DeviceZoneScopedN("{node_id.upper()}");')

            indent = "        " if self._emit_profiler_zones else "    "
            if node.spec.has_init:
                lines.append(f"{indent}{node_id}_op({arg_str});")
            else:
                op_type = self._build_op_type(node)
                lines.append(f"{indent}{node.spec.namespace}::{node.spec.struct_name}::" f"{op_type} {node_id}_op;")
                lines.append(f"{indent}{node_id}_op({arg_str});")

            if self._emit_profiler_zones:
                lines.append("    }")
            lines.append("")

        # Teardown for persistent ops
        for node_id in teardown_ops:
            if self._emit_profiler_zones:
                lines.append("    {")
                lines.append(f'        DeviceZoneScopedN("{node_id.upper()}_TEARDOWN");')
                lines.append(f"        {node_id}_op.teardown();")
                lines.append("    }")
            else:
                lines.append(f"    {node_id}_op.teardown();")
            lines.append("")

        return "\n".join(lines)

    # =========================================================================
    # Helpers
    # =========================================================================

    def _resolve_ct_type(self, contract, node: OpNode, risc: str) -> Optional[str]:
        """Resolve CTArgs type string, substituting template params from ct_args."""
        if not contract.ct_args_type:
            return None

        ct_type = contract.ct_args_type
        prefix = node.id

        # Substitute {param} placeholders with actual values
        for key, val in node.ct_args.items():
            placeholder = f"{{{key}}}"
            if placeholder in ct_type:
                if isinstance(val, bool):
                    ct_type = ct_type.replace(placeholder, "true" if val else "false")
                else:
                    ct_type = ct_type.replace(placeholder, str(val))

        # Substitute CB index placeholders
        for port_name, cb_idx in node.cb_bindings.items():
            placeholder = f"{{{port_name}}}"
            if placeholder in ct_type:
                # Use named CT arg reference instead of literal
                ct_type = ct_type.replace(placeholder, f'get_named_compile_time_arg_val("{prefix}_{port_name}_cb")')

        # Substitute remaining {param} with named CT arg lookups
        remaining = re.findall(r"\{(\w+)\}", ct_type)
        for param in remaining:
            ct_type = ct_type.replace(f"{{{param}}}", f'get_named_compile_time_arg_val("{prefix}_{param}")')

        # Substitute role flags
        for flag_name in ["is_sender", "is_receiver", "is_active"]:
            placeholder = f"{{{flag_name}}}"
            if placeholder in ct_type:
                ct_type = ct_type.replace(placeholder, f"Core::{self._flag_for(node, flag_name)}")

        return ct_type

    def _build_op_type(self, node: OpNode) -> str:
        """Build the C++ Op type instantiation string."""
        template = node.spec.op_template
        prefix = node.id

        # Substitute CTArgs
        ct_args_ref = f"{prefix}_CTArgs"
        template = template.replace("{CTArgs}", ct_args_ref)

        # Substitute role flags
        for flag_name in ["is_active", "is_sender", "is_receiver"]:
            placeholder = f"{{{flag_name}}}"
            if placeholder in template:
                template = template.replace(placeholder, f"Core::{self._flag_for(node, flag_name)}")

        # Substitute custom bool flags from ct_args
        remaining = re.findall(r"\{(\w+)\}", template)
        for param in remaining:
            val = node.ct_args.get(param)
            if isinstance(val, bool):
                template = template.replace(f"{{{param}}}", f"Core::{prefix}_{param}")
            elif val is not None:
                template = template.replace(f"{{{param}}}", str(val))

        return template

    def _build_rt_args_init(self, contract, node: OpNode, prefix: str) -> str:
        """Build RTArgs aggregate initializer fields.

        Source prefixes:
          ct:<name>         -> get_named_compile_time_arg_val("prefix_name")
          rt_uint32:<name>  -> get_common_arg_val<uint32_t>(base + offset)
          rt_float:<name>   -> get_common_arg_val<float>(base + offset)
          semaphore:<name>  -> get_semaphore(get_named_compile_time_arg_val("prefix_name"))
          read_ptr:<name>   -> get_read_ptr(prefix_name)  [uses constexpr variable]
          write_ptr:<name>  -> get_write_ptr(prefix_name) [uses constexpr variable]
        """
        # Build name->index mapping for common runtime args
        common_rt_index = {}
        for i, (arg_name, _) in enumerate(contract.common_runtime_args):
            common_rt_index[arg_name] = i

        base = self._trisc_rt_base.get(node.id, 0)

        fields = []
        for field_name, source in contract.rt_args_fields:
            if source.startswith("ct:"):
                ct_name = source[3:]
                val = f'get_named_compile_time_arg_val("{prefix}_{ct_name}")'
            elif source.startswith("rt_uint32:"):
                rt_name = source[len("rt_uint32:") :]
                idx = base + common_rt_index[rt_name]
                val = f"get_common_arg_val<uint32_t>({idx})"
            elif source.startswith("rt_float:"):
                rt_name = source[len("rt_float:") :]
                idx = base + common_rt_index[rt_name]
                val = f"get_common_arg_val<float>({idx})"
            elif source.startswith("semaphore:"):
                sem_name = source[10:]
                val = f'get_semaphore(get_named_compile_time_arg_val("{prefix}_{sem_name}"))'
            elif source.startswith("read_ptr:"):
                cb_name = source[9:]
                # Use the constexpr variable emitted earlier
                val = f"get_read_ptr({prefix}_{cb_name})"
            elif source.startswith("write_ptr:"):
                cb_name = source[10:]
                # Use the constexpr variable emitted earlier
                val = f"get_write_ptr({prefix}_{cb_name})"
            else:
                val = source
            fields.append(f"\n        .{field_name} = {val},")
        return "".join(fields) + "\n    "

    def _get_active_flag(self, node: OpNode, risc: str) -> str:
        """Get the if-constexpr condition for whether this core runs the op."""
        return f"Core::{node.placement.role_flag}"

    def _flag_for(self, node: OpNode, flag_name: str) -> str:
        """Map a generic flag name to the actual Core:: flag for this node."""
        if flag_name == "is_active":
            return node.placement.role_flag
        elif flag_name == "is_sender":
            return f"is_{node.id}_sender"
        elif flag_name == "is_receiver":
            return f"is_{node.id}_receiver"
        return node.placement.role_flag

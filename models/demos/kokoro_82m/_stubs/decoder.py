from __future__ import annotations

import transformers

HF_MODEL_ID = "hexgrad/Kokoro-82M"

_CANDIDATE_SUBMODULE_PATHS = ["decoder"]

_cache: dict = {}


def _resolve(obj, dotted: str):
    cur = obj
    for tok in dotted.replace("[", ".").replace("]", "").split("."):
        if tok == "":
            continue
        if tok.isdigit():
            cur = cur[int(tok)]
        else:
            cur = getattr(cur, tok)
    # 2026-06-03 fix: ModuleList/Sequential containers have no forward()
    # — calling them raises NotImplementedError and the test SKIPs. If
    # the resolved object is a container, automatically pick the first
    # element so the test gets a real callable module.
    try:
        import torch as _torch

        if isinstance(cur, (_torch.nn.ModuleList, _torch.nn.Sequential)) and len(cur) > 0:
            cur = cur[0]
    except Exception:
        pass
    return cur


def _get_torch_submodule():
    if "module" in _cache:
        return _cache["module"]
    # AutoModelForCausalLM-first fallback (same rationale as
    # _PCC_TEST_TEMPLATE._build_torch_reference); see comment there.
    model = None
    _last_err = None
    for _cls_name in ("AutoModelForCausalLM", "AutoModel"):
        try:
            _cls = getattr(transformers, _cls_name)
        except AttributeError:
            continue
        try:
            model = _cls.from_pretrained(
                HF_MODEL_ID, trust_remote_code=True, torch_dtype="bfloat16", low_cpu_mem_usage=True
            )
            break
        except Exception as _e:
            _last_err = _e
            continue

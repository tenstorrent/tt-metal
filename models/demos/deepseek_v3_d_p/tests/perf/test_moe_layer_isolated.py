"""
Isolated single-MoE-layer replay for the Kimi 8x4 chunked-prefill workload.

Runs ONE MoE layer's ``TtMoe.forward`` for a chosen (chunk_id, layer_id), using:
  * the real cached weights for that layer (from TT_KIMI_PREFILL_TTNN_CACHE), built
    via the exact same ``TtPrefillBlock._build_moe`` path the full test uses, and
  * the real MoE input activation captured from a full chunked run (see
    ``utils/moe_input_capture.py`` and TT_DS_CAPTURE_MOE_DIR).

This lets you profile / debug a single (chunk, layer)'s MoE forward without
re-running the whole 61-layer, 11-chunk prefill each time.

Two-step workflow
-----------------
1) Record (once) — run the normal no-PCC test with capture enabled, e.g.::

     TT_DS_CAPTURE_MOE_DIR=/data/fbajraktari/moe_cap \
     TT_DS_CAPTURE_MOE_CHUNKS=3 TT_DS_CAPTURE_MOE_LAYERS=27,50 \
     pytest "models/demos/deepseek_v3_d_p/tests/test_prefill_transformer_chunked.py::\
test_kimi_prefill_transformer_chunked_no_pcc[blackhole-kimi-mesh-8x4-L61-chunks_eleven-iters1-margin5pct]" -xvs

2) Replay one (chunk, layer) in isolation::

     TT_DS_CAPTURE_MOE_DIR=/data/fbajraktari/moe_cap \
     pytest "models/demos/deepseek_v3_d_p/tests/perf/test_moe_layer_isolated.py" -xvs \
       -k "chunk3 and layer27"

The device is opened identically to the no-PCC test (FABRIC_1D, l1_small=512,
Kimi fabric payload), and the MoE is sized identically (capacity_factor=8,
experts_per_chip=12, DEVICE_FP32 gate, the four expert dtypes).
"""


# --- Config for the Kimi 8x4 no-PCC run (mirrors test_prefill_transformer_chunked.py) ---
CHUNK = 5 * 1024  # 5120 tokens per chunk
SP_AXIS = 0
NUM_LINKS = 2
DISPATCH_BUFFER_CAPACITY_FACTOR = 8
EMB_DIM = KimiK26Config.EMB_SIZE  # 7168
NUM_DENSE_LAYERS = KimiK26Config.NUM_DENSE_LAYERS  # 1 -> MoE layers are 1..NUM_LAYERS-1
NUM_MOE_LAYERS = KimiK26Config.NUM_LAYERS  # 61

# All MoE layer indices (1..60) and all chunk indices for the chunks_eleven config (0..10).
_LAYER_IDS = list(range(NUM_DENSE_LAYERS, NUM_MOE_LAYERS))  # 1..60
_CHUNK_IDS = list(range(11))
# Iteration counts to expose as a parameter. Iteration 0 is always a warmup (builds the
# program cache) and is dropped from the reported mean/median/std, so use >= 2.
_NUM_ITERS = [2, 10, 15, 20, 25, 100, 1000]


def _parse_ttnn_dtype(s: str):
    tok = s.rsplit(".", 1)[-1].strip("<> ").split(":")[0].strip().upper()
    table = {
        "BFLOAT16": ttnn.bfloat16,
        "FLOAT32": ttnn.float32,
        "BFLOAT8_B": ttnn.bfloat8_b,
        "BFLOAT4_B": ttnn.bfloat4_b,
        "UINT16": ttnn.uint16,
        "UINT32": ttnn.uint32,
        "INT32": ttnn.int32,
    }
    if tok not in table:
        raise ValueError(f"Unrecognized captured dtype {s!r} (token {tok!r})")
    return table[tok]


def _parse_ttnn_layout(s: str):
    tok = s.rsplit(".", 1)[-1].strip("<> ").split(":")[0].strip().upper()
    if "TILE" in tok:
        return ttnn.TILE_LAYOUT
    if "ROW_MAJOR" in tok:
        return ttnn.ROW_MAJOR_LAYOUT
    raise ValueError(f"Unrecognized captured layout {s!r} (token {tok!r})")


# --------------------------------------------------------------------------------------
# Per-op DEVICE-KERNEL-duration statistics (post-tracy) -- see ``test_moe_stacked_op_stats``.
#
# The e2e ``profiler`` sample in ``test_moe_layer_isolated`` is a single number per
# iteration; to bisect the MoE forward's variance down to the individual device ops, we
# re-use the per-iteration signposts emitted around the timed region. For each measured
# iteration we run ``tt-perf-report`` bounded by that iteration's ``start``/``end``
# signpost and read the STACKED report's ``Device Time Sum [μs]`` column (the summed
# DEVICE KERNEL duration per op). Stacking the iterations gives a per-op x per-iteration
# matrix with trailing mean/std, which is what surfaces "which op is jittering".
#
# IMPORTANT: ``ops_perf_results_*.csv`` is generated by tracy AFTER the whole pytest
# process exits, so the recording test cannot see its own CSV. The analysis therefore
# lives in a SEPARATE entry point (``test_moe_stacked_op_stats``) that consumes an
# already-produced CSV, whose path is passed in TT_MOE_OPS_CSV. See that test's docstring.
# --------------------------------------------------------------------------------------
def _resolve_tt_perf_report_bin():
    """Locate the ``tt-perf-report`` console script (PATH, then the active venv)."""
    found = shutil.which("tt-perf-report")
    if found:
        return found
    candidate = Path(sys.prefix) / "bin" / "tt-perf-report"
    return str(candidate) if candidate.exists() else None


# Signpost emitted around the timed region, e.g. "MoE_isolated c5 l27 iter3 start".
_SIGNPOST_RE = re.compile(r"MoE_isolated c(\d+) l(\d+) iter(\d+) (?:start|end)")


def _discover_signpost_groups(ops_csv: Path):
    """Scan the ops CSV's signposts for recorded MoE runs.

    Returns {(chunk_id, layer_id): sorted_iter_ids}. Signposts are written verbatim into
    the ops CSV, so a plain text scan is enough (no tt-perf-report call needed here).
    """
    groups = {}
    with open(ops_csv, errors="ignore") as f:
        text = f.read()
    for m in _SIGNPOST_RE.finditer(text):
        chunk_id, layer_id, iter_id = int(m.group(1)), int(m.group(2)), int(m.group(3))
        groups.setdefault((chunk_id, layer_id), set()).add(iter_id)
    return {k: sorted(v) for k, v in groups.items()}


def _parse_stacked_device_time(stacked_csv: Path):
    """Return {op_code: device_time_sum_us} from a tt-perf-report stacked-report CSV."""
    out = {}
    with open(stacked_csv, newline="") as f:
        reader = csv.DictReader(f)
        fields = reader.fieldnames or []
        # "Device Time Sum [μs]" -- match on the stable prefix (unit/encoding may vary).
        time_col = next((c for c in fields if c.startswith("Device Time Sum")), None)
        op_col = next((c for c in fields if c.strip() == "Op Code"), None)
        if time_col is None or op_col is None:
            return out
        for row in reader:
            op = (row.get(op_col) or "").strip()
            if not op:
                continue
            try:
                out[op] = float(row[time_col])
            except (TypeError, ValueError):
                continue
    return out


def _stacked_device_time_for_iter(perf_bin, ops_csv, start_sp, end_sp, workdir, tag):
    """Run tt-perf-report for one signpost-bounded iteration; return {op: device_us}."""
    base = Path(workdir) / f"stacked_{tag}"  # tt-perf-report writes "<base>.csv" (+ a .png)
    cmd = [
        perf_bin,
        "--no-advice",
        "--no-color",
        "--start-signpost",
        start_sp,
        "--end-signpost",
        end_sp,
        "--stacked-csv",
        str(base),
        str(ops_csv),
    ]
    proc = subprocess.run(cmd, capture_output=True, text=True)
    out_csv = Path(f"{base}.csv")
    if proc.returncode != 0 or not out_csv.exists():
        logger.warning(
            f"[moe_isolated] tt-perf-report produced no data for {start_sp!r}: "
            f"rc={proc.returncode} {(proc.stderr or proc.stdout).strip()[:200]}"
        )
        return {}
    return _parse_stacked_device_time(out_csv)


def _log_per_op_device_stats(ops_csv, chunk_id, layer_id, iter_ids):
    """Build & log a per-op x per-iteration DEVICE-KERNEL-duration table (mean/std trailing)."""
    perf_bin = _resolve_tt_perf_report_bin()
    if perf_bin is None:
        logger.warning("[moe_isolated] tt-perf-report not found (PATH or venv); skipping per-op device stats")
        return

    per_iter = {}  # iter_id -> {op_code: device_time_sum_us}
    with tempfile.TemporaryDirectory(prefix="moe_stacked_") as workdir:
        for i in iter_ids:
            start_sp = f"MoE_isolated c{chunk_id} l{layer_id} iter{i} start"
            end_sp = f"MoE_isolated c{chunk_id} l{layer_id} iter{i} end"
            per_iter[i] = _stacked_device_time_for_iter(
                perf_bin, ops_csv, start_sp, end_sp, workdir, f"c{chunk_id}_l{layer_id}_i{i}"
            )

    measured = [i for i in iter_ids if per_iter.get(i)]
    if not measured:
        logger.warning(
            f"[moe_isolated] No per-op device data in {ops_csv} for chunk={chunk_id} layer={layer_id} "
            f"iters={iter_ids}. Record under tracy (python -m tracy -r -p -m pytest ...) and point "
            f"TT_MOE_OPS_CSV at the produced ops_perf_results_*.csv."
        )
        return

    op_codes = sorted({op for i in measured for op in per_iter[i]})

    # Per-op stats across the measured iterations.
    stats = {}  # op -> (values_per_measured_iter, mean, std, cov_pct)
    for op in op_codes:
        vals = [per_iter[i].get(op) for i in measured]
        present = [v for v in vals if v is not None]
        mean = statistics.mean(present) if present else 0.0
        std = statistics.stdev(present) if len(present) >= 2 else 0.0
        cov = (std / mean * 100.0) if mean else 0.0
        stats[op] = (vals, mean, std, cov)
    ordered_ops = sorted(op_codes, key=lambda o: stats[o][1], reverse=True)

    # Per-iteration totals (sum of all ops) -> overall variance row.
    totals = [sum(v for v in (per_iter[i].get(op) for op in op_codes) if v is not None) for i in measured]
    total_mean = statistics.mean(totals) if totals else 0.0
    total_std = statistics.stdev(totals) if len(totals) >= 2 else 0.0
    total_cov = (total_std / total_mean * 100.0) if total_mean else 0.0

    # --- Console table: Op Code | iter columns [μs] | Mean [μs] | Std [μs] | Std% ---
    op_w = min(60, max(len("Op Code"), *(len(o) for o in ordered_ops)))
    num_w = 9
    header = (
        f"{'Op Code':<{op_w}} "
        + " ".join(f"i{i:<{num_w-1}}" for i in measured)
        + f" {'Mean [μs]':>{num_w}} {'Std [μs]':>{num_w}} {'Std%':>6}"
    )

    def fmt_row(label, vals, mean, std, cov):
        cells = " ".join((f"{v:>{num_w}.2f}" if v is not None else f"{'-':>{num_w}}") for v in vals)
        return f"{label[:op_w]:<{op_w}} {cells} {mean:>{num_w}.2f} {std:>{num_w}.2f} {cov:>6.1f}"

    lines = [header, "-" * len(header)]
    for op in ordered_ops:
        vals, mean, std, cov = stats[op]
        lines.append(fmt_row(op, vals, mean, std, cov))
    lines.append("-" * len(header))
    lines.append(fmt_row("TOTAL (all ops)", totals, total_mean, total_std, total_cov))

    logger.info(
        f"[moe_isolated] PER-OP DEVICE-KERNEL duration [μs] chunk={chunk_id} layer={layer_id} "
        f"(n={len(measured)} iters: {measured}) from {ops_csv}\n" + "\n".join(lines)
    )

    # --- Also persist the full matrix next to the ops CSV for offline inspection ---
    out_csv = Path(ops_csv).parent / f"moe_stacked_stats_c{chunk_id}_l{layer_id}.csv"
    with open(out_csv, "w", newline="") as f:
        w = csv.writer(f)
        w.writerow(["Op Code"] + [f"iter{i}_us" for i in measured] + ["mean_us", "std_us", "std_pct"])
        for op in ordered_ops:
            vals, mean, std, cov = stats[op]
            w.writerow(
                [op] + [("" if v is None else f"{v:.2f}") for v in vals] + [f"{mean:.2f}", f"{std:.2f}", f"{cov:.2f}"]
            )
        w.writerow(
            ["TOTAL (all ops)"]
            + [f"{t:.2f}" for t in totals]
            + [f"{total_mean:.2f}", f"{total_std:.2f}", f"{total_cov:.2f}"]
        )
    logger.info(f"[moe_isolated] per-op device-kernel matrix written to {out_csv}")


@pytest.mark.parametrize("overlap_shared_expert_with_dispatch", [False, True], ids=["overlap_off", "overlap_on"])
@pytest.mark.parametrize("num_iters", _NUM_ITERS, ids=[f"iters{i}" for i in _NUM_ITERS])
@pytest.mark.parametrize("layer_id", _LAYER_IDS, ids=[f"layer{i}" for i in _LAYER_IDS])
@pytest.mark.parametrize("chunk_id", _CHUNK_IDS, ids=[f"chunk{i}" for i in _CHUNK_IDS])
@pytest.mark.parametrize(
    "mesh_device, device_params, num_links, topology",
    [
        pytest.param(
            (8, 4),
            {
                "fabric_config": ttnn.FabricConfig.FABRIC_1D,
                "fabric_router_config": create_fabric_router_config(max_payload_size=KimiK26Config.FABRIC_PAYLOAD_SIZE),
                "l1_small_size": 512,
            },
            NUM_LINKS,
            ttnn.Topology.Linear,
            marks=pytest.mark.requires_mesh_topology(mesh_shape=(8, 4), topology="mesh-8x4"),
            id="mesh-8x4",
        ),
    ],
    indirect=["mesh_device", "device_params"],
)
@pytest.mark.timeout(0)
def test_moe_layer_isolated(
    mesh_device,
    device_params,
    num_links,
    topology,
    chunk_id,
    layer_id,
    num_iters,
    overlap_shared_expert_with_dispatch,
):
    """Replay one (chunk_id, layer_id) MoE forward from a captured activation, num_iters times.

    Iteration 0 is a warmup that builds the program cache and is excluded from the
    reported mean/median/std; iterations 1..num_iters-1 are the measured samples.
    """
    cap_dir = os.environ.get("TT_DS_CAPTURE_MOE_DIR")
    if not cap_dir:
        pytest.skip("Set TT_DS_CAPTURE_MOE_DIR to the dir holding the captured MoE inputs")
    if layer_id < NUM_DENSE_LAYERS:
        pytest.skip(f"layer {layer_id} is a dense (non-MoE) layer")

    path = moe_input_capture.capture_path(cap_dir, chunk_id, layer_id)
    if not path.exists():
        pytest.skip(f"No captured MoE input for chunk={chunk_id} layer={layer_id} at {path}")

    cache_env = os.environ.get("TT_KIMI_PREFILL_TTNN_CACHE")
    if not cache_env:
        pytest.skip("Set TT_KIMI_PREFILL_TTNN_CACHE (same as the chunked test)")
    num_devices = mesh_device.get_num_devices()
    weight_cache_path = (
        Path(cache_env) / f"kimi_k2_6_bh_{num_devices}dev" / f"{mesh_device.shape[0]}x{mesh_device.shape[1]}"
    )

    experts_per_chip = KimiK26Config.NUM_ROUTED_EXPERTS // num_devices  # 384 / 32 = 12
    # Required before check_cache_complete / any cache pattern_exists lookup (mirrors
    # TtPrefillTransformer.__init__, which calls init_checker(cache_path) once up front).
    init_checker(weight_cache_path)
    if not TtMoe.check_cache_complete(weight_cache_path, layer_id, experts_per_chip):
        pytest.skip(f"Weight cache incomplete for layer {layer_id} under {weight_cache_path}")

    # --- Load the captured MoE input activation + its metadata ---
    with safe_open(str(path), framework="pt") as f:
        x_host = f.get_tensor("x")
        meta = f.metadata() or {}
    orig_dtype = _parse_ttnn_dtype(meta.get("orig_dtype", "DataType.BFLOAT16"))
    orig_layout = _parse_ttnn_layout(meta.get("orig_layout", "Layout.TILE"))
    actual_isl = int(meta["actual_isl"]) if meta.get("actual_isl") else None
    logger.info(
        f"[moe_isolated] chunk={chunk_id} layer={layer_id} x.shape={tuple(x_host.shape)} "
        f"dtype={orig_dtype} layout={orig_layout} actual_isl={actual_isl} cache={weight_cache_path}"
    )

    # --- Build ONE layer's MoE identically to TtPrefillBlock (weights from cache) ---
    moe = TtPrefillBlock._build_moe(
        mesh_device=mesh_device,
        model_cfg=KimiK26Config,
        state_dict={},  # all weights come from the TTNN cache
        seq_len=CHUNK,
        sp_axis=SP_AXIS,
        emb_dim=EMB_DIM,
        num_links=num_links,
        topology=topology,
        gate_fallback_mode=GateComputeMode.DEVICE_FP32,
        routed_expert_activations_dtype=ttnn.bfloat8_b,
        routed_expert_weights_dtype=ttnn.bfloat4_b,
        shared_expert_activations_dtype=ttnn.bfloat16,
        shared_expert_weights_dtype=ttnn.bfloat8_b,
        dispatch_buffer_capacity_factor=DISPATCH_BUFFER_CAPACITY_FACTOR,
        weight_cache_path=weight_cache_path,
        layer_idx=layer_id,
        routing_use_l1_small_for_semaphores=True,
        is_balanced=False,
        overlap_shared_expert_with_dispatch=overlap_shared_expert_with_dispatch,
    )

    # (Re)create the sharded device input from the captured host tensor. TtMoe.forward
    # deallocates its input x (tt_moe.py:571), so x is rebuilt for every iteration.
    # Capture used ConcatMesh2dToTensor(dims=(0, -1)); ShardTensor2dMesh(dims=(0, -1)) is its inverse.
    def make_x():
        return ttnn.from_torch(
            x_host,
            device=mesh_device,
            dtype=orig_dtype,
            layout=orig_layout,
            memory_config=ttnn.DRAM_MEMORY_CONFIG,
            mesh_mapper=ttnn.ShardTensor2dMesh(mesh_device, mesh_shape=tuple(mesh_device.shape), dims=(0, -1)),
        )

    # --- Timed iterations: run the MoE forward exactly as _moe_path does, num_iters times ---
    # The device-sync brackets (mirroring _moe_path with TT_MOE_PROF=1) make each sample pure
    # dispatch+device time for this layer. iter 0 warms the program cache and is dropped below.
    prof_key = f"moe_isolated_c{chunk_id}_l{layer_id}"
    profiler.clear()
    moe_out_shape = None
    for i in range(num_iters):
        x = make_x()

        # Measurement
        ttnn.synchronize_device(mesh_device)
        signpost(f"MoE_isolated c{chunk_id} l{layer_id} iter{i} start")
        profiler.start(prof_key)
        moe_out, _ = moe.forward(x, return_intermediates=False, actual_isl=actual_isl, padding_side="right")
        ttnn.synchronize_device(mesh_device)
        profiler.end(prof_key)
        signpost(f"MoE_isolated c{chunk_id} l{layer_id} iter{i} end")

        # Logging and cleanup
        assert moe_out is not None
        moe_out_shape = moe_out.shape
        ttnn.deallocate(moe_out)
        tag = " (warmup, excluded)" if i == 0 else ""
        logger.info(
            f"[moe_isolated] chunk={chunk_id} layer={layer_id} iter={i} "
            f"moe_ms={profiler.times[prof_key][-1] * 1000:.3f}{tag}"
        )

    # --- Stats over iters 1..num_iters-1 (warmup iter 0 dropped) ---
    samples_ms = [t * 1000.0 for t in profiler.times[prof_key][1:]]
    assert samples_ms, f"num_iters={num_iters} leaves no post-warmup samples; use num_iters >= 2"
    mean_ms = statistics.mean(samples_ms)
    median_ms = statistics.median(samples_ms)
    std_ms = statistics.stdev(samples_ms) if len(samples_ms) >= 2 else 0.0
    samples_str = (
        f" | samples_ms={[round(s, 3) for s in samples_ms]}"
        if num_iters <= 25
        else " | samples_ms suppressed (num_iters > 25)"
    )
    logger.info(
        f"[moe_isolated] SUMMARY chunk={chunk_id} layer={layer_id} out_shape={moe_out_shape} "
        f"num_iters={num_iters} (1 warmup dropped, n={len(samples_ms)} samples): "
        f"mean={mean_ms:.3f} ms | median={median_ms:.3f} ms | std={std_ms:.3f} ms{samples_str}"
    )
    # Per-op DEVICE-KERNEL-duration stats are computed by test_moe_stacked_op_stats, which
    # consumes the tracy-produced ops_perf_results_*.csv (unavailable until this process exits).


@pytest.mark.timeout(0)
def test_moe_stacked_op_stats():
    """Post-tracy analysis: bisect per-op DEVICE KERNEL duration from a recorded ops CSV.

    This is NOT a device test -- it opens no mesh and runs no forward. It consumes the
    ``ops_perf_results_*.csv`` that tracy produces from a recording of
    ``test_moe_layer_isolated`` and, for each recorded ``(chunk, layer)``, prints a
    per-op x per-iteration DEVICE-KERNEL-duration table (μs) with trailing mean/std, plus
    a ``moe_stacked_stats_c{chunk}_l{layer}.csv`` next to the input CSV.

    The recording test's ops CSV is only written after that pytest process exits, so run
    this as a second, separate invocation and point TT_MOE_OPS_CSV at the produced file::

        # 1) record under tracy (produces .../reports/<ts>/ops_perf_results_<ts>.csv)
        python -m tracy -r -p -m pytest \
          "models/demos/deepseek_v3_d_p/tests/perf/test_moe_layer_isolated.py::\
test_moe_layer_isolated[blackhole-mesh-8x4-chunk5-layer27-iters10]" -xvs

        # 2) analyze that CSV (no device needed)
        TT_MOE_OPS_CSV=generated/profiler/reports/<ts>/ops_perf_results_<ts>.csv \
          pytest "models/demos/deepseek_v3_d_p/tests/perf/test_moe_layer_isolated.py::\
test_moe_stacked_op_stats" -xvs

    Skips (does not fail) when TT_MOE_OPS_CSV is unset or points at a missing file, so a
    plain non-tracy run of the module is unaffected. The recorded ``(chunk, layer)`` groups
    and their iteration counts are discovered from the CSV's signposts -- no need to match
    the recording's parametrization here.
    """
    csv_env = os.environ.get("TT_MOE_OPS_CSV")
    if not csv_env:
        pytest.skip("Set TT_MOE_OPS_CSV to a tracy-produced ops_perf_results_*.csv to analyze")
    ops_csv = Path(csv_env)
    if not ops_csv.exists():
        pytest.skip(f"TT_MOE_OPS_CSV does not exist: {ops_csv}")

    groups = _discover_signpost_groups(ops_csv)
    if not groups:
        pytest.skip(f"No 'MoE_isolated' signposts found in {ops_csv}; record under tracy first")

    for (chunk_id, layer_id), iter_ids in sorted(groups.items()):
        # Drop the warmup iter 0 (program-cache build), matching test_moe_layer_isolated.
        measured = [i for i in iter_ids if i != 0]
        if not measured:
            logger.warning(
                f"[moe_isolated] chunk={chunk_id} layer={layer_id}: only warmup iter present "
                f"(iters={iter_ids}); record with num_iters >= 2 for stats"
            )
            continue
        _log_per_op_device_stats(ops_csv, chunk_id, layer_id, measured)
